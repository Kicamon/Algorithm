/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, m;
        cin >> n >> m;
        vector<int> a(m + 1);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                a[x]++;
        }
        for (int i = 1; i < m; ++i) {
                if (a[i] % (i + 1)) {
                        cout << "right" << endl;
                        return 0;
                }
                a[i + 1] += a[i] / (i + 1);
        }
        cout << (a[m] ? "left" : "right") << endl;

        return 0;
}
