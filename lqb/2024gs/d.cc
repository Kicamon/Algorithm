/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        int c = 0;
        cin >> c;
        priority_queue<int, vector<int>, less<int>> q;
        for (int i = 1, t; i < n; ++i) {
                cin >> t;
                q.push(t - c);
                c = t;
        }
        while (m--) {
                int t = q.top();
                q.pop();
                q.push(t + 1 >> 1);
                q.push(t >> 1);
        }
        int res = q.top() + 1 >> 1;
        q.pop();
        while (!q.empty()) {
                if (q.top() <= res) {
                        break;
                } else {
                        res = q.top();
                        q.pop();
                }
        }
        cout << res << endl;

        return 0;
}
