// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

ll qmod(ll a, ll b, ll mod) {
    ll res = 1;
    a %= mod;
    for (; b; b >>= 1) {
        if (b & 1) {
            res = res * a % mod;
        }
        a = a * a % mod;
    }
    return res;
}

void solve() {
    int p;
    cin >> p;
    int m = p - 1;
    vector<int> q;
    for (int i = 2; i * i <= m; ++i) {
        if (m % i == 0) {
            q.push_back(i);
            while (m % i == 0) {
                m /= i;
            }
        }
    }
    if (m > 1) {
        q.push_back(m);
    }
    for (int g = 1; g < p; ++g) {
        bool val = true;
        for (int x : q) {
            if (qmod(g, p / x, p) == 1) {
                val = false;
                break;
            }
        }
        if (val) {
            cout << g << endl;
            return;
        }
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
