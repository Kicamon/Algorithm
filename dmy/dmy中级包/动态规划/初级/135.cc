/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n), dp(n + 1);
        int len = 0;
        for (int &x : a) {
                cin >> x;
                int l = 0, r = len;
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (x < dp[mid]) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                len = max(len, l + 1);
                dp[l + 1] = x;
        }
        cout << len << endl;

        return 0;
}
