/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<vector<array<int, 2>>> p(n + 1);
        vector<int> dp(n + 1, 1e9);
        for (int i = 0, a, b, c; i < m; ++i) {
                cin >> a >> b >> c;
                p[a].push_back({ b, c });
        }
        dp[1] = 0;
        for (int i = 1; i <= n; ++i) {
                for (auto x : p[i]) {
                        dp[x[0]] = min(dp[x[0]], dp[i] + x[1]);
                }
        }
        cout << dp[n] << endl;

        return 0;
}
