#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e4 + 10;

vector<int> e[N];
int dnf[N], low[N], bel[N], sz[N], idx, cnt;
bool ins[N];
int out[N];
stack<int> stk;

void dfs(int u) {
        dnf[u] = low[u] = ++idx;
        ins[u] = true;
        stk.push(u);
        for (int v : e[u]) {
                if (!dnf[v])
                        dfs(v);
                if (ins[v])
                        low[u] = min(low[u], low[v]);
        }
        if (low[u] == dnf[u]) {
                ++cnt;
                while (true) {
                        int v = stk.top();
                        stk.pop();
                        bel[v] = cnt;
                        ins[v] = false;
                        sz[cnt]++;
                        if (v == u)
                                break;
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int n, m;
        cin >> n >> m;
        while (m--) {
                int a, b;
                cin >> a >> b;
                e[a].push_back(b);
        }
        for (int i = 1; i <= n; ++i)
                if (!dnf[i])
                        dfs(i);
        for (int u = 1; u <= n; ++u) {
                for (int v : e[u]) {
                        if (bel[v] != bel[u])
                                out[bel[u]]++;
                }
        }
        int ot = 0;
        for (int i = 1; i <= cnt; ++i) {
                if (!out[i]) {
                        if (!ot)
                                ot = i;
                        else {
                                cout << 0 << endl;
                                return 0;
                        }
                }
        }
        cout << sz[ot] << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
