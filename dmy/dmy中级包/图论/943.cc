// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e5 + 10, M = 3e5 + 10;
int n, m;
vector<int> e[N];
int dfn[N], low[N], bel[N], idx, cnt;
bool ins[N];
stack<int> stk;
vector<vector<int>> scc;

void dfs(int u) {
        dfn[u] = low[u] = ++idx;
        ins[u] = true;
        stk.push(u);
        for (auto v : e[u]) {
                if (!dfn[v])
                        dfs(v);
                if (ins[v])
                        low[u] = min(low[u], low[v]);
        }
        if (dfn[u] == low[u]) {
                vector<int> c;
                ++cnt;
                while (true) {
                        int v = stk.top();
                        stk.pop();
                        c.push_back(v);
                        ins[v] = false;
                        bel[v] = cnt;
                        if (v == u)
                                break;
                }
                sort(c.begin(), c.end());
                scc.push_back(c);
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        cin >> n >> m;
        for (int i = 0, a, b; i < m; ++i) {
                cin >> a >> b;
                e[a].push_back(b);
        }

        for (int i = 1; i <= n; ++i)
                if (!dfn[i])
                        dfs(i);
        sort(scc.begin(), scc.end());
        for (auto u : scc) {
                for (auto v : u)
                        cout << v << ' ';
                cout << endl;
        }

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
