#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

const int N = 1e6 + 10;
uint f[N], g[N], mu[N], idx;
int p[N], pr[N / 5];
int n;

unsigned int A, B, C;
inline unsigned int rng61() {
        A ^= A << 16;
        A ^= A >> 5;
        A ^= A << 1;
        unsigned int t = A;
        A = B;
        B = C;
        C ^= t ^ A;
        return C;
}

void remu() {
        p[1] = 1, mu[1] = 1;
        for (int i = 2; i <= n; ++i) {
                if (!p[i]) {
                        p[i] = i, mu[i] = (uint)-1, pr[++idx] = i;
                }
                for (int j = 0; j <= idx && i * pr[j] <= n; ++j) {
                        p[i * pr[j]] = pr[j];
                        if (p[i] == pr[j]) {
                                mu[i * pr[j]] = 0;
                                break;
                        } else {
                                mu[i * pr[j]] = (uint)-mu[i];
                        }
                }
        }

        for (int i = 1; i <= n; ++i) {
                for (int j = 1; i * j <= n; ++j) {
                        g[i * j] += f[i] * mu[j];
                }
        }
        uint ans = 0;
        for (int i = 1; i <= n; ++i) {
                ans ^= g[i];
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> A >> B >> C;
        for (int i = 1; i <= n; i++)
                f[i] = rng61();

        return 0;
}
