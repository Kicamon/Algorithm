// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

ll qmi(ll a, ll b, ll p) {
        ll res = 1;
        a %= p;
        while (b) {
                if (b & 1) {
                        res = res * a % p;
                }
                b >>= 1;
                a = a * a % p;
        }
        return res;
}

int calc(int p) {
        if (p == 1) {
                return 0;
        }
        int phip = p, q = p;
        for (int i = 2; i * i <= q; ++i) {
                if (q % i == 0) {
                        phip = phip / i * (i - 1);
                        while (q % i == 0) {
                                q /= i;
                        }
                }
        }
        if (q > 1) {
                phip = phip / q * (q - 1);
        }
        return qmi(2ll, (ll)calc(phip) + phip, (ll)p);
}

void solve() {
        int p;
        cin >> p;
        cout << calc(p) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
