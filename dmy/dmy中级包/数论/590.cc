// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

const int N = 1e7 + 10, M = 1e7;
int smu[N], mu[N], idx;
int p[N], pr[N / 5];

void solve() {
        int n, m;
        cin >> n >> m;
        if (n > m) {
                swap(n, m);
        }
        ll ans = 0;
        for (int l = 1; l <= n; ++l) { // 整除分块
                int n1 = n / l, m1 = m / l;
                int r = min(n / n1, m / m1);
                ans += 1ll * (smu[r] - smu[l - 1]) * n1 * m1;
                l = r;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        p[1] = 1, mu[1] = 1;
        for (int i = 2; i <= M; ++i) {
                if (!p[i]) {
                        p[i] = i, mu[i] = (uint)-1, pr[++idx] = i;
                }
                for (int j = 0; j <= idx && i * pr[j] <= M; ++j) {
                        p[i * pr[j]] = pr[j];
                        if (p[i] == pr[j]) {
                                mu[i * pr[j]] = 0;
                                break;
                        } else {
                                mu[i * pr[j]] = (uint)-mu[i];
                        }
                }
        }

        for (int i = 1; i < M; ++i) {
                smu[i] = smu[i - 1] + mu[i];
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
