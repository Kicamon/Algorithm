#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e5 + 10;

int p;
int q[N], t;

ll qmod(ll a, ll b, ll mod) {
        ll ans = 1;
        a %= mod;
        while (b) {
                if (b & 1) {
                        ans = ans * a % mod;
                }
                b >>= 1;
                a = a * a % mod;
        }
        return ans;
}

void solve() {
        int a;
        cin >> a;
        int d = p - 1;
        for (int i = 0; i < t; ++i) {
                while (d % q[i] == 0 && qmod(a, d / q[i], p) == 1) {
                        d /= q[i];
                }
        }
        cout << d << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int T;
        cin >> p >> T;
        int m = p - 1;
        for (int i = 2; i * i <= m; ++i) {
                if (m % i == 0) {
                        q[t++] = i;
                        while (m % i == 0) {
                                m /= i;
                        }
                }
        }
        if (m > 1) {
                q[t++] = m;
        }

        while (T--)
                solve();

        return 0;
}
