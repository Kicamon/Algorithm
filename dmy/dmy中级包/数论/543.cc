// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

ll qmod(ll a, ll b, ll mod) {
        ll ans = 1;
        a %= mod;
        for (; b; b >>= 1) {
                if (b & 1) {
                        ans = ans * a % mod;
                }
                a = a * a % mod;
        }
        return ans;
}

void solve() {
        int a, b, m;
        cin >> a >> b >> m;
        int T = sqrt(m) + 1;
        unordered_map<int, int> hs;
        ll v = qmod(a, T, m), d = 1;
        for (int i = 1; i <= T; ++i) {
                d = d * v % m;
                if (!hs.count(d)) {
                        hs[d] = i * T;
                }
        }
        int ans = m + 1;
        d = b;
        for (int i = 1; i <= T; ++i) {
                d = d * a % m;
                if (hs.count(d)) {
                        ans = min(ans, hs[d] - i);
                }
        }
        if (ans >= m) {
                ans = -1;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
