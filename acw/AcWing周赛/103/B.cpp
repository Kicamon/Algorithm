// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 4010;

vector<int> e[N];

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n, m;
    cin >> n >> m;
    while (m--)
    {
        int a, b;
        cin >> a >> b;
        e[a].push_back(b);
        e[b].push_back(a);
    }
    for (int i = 1; i <= n; ++i)
        sort(e[i].begin(), e[i].end());
    int res = INT_MAX;
    for (int i = 1; i <= n; ++i)
    {
        int num = (int)e[i].size();
        if (num < 2)
            continue;
        for (int x : e[i])
        {
            for (int y : e[x])
            {
                if (i == y)
                    continue;
                int k = lower_bound(e[i].begin(), e[i].end(), y) - e[i].begin();
                if (k < num)
                    res = min(res, num + (int)e[x].size() + (int)e[y].size() - 6);
            }
        }
    }
    if (res == INT_MAX)
        res = -1;
    cout << res << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
