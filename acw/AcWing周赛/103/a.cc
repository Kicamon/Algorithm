void solve() {
        int a, b, c;
        cin >> a >> b >> c;
        int x = 0;
        while (true) {
                if (a * x > c) {
                        cout << "No\n";
                        return;
                } else if ((c - a * x) % b == 0) {
                        cout << "Yes\n";
                        return;
                }
                x++;
        }
}

