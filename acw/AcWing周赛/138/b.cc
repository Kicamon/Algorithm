/* #pragma GCC optimize(2) */
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, q;
        cin >> n >> q;
        vector<ll> a(n + 1), k(q + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                a[i] += a[i - 1];
        }
        for (int i = 1; i <= q; ++i) {
                cin >> k[i];
        }
        ll res = 0;
        for (int i = 1; i <= q; ++i) {
                res += k[i];
                int l = 1, r = n + 1;
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (a[mid] <= res) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                if (r == n + 1) {
                        cout << n << endl;
                        res = 0;
                } else {
                        cout << n - r + 1 << endl;
                }
        }

        return 0;
}
