/* #pragma GCC optimize(2) */
#include <array>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<array<int, 3>> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i][0] >> a[i][1];
        a[i][2] = i;
    }
    sort(all(a), [&](auto x, auto y) {
        if (x[0] != y[0]) {
            return x[0] < y[0];
        } else if (x[1] != y[1]) {
            return x[1] > y[1];
        } else {
            return x[2] < y[2];
        }
    });
    int idx = 0;
    int i;
    for (i = 1; i < n; ++i) {
        if (a[i][1] <= a[idx][1]) {
            break;
        } else {
            idx = i;
        }
    }
    if (i == n) {
        cout << "-1 -1" << endl;
    } else {
        cout << a[i][2] + 1 << ' ' << a[idx][2] + 1 << endl;
    }

    return 0;
}
