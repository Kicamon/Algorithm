/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <functional>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    map<int, int> f;
    int res = 0, end = -1;
    for (int i = 1; i <= n; ++i) {
        f[a[i]] = max(f[a[i] - 1] + 1, 0);
        if (f[a[i]] > res) {
            res = f[a[i]];
            end = i;
        }
    }
    cout << res << endl;
    for (int i = 1; i <= n && res; ++i) {
        if (a[i] == a[end] - res + 1) {
            cout << i << ' ';
            res--;
        }
    }
    cout << endl;

    return 0;
}
