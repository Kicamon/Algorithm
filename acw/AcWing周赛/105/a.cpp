#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i)
        cin >> a[i];
    int res = 0;
    for (int i = 1; i < n - 1; ++i)
        if (a[i] < a[i - 1] && a[i] < a[i + 1])
            res++;
        else if (a[i] > a[i - 1] && a[i] > a[i + 1])
            res++;

    cout << res << endl;

    return 0;
}
