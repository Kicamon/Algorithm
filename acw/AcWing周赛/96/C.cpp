#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl

template <class T> struct BIT
{
    int size;
    vector<T> a, b;
    BIT(int n) : a(n + 1), b(n + 1)
    {
        size = n;
    };
    void resize(int x)
    {
        size = x;
    }

    int lowbit(int x)
    {
        return x & -x;
    }

    void modify(int x, int op, T d = 1)
    {
        if (op == 1)
        {
            for (int i = x; i <= size; i += lowbit(i))
                a[i] += d;
        }
        else
        {
            for (int i = x; i <= size; i += lowbit(i))
                b[i] += d;
        }
    }

    T query(int x, int op)
    {
        assert(x <= size);
        T res = 0;
        if (op == 1)
        {
            for (int i = x; i; i -= lowbit(i))
                res += a[i];
        }
        else
        {
            for (int i = x; i; i -= lowbit(i))
                res += b[i];
        }

        return res;
    }
};

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n, k, a, b, m;
    cin >> n >> k >> a >> b >> m;
    vint num(n + 1);
    BIT<int> c(n);
    while (m--)
    {
        int op;
        cin >> op;
        if (op == 1)
        {
            int x, y;
            cin >> x >> y;
            c.modify(x, 1, min(num[x] + y, b) - min(num[x], b));
            c.modify(x, 2, min(num[x] + y, a) - min(num[x], a));
            num[x] += y;
        }
        else
        {
            int p;
            cin >> p;
            cout << c.query(p - 1, 1) + c.query(n, 2) - c.query(p + k - 1, 2) << endl;
        }
    }

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
