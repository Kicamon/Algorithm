#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int n, m;
        cin >> n >> m;
        vint f(m + 1);
        vector<array<int, 2>> good;
        for (int i = 0; i < n; ++i) {
                int v, w, s;
                cin >> v >> w >> s;
                for (int j = 1; j <= s; s -= j, j *= 2)
                        good.pb({ v * j, w * j });
                if (s)
                        good.pb({ v * s, w * s });
        }

        for (auto g : good) {
                for (int j = m; j >= g[0]; --j)
                        f[j] = max(f[j], f[j - g[0]] + g[1]);
        }
        cout << f[m] << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
