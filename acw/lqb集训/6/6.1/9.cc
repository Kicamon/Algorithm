        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int n, m;
        cin >> n >> m;
        vint f(m + 1);
        for (int i = 0; i < n; ++i) {
                int k;
                cin >> k;
                vint v(k), w(k);
                for (int j = 0; j < k; ++j)
                        cin >> v[j] >> w[j];
                for (int j = m; j > 0; --j)
                        for (int cnt = 0; cnt < k; ++cnt)
                                if (j >= v[cnt])
                                        f[j] = max(f[j], f[j - v[cnt]] + w[cnt]);
        }
        cout << f[m] << endl;

        // finish = clock();
