    int n, m;
    cin >> n >> m;
    vint f(m + 1);
    for (int i = 0; i < n; ++i)
    {
        int v, w;
        cin >> v >> w;
        for (int j = v; j <= m; ++j)
            f[j] = max(f[j], f[j - v] + w);
    }
