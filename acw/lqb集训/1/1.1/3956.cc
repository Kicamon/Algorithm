#include <array>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;

void solve()
{
	int n;
	cin >> n;
	vector<ll> a(n + 1);
	for(int i = 1;i <= n;++i)
	{
		cin >> a[i];
		a[i] += a[i - 1];
	}
	if(n < 3 || a[n] % 3)
	{
		cout << 0 << endl;
		return;
	}
	array<ll, 2> f = {0,0};
	int k = a[n] / 3;
	for(int i = 1;i < n;++i)
	{
		if(a[i] == k * 2)
			f[1] += f[0];
		if(a[i] == k)
			f[0]++;
	}
	cout << f[1] << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
