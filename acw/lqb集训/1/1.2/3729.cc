//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n;
    cin >> n;
    vint a(n);
    for (int i = 0; i < n; ++i)
        cin >> a[i];
    vint b(n + 1);
    for (int i = 1; i <= n; ++i)
    {
        if (a[i - 1])
        {
            b[max(1, i - a[i - 1] + 1)]++;
            if (i + 1 <= n)
                b[i + 1]--;
        }
    }
    for (int i = 1; i <= n; ++i)
        b[i] += b[i - 1];
    for (int i = 1; i <= n; ++i)
        cout << min(b[i], 1) << ' ';
    cout << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
