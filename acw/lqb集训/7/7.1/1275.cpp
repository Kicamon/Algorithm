#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 2e5 + 10;
int n, m;

struct node
{
    int num;
} tr[N << 2];

void pushup(int u)
{
    tr[u].num = max(tr[u << 1].num, tr[u << 1 | 1].num);
}

void modify(int u, int x, int k, int l, int r)
{
    if (l == r)
    {
        tr[u] = {k};
        return;
    }
    int mid = l + r >> 1;
    if (x <= mid)
        modify(u << 1, x, k, l, mid);
    else
        modify(u << 1 | 1, x, k, mid + 1, r);
    pushup(u);
}

int query(int u, int l, int r, int ml, int mr)
{
    if (l <= ml && mr <= r)
        return tr[u].num;
    int res = 0;
    int mid = ml + mr >> 1;
    if (l <= mid)
        res = max(res, query(u << 1, l, r, ml, mid));
    if (r > mid)
        res = max(res, query(u << 1 | 1, l, r, mid + 1, mr));
    return res;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    cin >> n >> m;
    int idx = 0, a = 0;
    for (int i = 0; i < n; ++i)
    {
        char op;
        cin >> op;
        int k;
        cin >> k;
        if (op == 'A')
        {
            k = (1ll * k + a) % m;
            modify(1, ++idx, k, 1, n);
        }
        else
        {
            a = query(1, idx - k + 1, idx, 1, n);
            cout << a << endl;
        }
    }

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
