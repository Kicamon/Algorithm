int input = 0;
const int N = 5010;

int w[N][N];

void solve()
{
	int n,r;
	cin >> n >> r;
	r = min(r,5001);
	for(int i = 0,a,b,c;i < n;++i)
	{
		cin >> a >> b >> c;
		w[a + 1][b + 1] += c;
	}
	for(int i = 1;i < N;++i)
		for(int j = 1;j < N;++j)
			w[i][j] += w[i - 1][j] + w[i][j - 1] - w[i - 1][j - 1];
	int res = 0;
	for(int i = r;i < N;++i)
		for(int j = r;j < N;++j)
			res = max(res,w[i][j] - w[i - r][j] - w[i][j - r] + w[i - r][j - r]);
	cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
