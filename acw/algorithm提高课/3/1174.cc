#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e4 + 10;

int n, m;
vector<int> e[N];
int dfn[N], low[N], bel[N], out[N], sz[N], idx, cnt;
bool ins[N];
stack<int> stk;

void dfs(int u) {
        dfn[u] = low[u] = ++idx;
        ins[u] = true;
        stk.push(u);
        for (int v : e[u]) {
                if (!dfn[v])
                        dfs(v);
                if (ins[v])
                        low[u] = min(low[u], low[v]);
        }
        if (dfn[u] == low[u]) {
                ++cnt;
                while (true) {
                        int v = stk.top();
                        ins[v] = false;
                        bel[v] = cnt;
                        sz[cnt]++;
                        stk.pop();
                        if (v == u)
                                break;
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        cin >> n >> m;
        for (int i = 0; i < m; ++i) {
                int a, b;
                cin >> a >> b;
                e[a].push_back(b);
        }
        for (int i = 1; i <= n; ++i)
                if (!dfn[i])
                        dfs(i);
        for (int i = 1; i <= n; ++i) {
                for (auto v : e[i])
                        if (bel[i] != bel[v])
                                out[bel[i]]++;
        }
        int cntn = 0, cnts = 0;
        for (int i = 1; i <= cnt; ++i) {

                if (!out[i]) {
                        if (cntn) {
                                cnts = 0;
                                break;
                        }
                        cntn++;
                        cnts += sz[i];
                }
        }
        cout << cnts << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
