// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 110, M = 210;

int n, m;
struct node {
        int a, b, c;
};

int f[N];
int find(int x) {
        if (x != f[x])
                f[x] = find(f[x]);
        return f[x];
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        cin >> n >> m;
        vector<node> p(m);
        for (int i = 1; i <= n; ++i)
                f[i] = i;
        int a, b, c;
        for (int i = 0; i < m; ++i) {
                cin >> a >> b >> c;
                p[i] = { a, b, c };
        }
        sort(all(p), [&](node a, node b) { return a.c < b.c; });
        int res = 0;
        for (int i = 0; i < m; ++i) {
                int a = p[i].a, b = p[i].b, c = p[i].c;
                a = find(a), b = find(b);
                if (a != b)
                        f[a] = b;
                else
                        res += c;
        }
        cout << res << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
