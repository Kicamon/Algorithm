int input = 0;
const int N = 110, inf = 0x3f3f3f3f;
int w[N][N];

void solve()
{
    int n, m;
    cin >> n >> m;
    memset(w, 0x3f, sizeof w);
    for (int i = 0; i < m; ++i)
    {
        int a, b, c;
        cin >> a >> b >> c;
        w[a][b] = w[b][a] = min(w[a][b], c);
    }
    for (int i = 1; i <= n; ++i)
        w[i][i] = 0;

    for (int k = 1; k <= n; ++k)
        for (int i = 1; i <= n; ++i)
            for (int j = 1; j <= n; ++j)
                w[i][j] = min(w[i][j], w[i][k] + w[k][j]);
    int res = 0;
    for (int i = 1; i <= n; ++i)
        res = max(res, w[1][i]);
    if (res == inf)
        res = -1;
    cout << res << endl;
}

signed main()
{
    // ios::sync_with_stdio(false);
    // cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
