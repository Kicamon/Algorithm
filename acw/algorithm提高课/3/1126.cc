int input = 0;

const int N = 2010, M = 2e5 + 10;
const double inf = 0x3f3f3f3f;
const double enf = 1e-8;
#define pdi pair<double, int>

int e[M], ne[M], w[M], h[N], idx;

void add(int a, int b, int c) {
        e[idx] = b, ne[idx] = h[a], w[idx] = c, h[a] = idx++;
}

double dist[N];
bool vis[N];
int n, m;

double dijkstra(int start, int end) {
        for (int i = 1; i <= n; ++i)
                dist[i] = inf;
        dist[start] = 100;
        priority_queue<pdi, vector<pdi>, greater<pdi>> q;
        q.push({ 100, start });
        while (!q.empty()) {
                pdi t = q.top();
                q.pop();

                if (vis[t.second])
                        continue;
                vis[t.second] = true;

                for (int i = h[t.second]; ~i; i = ne[i]) {
                        int j = e[i];
                        if (dist[j] > dist[t.second] * 100 / (100 - w[i])) {
                                dist[j] = dist[t.second] * 100 / (100 - w[i]);
                                q.push({ dist[j], j });
                        }
                }
        }

        return dist[end];
}

void solve() {
        memset(h, -1, sizeof h);
        cin >> n >> m;
        while (m--) {
                int a, b, c;
                cin >> a >> b >> c;
                add(a, b, c), add(b, a, c);
        }
        int s, e;
        cin >> s >> e;

        printf("%.8lf\n", dijkstra(e, s));
}

