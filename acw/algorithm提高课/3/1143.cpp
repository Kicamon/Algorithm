// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 2010, M = 10010;

int n, m;

struct node
{
    int p, a, b, w;
};

int f[N];

int find(int x)
{
    if (x != f[x])
        f[x] = find(f[x]);
    return f[x];
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    cin >> n >> m;
    vector<node> p(m);
    for (int i = 0; i < n; ++i)
        f[i] = i;
    for (int i = 0, a, b, c, d; i < m; ++i)
    {
        cin >> a >> b >> c >> d;
        p[i] = {a, b, c, d};
    }
    sort(all(p), [&](node a, node b) {
        if (a.p != b.p)
            return a.p < b.p;
        return a.w < b.w;
    });
    int res = 0;
    for (int i = 0; i < m; ++i)
    {
        int op = p[i].p, a = p[i].a, b = p[i].b, w = p[i].w;
        a = find(a), b = find(b);
        if (op == 1)
        {
            res += w;
            if (a != b)
                f[a] = b;
        }
        else
        {
            if (a != b)
            {
                res += w;
                f[a] = b;
            }
        }
    }
    cout << res << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
