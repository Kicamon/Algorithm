// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int inf = 0x3f3f3f3f;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int n;
        cin >> n;
        vint a(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
        }
        vector<vint> w(n + 1, vint(n + 1));
        for (int i = 1; i <= n; ++i)
                for (int j = 1; j <= n; ++j)
                        cin >> w[i][j];
        int res = inf;
        for (int i = 1; i <= n; ++i) {
                vector<bool> vis(n + 1);
                vint dist(n + 1, inf);
                dist[i] = a[i];
                int cnt = 0;
                for (int k = 0; k < n; ++k) {
                        int t = -1;
                        for (int j = 1; j <= n; ++j)
                                if (!vis[j] && (t == -1 || dist[j] < dist[t]))
                                        t = j;
                        cnt += dist[t];
                        vis[t] = true;
                        for (int j = 1; j <= n; ++j)
                                dist[j] = min(dist[j], w[t][j]);
                }
                res = min(res, cnt);
        }
        cout << res << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
