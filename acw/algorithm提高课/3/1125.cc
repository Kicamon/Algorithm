#include <bits/stdc++.h>
#include <random>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
const int N = 200;
const double inf = 1e20;
#define PDD pair<double, double>

int n;
PDD p[N];
double dist[N][N];
char g[N][N];
double maxd[N];

double get(PDD a, PDD b)
{
    double dx = a.first - b.first, dy = a.second - b.second;
    return sqrt(dx * dx + dy * dy);
}

void solve()
{
    cin >> n;
    for (int i = 0; i < n; ++i)
        cin >> p[i].first >> p[i].second;
    for (int i = 0; i < n; ++i)
        cin >> g[i];
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j)
            if (i != j)
            {
                if (g[i][j] == '1')
                    dist[i][j] = get(p[i], p[j]);
                else
                    dist[i][j] = inf;
            }
    for (int k = 0; k < n; ++k)
        for (int i = 0; i < n; ++i)
            for (int j = 0; j < n; ++j)
                dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j]);

    double res1 = 0;
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j)
            if (dist[i][j] < inf / 2)
            {
                maxd[i] = max(maxd[i], dist[i][j]);
                res1 = max(res1, maxd[i]);
            }
    double res2 = inf;
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j)
            if (dist[i][j] > inf / 2)
                res2 = min(res2, maxd[i] + get(p[i], p[j]) + maxd[j]);
    printf("%lf\n", max(res1, res2));
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
