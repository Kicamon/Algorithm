const int N = 510, inf = 0x3f3f3f3f;
int m, n;
bool w[N][N];
int dist[N];

int bfs()
{
    queue<int> q;
    q.push(1);
    memset(dist, 0x3f, sizeof dist);
    dist[1] = 0;

    while (!q.empty())
    {
        int t = q.front();
        q.pop();

        for (int i = 1; i <= n; ++i)
            if (w[t][i] && dist[i] > dist[t] + 1)
            {
                dist[i] = dist[t] + 1;
                q.push(i);
            }
    }
    return dist[n];
}

void solve()
{
    cin >> m >> n;
    string line;
    getline(cin, line);
    while (m--)
    {
        getline(cin, line);
        stringstream ssin(line);
        vint s;
        int t;
        while (ssin >> t)
            s.pb(t);
        for (int i = 0; i + 1 < (int)s.size(); ++i)
            for (int j = i + 1; j < (int)s.size(); ++j)
                w[s[i]][s[j]] = true;
    }

    int t = bfs();
    if (t == inf)
        cout << "NO" << endl;
    else
        cout << t - 1 << endl;
}

