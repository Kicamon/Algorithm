// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 310, M = 8010;

struct node
{
    int a, b, c;
};
int n, m;
int p[N];

int find(int x)
{
    if (x != p[x])
        p[x] = find(p[x]);
    return p[x];
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    cin >> n >> m;
    vector<node> f(m);
    for (int i = 0; i < n; ++i)
        p[i] = i;
    int a, b, c;
    for (int i = 0; i < m; ++i)
    {
        cin >> a >> b >> c;
        f[i] = {a, b, c};
    }
    sort(all(f), [&](node a, node b) { return a.c < b.c; });
    int res = 0, cnt = 0;
    for (int i = 0; i < m; ++i)
    {
        a = f[i].a, b = f[i].b, c = f[i].c;
        a = find(a), b = find(b);
        if (a != b)
        {
            p[a] = b;
            res++;
            cnt = max(cnt, c);
        }
    }
    cout << res << ' ' << cnt << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
