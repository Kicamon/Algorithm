#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 35;
int n;
int a[N][N];

int gauss() {
    int cnt = 1;
    for (int r = 1; r <= n; ++r) {
        int t = r;
        for (int i = r + 1; i <= n; ++i) {
            if (a[i][r]) {
                t = i;
            }
        }
        if (!a[t][r]) {
            continue;
        }
        for (int i = r; i <= n + 1; ++i) {
            swap(a[r][i], a[t][i]);
        }
        for (int i = r + 1; i <= n; ++i) {
            for (int j = n + 1; j >= r; --j) {
                a[i][j] ^= a[i][r] & a[r][j];
            }
        }
        cnt++;
    }
    int res = 1;
    if (cnt < n + 1) {
        for (int i = cnt; i <= n; ++i) {
            if (a[i][n + 1]) {
                return -1;
            }
            res <<= 1;
        }
    }
    return res;
}

void solve() {
    cin >> n;
    memset(a, 0, sizeof a);
    for (int i = 1; i <= n; ++i)
        cin >> a[i][n + 1];
    for (int i = 1, t; i <= n; ++i) {
        cin >> t;
        a[i][n + 1] ^= t;
        a[i][i] = 1;
    }
    int x, y;
    while (cin >> x >> y, x || y) {
        a[y][x] = 1;
    }
    int res = gauss();
    if (res == -1) {
        cout << "Oh,it's impossible~!!" << endl;
    } else {
        cout << res << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
