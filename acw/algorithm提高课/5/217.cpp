// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e5 + 10;

int n, m;
vector<array<int, 2>> e[N];
int dount[N];
double f[N];

double dp(int u){
    if(f[u] >= 0){
        return f[u];
    }
    f[u] = 0;
    for(auto v : e[u]){
        f[u] += (v[1] + dp(v[0])) / dount[u];
    }
    return f[u];
}

signed main() {

    cin >> n >> m;
    int a, b, c;
    for (int i = 0; i < m; ++i) {
        cin >> a >> b >> c;
        e[a].push_back({b, c});
        dount[a]++;
    }

    memset(f,-1,sizeof f);
    printf("%.2lf\n", dp(1));

    return 0;
}
