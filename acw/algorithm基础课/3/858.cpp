#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
int input = 0;
const int N = 510;
#define inf 0x3f3f3f3f

int n,m;

int g[N][N],dist[N];
bool vis[N];
int prim()
{
	memset(dist,0x3f,sizeof dist);
	int ans = 0;
	for(int i = 0;i < n;++i)
	{
		int t = -1;
		for(int j = 1;j <= n;++j)
			if(!vis[j] && (t == -1 || dist[t] > dist[j]))
				t = j;

		if(i && dist[i] == inf)
			return inf;
		if(i) ans += dist[t];
		vis[t] = true;

		for(int j = 1;j <= n;++j)
			dist[j] = min(dist[j],g[t][j]);
	}

	return ans;
}

void solve()
{
	cin >> n >> m;
	for(int i = 0;i < m;++i)
	{
		int a,b,c;
		cin >> a >> b >> c;
		g[a][b] = g[b][a] = min(g[a][b],c);
	}

	int t = prim();
	if(t == inf)
		cout << "impossible\n";
	else 
		cout << t << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
