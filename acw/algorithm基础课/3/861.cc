int input = 0;
const int N = 1e5 + 10, M = 2e5 + 10;

int e[M], ne[M], h[N], idx;

void add(int a, int b) {
        e[idx] = b, ne[idx] = h[a], h[a] = idx++;
}

int metch[N];
bool vis[N];

bool xyl(int u) {
        for (int i = h[u]; ~i; i = ne[i]) {
                int j = e[i];
                if (!vis[j]) {
                        vis[j] = true;
                        if (!metch[j] || xyl(metch[j])) {
                                metch[j] = u;
                                return true;
                        }
                }
        }
        return false;
}

void solve() {
        memset(h, -1, sizeof h);
        int n1, n2, m;
        cin >> n1 >> n2 >> m;
        while (m--) {
                int a, b;
                cin >> a >> b;
                add(a, b);
        }

        int res = 0;
        for (int i = 1; i <= n1; ++i) {
                memset(vis, 0, sizeof vis);
                if (xyl(i))
                        res++;
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        //clock_t start, finish;
        //start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        //finish = clock();
        //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
