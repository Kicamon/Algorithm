// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, k;
        cin >> n >> m >> k;
        vector<array<int, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
        }
        sort(all(a), greater<>());
        if (a[0][0] <= k) {
                cout << k << endl;
        } else {
                int sumc = 0;
                int time = a[0][0];
                for (int i = 0; i < n && a[i][0] > k && m; ++i) {
                        sumc += a[i][1];
                        if (i < n - 1) {
                                int t = min(time - a[i + 1][0], m / sumc);
                                time -= t;
                                m -= sumc * t;
                        } else {
                                if (time > k) {
                                        int t = min(time - k, m / sumc);
                                        time -= t;
                                        m -= sumc * t;
                                }
                        }
                }
                cout << time << endl;
        }

        return 0;
}
