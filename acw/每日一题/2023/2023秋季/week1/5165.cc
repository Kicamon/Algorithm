#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 110;

string str;
int r, c;
char w[N][N];
int len;
queue<array<int, 2>> q;

int dfs(int x, int y, int u) {
        int re = 0;
        if (u == len + 1) {
                return 1;
        }
        for (int i = x - 1; i <= x + 1; ++i) {
                for (int j = y - 1; j <= y + 1; ++j) {
                        if (i == x && j == y) {
                                continue;
                        } else if (x < 0 || x >= r || y < 0 || y >= c) {
                                continue;
                        }
                        if (w[i][j] == str[u]) {
                                re += dfs(i, j, u + 1);
                        }
                }
        }
        return re;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> str;
        len = str.size();
        cin >> r >> c;
        for (int i = 0; i < r; ++i) {
                for (int j = 0; j < c; ++j) {
                        cin >> w[i][j];
                        if (w[i][j] == str[0]) {
                                q.push({ i, j });
                        }
                }
        }
        int ans = 0;
        while (!q.empty()) {
                auto [x, y] = q.front();
                q.pop();
                ans += dfs(x, y, 1);
        }
        cout << ans << endl;

        return 0;
}
