/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, d;
        cin >> n >> d;
        vector<int> v(n), a(n);
        int tmp = 0;
        for (int i = 1; i < n; ++i) {
                cin >> v[i];
                tmp += v[i];
        }
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        int sum = 0, res = 0;
        for (int i = 0; i < n - 1; ++i) {
                int len = 0;
                int j = i + 1;
                for (; j < n; ++j) {
                        len += v[j];
                        if (a[j] < a[i]) {
                                break;
                        }
                }
                int l = 0, r = tmp;
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (mid * d + res < len) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                res = r * d + res - len;
                sum += a[i] * r;
                i = j - 1;
        }
        cout << sum << endl;

        return 0;
}
