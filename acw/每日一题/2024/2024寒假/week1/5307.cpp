#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    int res = 0, ans = 0, m = n;
    while (m) {
        res++;
        if (!ans && (m - 1) % 3 == 0) {
            ans = res;
        }
        m -= max((m + 2) / 3, 1);
    }
    ans = !ans ? res : ans;
    cout << res << ' ' << ans << endl;

    return 0;
}
