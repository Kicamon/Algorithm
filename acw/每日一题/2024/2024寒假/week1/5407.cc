/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <functional>
#include <queue>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, len;
        cin >> n >> len;
        vector<array<int, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
        }
        function<bool(int)> check = [&](int time) {
                priority_queue<array<int, 2>, vector<array<int, 2>>, greater<array<int, 2>>> q;
                for (auto x : a) {
                        if (x[1] <= time) {
                                q.push({ max(1ll, x[0] - (time - x[1])),
                                         min(len, x[0] + (time - x[1])) });
                        }
                }
                int l = 0, r = -1;
                while (!q.empty()) {
                        auto t = q.top();
                        q.pop();
                        if (t[0] <= r + 1) {
                                r = max(r, t[1]);
                        } else {
                                l = t[0], r = t[1];
                        }
                }
                return l == 1 && r == len;
        };
        int l = 1, r = 2e9;
        while (l < r) {
                int mid = (l + r) >> 1;
                if (check(mid)) {
                        r = mid;
                } else {
                        l = mid + 1;
                }
        }
        cout << r << endl;

        return 0;
}
