/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <functional>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        cin >> s;
        vector<int> a(3);
        int num = 0;
        function<void(int)> up = [&](int x) {
                if (a[x]) {
                        a[x] += a[2];
                        a[2] = 0;
                } else if (a[1 - x]) {
                        if (a[1 - x] % 2 == a[2] % 2) {
                                a[1 - x] += a[2];
                                a[2] = 0;
                        } else if (a[2]) {
                                a[1 - x] += a[2] - 1;
                                a[2] = 0;
                                a[x] = 1;
                        }
                        num += a[1 - x] / 2;
                }
                a[1 - x] = 0;
                a[x]++;
        };
        for (char c : s) {
                if (c == '?') {
                        a[2]++;
                } else if (c == '0') {
                        up(0);
                } else {
                        up(1);
                }
        }
        num += accumulate(all(a), 0) >> 1;
        cout << num << endl;

        return 0;
}
