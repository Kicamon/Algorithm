#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 1e9 + 7;

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    ll res = 0;
    for (ll i = 1; i <= n; ++i) {
        res = (res + i * i * (n / i) % mod) % mod;
    }
    cout << res << endl;

    return 0;
}
