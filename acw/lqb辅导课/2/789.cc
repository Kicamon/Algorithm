// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, q;
        cin >> n >> q;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        auto bs = [&](int c) {
                int l = 0, r = n - 1;
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (a[mid] < c) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                if (a[l] != c) {
                        return -1;
                }
                return l;
        };
        auto _bs = [&](int c) {
                int l = 0, r = n - 1;
                while (l < r) {
                        int mid = (l + r + 1) >> 1;
                        if (a[mid] > c) {
                                r = mid - 1;
                        } else {
                                l = mid;
                        }
                }
                if (a[l] != c) {
                        return -1;
                }
                return l;
        };
        while (q--) {
                int c;
                cin >> c;
                int l = bs(c), r = _bs(c);
                cout << l << ' ' << r << endl;
        }

        return 0;
}
