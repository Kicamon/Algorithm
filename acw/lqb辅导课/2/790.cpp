// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const double mop = 1e-8;

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    double a;
    cin >> a;
    double l = -100, r = 100;
    while (fabs(l - r) > mop) {
        double mid = (l + r) / 2;
        double val = mid * mid * mid;
        if (val < a) {
            l = mid;
        } else {
            r = mid;
        }
    }
    printf("%.6lf\n", l);

    return 0;
}
