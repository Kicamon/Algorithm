/* #pragma GCC optimize(2) */
#include <algorithm>
#include <bits/stdc++.h>
#include <queue>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define pli pair<ll, int>

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        vector<ll> a(n + 2);
        vector<int> l(n + 2), r(n + 2);
        priority_queue<pli, vector<pli>, greater<pli>> q;
        r[0] = 1;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                l[i] = i - 1, r[i] = i + 1;
                q.push({ a[i], i });
        }
        while (k--) {
                pli t = q.top();
                q.pop();
                if (t.first != a[t.second]) {
                        q.push({ a[t.second], t.second });
                        ++k;
                } else {
                        a[l[t.second]] += t.first, a[r[t.second]] += t.first;
                        r[l[t.second]] = r[t.second], l[r[t.second]] = l[t.second];
                }
        }
        for (int i = r[0]; i < n + 1; i = r[i]) {
                cout << a[i] << ' ';
        }
        cout << endl;

        return 0;
}
