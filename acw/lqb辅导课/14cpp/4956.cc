// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int INF = 1e9;

pair<int, int> solve() {
        int mi, ma;
        int a, b;
        cin >> a >> b;
        int l = 1, r = INF;
        while (l < r) {
                int mid = (l + r + 1) >> 1;
                if (a / mid < b) {
                        r = mid - 1;
                } else {
                        l = mid;
                }
        }
        ma = r;
        l = 1, r = ma;
        while (l < r) {
                int mid = (l + r) >> 1;
                if (a / mid > b) {
                        l = mid + 1;
                } else {
                        r = mid;
                }
        }
        mi = r;

        return pair<int, int>{ mi, ma };
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        int mi = 0, ma = INF;
        while (t--) {
                pair<int, int> res = solve();
                mi = max(mi, res.first), ma = min(ma, res.second);
        }
        cout << mi << ' ' << ma << endl;

        return 0;
}
