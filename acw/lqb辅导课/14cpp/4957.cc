#include <bits/stdc++.h>
#include <functional>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<vector<int>> a(n, vector<int>(3));
        vector<bool> vis(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1] >> a[i][2];
                vis[i] = false;
        }
        function<bool(int, int)> dfs = [&](int u, int last) {
                if (u == n) {
                        return true;
                }
                for (int i = 0; i < n; ++i) {
                        if (!vis[i] && a[i][0] + a[i][1] >= last) {
                                vis[i] = true;
                                if (dfs(u + 1, max(last, a[i][0]) + a[i][2])) {
                                        return true;
                                }
                                vis[i] = false;
                        }
                }
                return false;
        };
        bool flag = dfs(0, 0);
        cout << (flag ? "YES" : "NO") << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
