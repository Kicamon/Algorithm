// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    vector<int> num(10);
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        int x = to_string(a[i])[0] - '0', y = a[i] % 10;
        num[y] = max(num[y], num[x] + 1);
    }
    cout << n - *max_element(all(num)) << endl;

    return 0;
}
