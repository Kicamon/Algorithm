// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

int n, k;

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> k;
    int m = 1 << n;
    vector<int> a(m), ra(m + 1);
    for (int i = 0; i < m; ++i) {
        cin >> a[i];
        ra[a[i]] = i;
    }
    vector<int> b(m + 1);
    if (k) {
        int res = 2;
        b[0] = 0;
        for (int i = 1; i <= m; ++i) {
            b[i] = b[i - 1];
            if (i == res) {
                b[i]++;
                res <<= 1;
            }
        }
        for (int i = 0; i < m; ++i) {
            cout << b[a[i]] << ' ';
        }
    } else {
        vector<int> res;
        for (int i = 0; i < n; ++i) {
            for (int i = 0; i < a.size(); i += 2) {
                if (a[i] > a[i + 1]) {
                    res.push_back(a[i]);
                    b[ra[a[i]]]++;
                } else {
                    res.push_back(a[i + 1]);
                    b[ra[a[i + 1]]]++;
                }
            }
            a = res;
            res.clear();
        }
        for (int i = 0; i < m; ++i) {
            cout << b[i] << ' ';
        }
    }
    cout << endl;

    return 0;
}
