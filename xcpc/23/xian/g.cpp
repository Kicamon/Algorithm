#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

vector<int> primes;
void prime(int n) {
    vector<bool> vis(n + 1, 0);
    vis[0] = vis[1] = 1;
    for (int i = 2; i <= n; i++) {
        if (!vis[i])
            primes.push_back(i);
        for (int j = 0; primes[j] * i <= n; j++) {
            vis[primes[j] * i] = 1;
            if (i % primes[j] == 0)
                break;
        }
    }
}

void solve() {
    int n, ans = 1;
    cin >> n;
    for (int i : primes) {
        if (i * i > n) {
            break;
        }
        int cnt = 0;
        while (n % i == 0) {
            n /= i;
            cnt++;
        }
        ans *= 2 * cnt + 1;
    }
    if (n > 1) {
        ans *= 3;
    }
    cout << (ans - 1) / 2 + 1 << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    prime(1e5);
    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
