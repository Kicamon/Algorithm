#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

struct node {
        string s;
        int val;
        bool operator<(const node a) const {
                return val < a.val;
        }
};

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        map<string, int> a, b;
        string s;
        for (int i = 0, idx = 0; i < n; ++i) {
                cin >> s;
                if (!a.count(s)) {
                        a[s] = idx;
                        idx++;
                }
        }
        for (int i = 0, idx = 0; i < m; ++i) {
                cin >> s;
                if (!b.count(s)) {
                        b[s] = idx;
                        idx++;
                }
        }
        map<string, int> c;
        for (auto t : a) {
                c[t.first] += t.second;
        }
        for (auto t : b) {
                if (c.count(t.first)) {
                        c[t.first] = (c[t.first] + t.second) >> 1;
                } else {
                        c[t.first] += t.second;
                }
        }
        /* for (auto t : c) { */
        /* c[t.first] += t.second; */
        /* cout << t.first << ' ' << t.second << endl; */
        /* } */
        vector<pair<string, int>> res(c.begin(), c.end());
        sort(all(res),
             [&](pair<string, int> a, pair<string, int> b) { return a.second < b.second; });
        for (auto t : res) {
                cout << t.first << endl;
        }

        return 0;
}
