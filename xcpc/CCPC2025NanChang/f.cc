/* #pragma GCC optimize(2) */
#include <algorithm>
#include <array>
#include <iomanip>
#include <ios>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        array<double, 2> c;
        vector<double> r(n + 1);
        double L, R, p;
        cin >> r[0] >> c[0] >> p >> L >> R;
        fill(r.begin() + 1, r.end(), L);
        for (int i = 0, x; i < k; ++i) {
                double y;
                cin >> x >> y;
                r[x] = y;
        }
        double ans = 0;
        for (int i = 1, idx = 1; i <= n; ++i, idx ^= 1) {
                c[idx] = p * c[1 - idx] + (1 - p) * r[i - 1];
                ans += c[idx] - r[i];
        }
        cout << fixed << setprecision(10) << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
