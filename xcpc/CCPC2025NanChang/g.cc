using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, q;
        cin >> n >> m >> q;
        vector<vector<array<int, 2> > > g(n);
        for (int i = 0, u, v, w; i < m; ++i) {
                cin >> u >> v >> w;
                u--, v--;
                g[u].push_back({ v, w });
        }

        return 0;
}
