// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e6 + 10, inf = 1e9 + 7;

int n, m, s, t, k;
vector<array<int, 2>> e[N];
int vis[N];

int ans;

void bfs()
{
    ans = k;
    priority_queue<array<int, 2>, vector<array<int, 2>>, less<array<int, 2>>> q;
    q.push({inf, s});
    while (!q.empty())
    {
        auto T = q.top();
        q.pop();
        int u = T[1];
        ans = min(ans, T[0]);
        if (u == t)
            return;
        vis[u] = true;
        for (auto x : e[u])
        {
            int v = x[0];
            if (vis[v])
                continue;
            int w = x[1];
            q.push({w, v});
        }
    }
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    cin >> n >> m >> s >> t >> k;
    while (m--)
    {
        int a, b, c;
        cin >> a >> b >> c;
        e[a].push_back({b, c});
        e[b].push_back({a, c});
    }
    bfs();
    cout << max(ans, 0) << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
