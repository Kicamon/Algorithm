// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int mx[2] = {0, 1}, my[2] = {1, 0};

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n, m;
    cin >> n >> m;
    vector<string> C(n), A(n), B(n);
    vector<vector<bool>> vis(n, vector<bool>(m));
    for (int i = 0; i < n; ++i) {
        cin >> C[i];
    }
    A = B = C;

    auto bfs = [&](int x, int y) {
        queue<array<int, 2>> q;
        q.push({x, y});
        while (!q.empty()) {
            auto t = q.front();
            q.pop();
            for (int _; _ < 2; ++_) {
                int X = t[0] + mx[_], Y = t[1] + my[_];
                if (X >= n || Y >= m || vis[X][Y]) {
                    continue;
                }
                q.push({X, Y});
                A[X][Y] = '1';
                vis[X][Y] = true;
            }
        }
    };
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            if (A[i][j] == '1' && !vis[i][j]) {
                vis[i][j] = true;
                bfs(i, j);
            }
        }
    }

    return 0;
}
