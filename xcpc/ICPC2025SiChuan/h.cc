/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll x, y, X, Y;
        cin >> x >> y >> X >> Y;
        x = abs(X - x), y = abs(Y - y);
        if (x > y) {
                swap(x, y);
        }
        int ans = y / 2 + y % 2;
        if (!x && ans == 1) {
                ans++;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
