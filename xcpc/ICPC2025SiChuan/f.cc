/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        s = ' ' + s;
        vector<int> a(n + 2), pre(n + 1), suf(n + 2);
        for (int i = 1; i <= n; ++i) {
                if (s[i] == '1') {
                        a[i] = 1;
                } else {
                        a[i] = 0;
                }
        }
        a[n + 1] = -1;
        for (int i = n; i; --i) {
                suf[i] = suf[i + 1];
                if (!a[i]) {
                        suf[i]++;
                }
        }
        for (int i = 1; i <= n; ++i) {
                pre[i] = pre[i - 1];
                if (s[i] == '?') {
                        if (pre[i] < suf[i + 1]) {
                                pre[i]++;
                                a[i] = 1;
                        }
                } else if (s[i] == '1') {
                        pre[i]++;
                }
        }
        for (int i = n; i; --i) {
                suf[i] = suf[i + 1];
                if (!a[i + 1]) {
                        suf[i]++;
                }
        }
        ll ans = 0;
        for (int i = 1; i <= n; ++i) {
                if (a[i]) {
                        ans += suf[i];
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
