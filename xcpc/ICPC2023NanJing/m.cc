/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

struct ST {
        struct Edge {
                ll val;
                int lm, rm;
        };

        int n;
        vector<Edge> node, temp;
        ll sum;

        ST(vector<int> &t) {
                n = (int)t.size();
                node.resize(n << 2);
                temp.resize(n + 2);
                sum = accumulate(all(t), 0);
                for (int i = 1; i <= n; ++i) {
                        temp[i].lm = max(temp[i - 1].lm, t[i]);
                }
                for (int i = n; i; --i) {
                        temp[i].rm = max(temp[i + 1].rm, t[i]);
                        temp[i].val = min(temp[i].lm, temp[i].rm);
                }
        }

        void push_up(int u) {
                Edge &l = node[u << 1], &r = node[u << 1 | 1];
                node[u].val = 1ll * l.val + r.val;
        }

        void build(int u, int l, int r) {
                if (l == r) {
                        node[u] = temp[l];
                        return;
                }
                int mid = (l + r) >> 1;
                build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
        }
};

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
        }
        ST st(a);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
