#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e6 + 10;

bool vis[N];

void prime()
{
    vector<int> primes;
    for (int i = 2; i < N; ++i)
    {
        if (!vis[i])
            primes.push_back(i);
        for (auto j : primes)
        {
            if (1ll * i * j >= N)
                break;
            vis[i * j] = true;
            if (i % j == 0)
                break;
        }
    }
}

signed main()
{
    // clock_t start, finish;
    // start = clock();

    prime();
    int n;
    cin >> n;
    vector<int> a(n);
    vector<int> primes;
    for (int i = 0; i < n; ++i)
    {
        cin >> a[i];
        if (!vis[a[i]])
            primes.push_back(a[i]);
    }
    sort(primes.begin(), primes.end());
    primes.erase(unique(primes.begin(), primes.end()), primes.end());
    sort(a.begin(), a.end(), greater<>());
    a.erase(unique(a.begin(), a.end()), a.end());
    if (primes.empty())
        cout << -1 << endl;
    else
    {
        for (int i : a)
        {
            int cnt = i;
            for (int j : primes)
            {
                while (cnt % j == 0)
                    cnt /= j;
            }
            if (cnt == 1)
            {
                cout << i << endl;
                return 0;
            }
        }
    }

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
