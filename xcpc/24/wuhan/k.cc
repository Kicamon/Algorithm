/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        int res = 0;
        for (int i = 1; i <= n; ++i) {
                res ^= i;
        }
        for (int i = 1, l = 1, r = n; i <= n; ++i) {
                if (res == 0) {
                        cout << (i % 2 ? "Pinkie Pie" : "Fluttershy") << endl;
                        return;
                }
                if ((res ^ l) != 0) {
                        res ^= r;
                        r--;
                } else {
                        res ^= l;
                        l++;
                }
        }
        cout << (n % 2 ? "Fluttershy" : "Pinkie Pie") << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
