// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n;
    cin >> n;
    vector<int> a(n);
    priority_queue<int, vector<int>, greater<int>> q;
    for (int i = 0; i < n; ++i)
    {
        cin >> a[i];
        if (i)
            q.push(a[i - 1] - a[i]);
    }
    vector<int> dp(n + 1);
    dp[n] = 0;
    for (int i = n - 1; i; --i)
    {
        dp[i] = dp[i + 1] + q.top();
        q.pop();
    }
    int m;
    cin >> m;
    while (m--)
    {
        int op, x;
        cin >> op >> x;
        if (op)
            cout << dp[x] << endl;
    }

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
