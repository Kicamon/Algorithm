/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iomanip>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<long double> r(n + 1), P(n + 1);
        long double c, p, L, R;
        cin >> c >> r[0] >> p >> L >> R;
        fill(r.begin() + 1, r.end(), L);
        for (int i = 0, x; i < k; ++i) {
                long double y;
                cin >> x >> y;
                r[x] = y;
        }
        P[0] = 1;
        for (int i = 1; i <= n; ++i) {
                P[i] = P[i - 1] * p;
        }
        long double ans = (1 - P[n]) * r[0];
        for (int i = 1; i < n; ++i) {
                ans += (c - r[i]) * P[n - i];
        }
        ans += c * P[n] - r[n];
        cout << fixed << setprecision(10) << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
