signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        if (m == 0 || m == n) {
                for (int i = 0; i < n / 2; ++i) {
                        cout << 1;
                }
                for (int i = 0; i < n / 2; ++i) {
                        cout << 3;
                }
                if (n % 2) {
                        cout << 4;
                }
                cout << endl;
        } else {
                for (int i = 0; i < m; ++i) {
                        cout << 1;
                }
                for (int i = m; i < n; ++i) {
                        cout << 4;
                }
                cout << endl;
        }

        return 0;
}
