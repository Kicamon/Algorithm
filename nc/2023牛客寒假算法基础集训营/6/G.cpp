//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int n,k;
	cin >> n >> k;
	vint a,b;
	for(int i = 0,c;i < n;++i)
	{
		cin >> c;
		if(c >= 0)
			a.push_back(c);
		else 
			b.push_back(c);
	}
	sort(rall(a)),sort(all(b));
	vint q;
	int i,j;
	for(i = 0;i + 1 < a.size();i += 2)
		q.pb(a[i] * a[i + 1]);
	for(j = 0;j + 1 < b.size();j += 2)
		q.pb(b[j] * b[j + 1]);
	if(q.size() < k)
		q.pb(a[i] * b[j]);
	sort(rall(q));
	ll res = 0;
	for(int i = 0;i < k;++i)
		res += q[i];
	cout << res << endl;

    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
