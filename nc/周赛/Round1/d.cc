// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int mod = 1e9 + 7;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        vector<vector<ll>> f(2, vector<ll>(9));
        string s;
        cin >> s;
        f[0][0] = 1;
        int idx = 0;
        for (int r = 0; r < (int)s.size(); ++r) {
                idx ^= 1;
                char c = s[r];
                f[idx] = f[idx ^ 1];
                int x = c - '0';
                for (int i = 0; i < 9; ++i) {
                        int y = (x + i) % 9;
                        f[idx][y] = (f[idx][y] + f[idx ^ 1][i]) % mod;
                }
        }
        cout << f[idx][0] - 1 << endl;

        return 0;
}
