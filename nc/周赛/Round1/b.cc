// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        vector<int> b = a;
        sort(b.begin(), b.end());
        b.erase(unique(b.begin(), b.end()), b.end());
        int m = (int)b.size();
        vector<array<int, 2>> c(m);
        string s;
        cin >> s;
        for (int i = 0; i < n; ++i) {
                c[lower_bound(b.begin(), b.end(), a[i]) - b.begin()][s[i] == 'R']++;
        }
        ll res = 0;
        for (int i = 0; i < m; ++i) {
                res += 1ll * c[i][0] * c[i][1];
        }
        cout << res << endl;

        return 0;
}
