#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int mod = 1e9 + 7;

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    string s;
    cin >> s;
    int n = (int)s.size();
    ll ans = 0;
    ll res = 0;
    for (int i = 0; i < n; ++i) {
        res += s[i] == '0';
    }
    if ((n % 2 && res < n - res)) {
        int cnt = 1;
        for (int i = 0; i < n; ++i) {
            if (s[i] == '0') {
                ans += abs(i - cnt);
                cnt += 2;
            }
        }
    } else {
        ll cnt = 0;
        for (int i = 0; i < n; ++i) {
            if (s[i] == '0') {
                ans += abs(i - cnt);
                cnt += 2;
            }
        }
        res = 0, cnt = 0;
        for (int i = 0; i < n; ++i) {
            if (s[i] == '1') {
                res += abs(i - cnt);
                cnt += 2;
            }
        }
        ans = min(ans, res);
    }
    cout << ans << endl;

    return 0;
}
