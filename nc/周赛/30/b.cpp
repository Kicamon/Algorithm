/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> num(10);
    while (n) {
        num[n % 10]++;
        n /= 10;
    }
    string res = "";
    for (int i = 1; i < 10; ++i) {
        if (num[i]) {
            res += char(i + '0');
            num[i]--;
            break;
        }
    }
    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < num[i]; ++j) {
            res += char(i + '0');
        }
    }
    cout << res << endl;

    return 0;
}
