/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    string s;
    cin >> s;
    vector<int> num(26);
    for (char c : s) {
        num[c - 'a']++;
    }
    map<int, int> p;
    int res = -1, cnt = -1;
    for (int i = 0; i < 26; ++i) {
        if (p.count(num[i])) {
            res = i;
            cnt = p[num[i]];
            break;
        }
        if (num[i]) {
            p[num[i]] = i;
        }
    }
    if (res == -1) {
        cout << -1 << endl;
    } else {
        for (char c : s) {
            if (c == char(res + 'a')) {
                cout << char(cnt + 'a');
            } else if (c == char(cnt + 'a')) {
                cout << char(res + 'a');
            } else {
                cout << c;
            }
        }
    }

    return 0;
}
