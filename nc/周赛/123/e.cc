#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define all(x) (x).begin(), (x).end()

class DSU {
    private:
        vector<int> f;
        vector<bool> vis;
        int siz;

    public:
        DSU(int n) {
                f.resize(n), vis.resize(n);
                iota(all(f), 0);
                siz = 0;
        }

        int find(int x) {
                while (x != f[x]) {
                        x = f[x] = f[f[x]];
                }
                return x;
        }

        void add_node(int x) {
                if (!vis[x]) {
                        vis[x] = true;
                        siz++;
                }
        }

        bool merge(int x, int y) {
                if (!(vis[x] && vis[y])) {
                        return false;
                }
                x = find(x);
                y = find(y);
                if (x == y) {
                        return false;
                }
                f[y] = x;
                siz--;
                return true;
        }

        int size() {
                return siz;
        }
};

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        DSU dsu(n + 1);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                dsu.add_node(x);
                dsu.merge(x, x + 1);
                dsu.merge(x - 1, x);
                cout << dsu.size() << " \n"[i == n - 1];
        }

        return 0;
}
