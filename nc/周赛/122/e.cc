/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1), b(n + 1, 1), dp(n + 1);
        b[0] = 1;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                if (a[i] == a[i - 1]) {
                        b[i] += b[i - 1];
                }
        }
        for (int i = 1; i <= n; ++i) {
                if (a[i] <= b[i]) {
                        dp[i] = dp[i - a[i]] + 1;
                }
        }
        cout << accumulate(all(dp), 0) << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
