/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 998244353, N = 2e5 + 10;

vector<int> ans(N);

void solve() {
        int n;
        cin >> n;
        int m = 0;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x % 2 == 0) {
                        m++;
                }
        }
        n -= m;
        cout << 1ll * ans[m] * (ans[n] + 1) % mod << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ans[1] = 1;
        for (int i = 2; i < N; ++i) {
                ans[i] = 1ll * ans[i - 1] * 2 % mod;
        }
        for (int i = 1; i < N; ++i) {
                (ans[i] += ans[i - 1]) %= mod;
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
