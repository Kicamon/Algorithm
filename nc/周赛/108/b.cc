/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a, b;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x % 2) {
                        b.push_back(x);
                } else {
                        a.push_back(x);
                }
        }
        sort(all(a)), sort(all(b));
        for (int x : a) {
                cout << x << ' ';
        }
        for (int x : b) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
