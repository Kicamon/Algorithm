#include <iostream>
#include <vector>
using namespace std;
const int N = 105, M = 2e4 + 5;

bool dp[M][N];
bool vis[M];
vector<int> is;

void solve() {
        int n, sum = 0;
        cin >> n;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                sum += x;
        }
        cout << n - !dp[sum][n] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        for (int i = 1; i * i < M; ++i) {
                vis[i * i] = true;
        }

        for (int i = 1; i < M; ++i) {
                if (!vis[i]) {
                        continue;
                }
                int t = 0, x = i;
                while (x) {
                        t += x % 10;
                        x /= 10;
                }
                if (vis[t]) {
                        is.push_back(i);
                }
        }

        dp[0][0] = true;
        for (int i = 0; i < M; ++i) {
                for (int j = 0; j < N - 1; ++j) {
                        if (!dp[i][j]) {
                                continue;
                        }
                        for (int x : is) {
                                if (i + x >= M) {
                                        break;
                                }
                                dp[i + x][j + 1] = true;
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
