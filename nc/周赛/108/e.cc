/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()
const int N = 230, M = 2e4 + 10;

vector<int> is;
bool dp[M][N];
bool vis[N];

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        sort(rall(a));
        int num = accumulate(all(a), 0);
        cout << n - !dp[num][n] << endl;
}
signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 1; i * i < N; ++i) {
                vis[i * i] = true;
        }
        for (int i = 0; i < N; ++i) {
                int x = i, t = 0;
                while (x) {
                        t += x % 10;
                        x /= 10;
                }
                if (vis[i] && vis[t]) {
                        is.push_back(i);
                }
        }

        dp[0][0] = true;
        for (int i = 0; i < M; ++i) {
                for (int j : is) {
                        if (i + j >= M) {
                                continue;
                        }
                        for (int k = 0; k < N - 1; ++k) {
                                dp[i + j][k + 1] |= dp[i][k];
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
