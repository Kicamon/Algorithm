/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <set>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int mx[] = { -1, -2, -2, -1, 1, 2, 2, 1 };
int my[] = { -2, -1, 1, 2, 2, 1, -1, -2 };

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        set<array<int, 2>> st;
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                st.insert({ x, y });
        }

        function<int(int, int)> check = [&](int x, int y) {
                int res = 0;
                for (int i = 0; i < 8; ++i) {
                        int X = x + mx[i], Y = y + my[i];
                        if (st.count({ X, Y })) {
                                res++;
                        }
                }
                return res;
        };

        int ans = 0, ax = 0, ay = 0;
        for (auto [x, y] : st) {
                for (int i = 0; i < 8; ++i) {
                        int X = x + mx[i], Y = y + my[i];
                        if (X <= 0 || Y <= 0) {
                                continue;
                        }
                        int temp = check(X, Y);
                        if (temp > ans) {
                                ans = temp;
                                ax = X, ay = Y;
                        }
                }
        }
        cout << ax << ' ' << ay << endl;

        return 0;
}
