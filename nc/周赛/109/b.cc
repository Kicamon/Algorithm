/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 2>> a(n);
        for (auto &x : a) {
                cin >> x[0] >> x[1];
        }
        int ans = 0;
        for (int i = 0; i < n; ++i) {
                for (int j = i + 1; j < n; ++j) {
                        if (abs(a[i][0] - a[j][0]) + abs(a[i][1] - a[j][1]) == 1) {
                                ans++;
                        }
                }
        }
        cout << ans << endl;

        return 0;
}
