/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <set>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 2> > a(n);
        set<array<int, 2> > st;
        for (auto &x : a) {
                cin >> x[0] >> x[1];
                st.insert({ x[0], x[1] });
        }
        sort(all(a));
        ll ans = 0;
        for (int i = 0; i < n; ++i) {
                int t = 0, j = i;
                while (j < n && a[j][0] == a[i][0]) {
                        t += st.count({ a[j][0] + 1, a[j][1] });
                        j++;
                }
                i = j - (j != i);
                ans += 1ll * t * (t - 1) >> 1;
        }
        sort(all(a), [](auto x, auto y) {
                if (x[1] != y[1]) {
                        return x[1] < y[1];
                }
                return x[0] < y[0];
        });
        for (int i = 0; i < n; ++i) {
                int t = 0, j = i, p = -2;
                while (j < n && a[j][1] == a[i][1]) {
                        if (st.count({ a[j][0], a[j][1] + 1 })) {
                                t++;
                                if (a[j][0] == p + 1) {
                                        ans--;
                                }
                                p = a[j][0];
                        }
                        j++;
                }
                i = j - (j != i);
                ans += 1ll * t * (t - 1) >> 1;
        }
        cout << ans << endl;

        return 0;
}
