/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, q;
        cin >> n >> q;
        vector<array<int, 2> > a(n);
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                a[i][0] = y - x;
                a[i][1] = y + x;
        }
        sort(all(a));
        map<int, vector<int> > mp;
        mp[a[0][0]].push_back(a[0][1]);
        vector<int> idx;
        idx.push_back(a[0][0]);
        for (int i = 1; i < n; ++i) {
                if (a[i][0] == a[i - 1][0]) {
                        mp[a[i][0]].push_back(a[i][1]);
                } else {
                        mp[a[i][0]] = mp[a[i - 1][0]];
                        mp[a[i][0]].push_back(a[i][1]);
                        idx.push_back(a[i][0]);
                }
        }
        for (auto &[_, st] : mp) {
                sort(all(st));
        }
        while (q--) {
                int k1, k2;
                cin >> k1 >> k2;
                int i = lower_bound(all(idx), k1) - idx.begin() - 1;
                int j = lower_bound(all(mp[idx[i]]), k2) - mp[idx[i]].begin();
                cout << j << endl;
        }

        return 0;
}
