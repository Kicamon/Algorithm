/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e3;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        vector<vector<bool> > a(n, vector<bool>(n));
        if (k % 2) {
                cout << -1 << endl;
        } else {
                for (int i = 0; i < n; ++i) {
                        for (int j = 0; j < n; ++j) {
                                a[i][j] = i == j;
                        }
                }
                int t = n << 1;
                for (int i = 1; i < n && t > k; ++i, t -= 2) {
                        a[i - 1][i] = 1;
                }
                if (t > k) {
                        a[n - 1][0] = 1;
                }
                for (int i = 0; i < n; ++i) {
                        for (int j = 0; j < n; ++j) {
                                cout << a[i][j];
                        }
                        cout << endl;
                }
        }

        return 0;
}
