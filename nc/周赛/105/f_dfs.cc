#include <functional>
#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n, m, x;
        cin >> n >> m >> x;
        vector<int> ans(n, 1);

        function<bool(int, int)> dfs = [&](int u, int t) {
                if (u == n) {
                        return (t == x);
                }
                for (int i = ans[u - 1]; i >= 1; --i) {
                        if (ans[u - 1] % i || t + i > x) {
                                continue;
                        }
                        ans[u] = i;
                        int p = n - u - 1;
                        if (t + (p + 1) * i < x) {
                                return false;
                        }
                        if (t + i == x - p || dfs(u + 1, t + i)) {
                                return true;
                        }
                        ans[u] = 1;
                }
                return false;
        };

        for (int i = 1; i <= m; ++i) {
                ans[0] = i;
                if (dfs(1, i)) {
                        for (int i = 0; i < n; ++i) {
                                cout << ans[i] << " \n"[i == n - 1];
                        }
                        return;
                }
        }
        cout << "-1\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
