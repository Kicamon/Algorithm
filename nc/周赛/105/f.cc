/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

vector<vector<int>> num(51);

void solve() {
        int n, m, x;
        cin >> n >> m >> x;
        if (x < n) {
                cout << -1 << endl;
                return;
        }
        m = min(m, x);
        vector<int> ans(n);
        vector<vector<int>> dp(x + 1, vector<int>(n + 1));
        for (int i = 1; i <= m; ++i) {
                dp[i][1] = i;
        }
        for (int i = 1; i < x; ++i) {
                for (int j = 1; j < n; ++j) {
                        for (int t : num[dp[i][j]]) {
                                if (i + t <= x) {
                                        dp[i + t][j + 1] = max(dp[i + t][j + 1], t);
                                }
                        }
                }
        }
        if (dp[x][n]) {
                for (int j = n, idx = 0, t = x; j; t -= dp[t][j], --j, idx++) {
                        ans[idx] = dp[t][j];
                }
                for (int v : vector<int>(ans.rbegin(), ans.rend())) {
                        cout << v << ' ';
                }
                cout << endl;
                return;
        }
        cout << -1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 1; i <= 50; ++i) {
                for (int j = 1; j <= i; ++j) {
                        if (i % j == 0) {
                                num[i].push_back(j);
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
