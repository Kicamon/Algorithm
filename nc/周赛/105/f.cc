/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<vector<int> > num(51);
vector<int> ans;
int n, m, x;

bool dfs(int u, int val, int idx, const int &i) {
        if (u == n) {
                if (val == x) {
                        return true;
                } else {
                        return false;
                }
        }
        while (n - u - 1 + num[i][idx] > x - val) {
                idx++;
        }
        for (int j = idx; j < (int)num[i].size(); ++j) {
                if (num[i][j] + val > x) {
                        continue;
                }
                ans.push_back(num[i][j]);
                if (dfs(u + 1, num[i][j] + val, j, i)) {
                        return true;
                }
                ans.pop_back();
        }
        return false;
}

void solve() {
        cin >> n >> m >> x;
        if (x < n || x > n * m) {
                cout << -1 << endl;
                return;
        }
        if (x % n == 0) {
                int t = x / n;
                for (int i = 0; i < n; ++i) {
                        cout << t << ' ';
                }
                cout << endl;
                return;
        }
        m = min(m, x);
        for (int i = 1; i <= m; ++i) {
                if (dfs(0, 0, 0, i)) {
                        for (int x : ans) {
                                cout << x << ' ';
                        }
                        cout << endl;
                        return;
                }
                ans.clear();
        }
        cout << -1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 1; i <= 50; ++i) {
                for (int j = 1; j <= i; ++j) {
                        if (i % j == 0) {
                                num[i].push_back(j);
                        }
                }
                reverse(all(num[i]));
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
