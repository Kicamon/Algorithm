/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        int num[2] = { 0, 0 };
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                num[x - 1]++;
        }
        int min_2 = max(0, num[1] - num[0] - 1);
        int a = min_2, b = num[1] - 1;
        int t = -1;
        for (int i = a; i <= b; ++i) {
                if (i * 2 + (n - 1 - i) == k) {
                        t = i;
                        break;
                }
        }
        if (t == -1) {
                cout << -1 << endl;
        } else {
                num[1] -= t + 1;
                for (int i = 0; i <= t; ++i) {
                        cout << 2 << ' ';
                }
                for (int i = 0; i < num[0]; ++i) {
                        cout << 1 << ' ';
                        if (num[1]) {
                                num[1]--;
                                cout << 2 << ' ';
                        }
                }
                cout << endl;
        }

        return 0;
}
