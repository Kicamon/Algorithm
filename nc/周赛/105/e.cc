/* #pragma GCC optimize(2) */
#include <bitset>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 998244353;

int ans = 0;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<vector<int>> g(n);
        vector<int> vis(n), val(n);
        vector<bitset<32>> a(n);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                a[i] = x;
        }
        int u, v;
        while (m--) {
                cin >> u >> v;
                u--, v--;
                g[u].push_back(v);
                g[v].push_back(u);
        }
        ll ans = 0;
        for (int u = 0; u < n; ++u) {
                vector<array<int, 32>> num(2);
                for (int v : g[u]) {
                        for (int i = 0; i < 32; ++i) {
                                if (a[u][i]) {
                                        (ans += 1ll * num[a[v][i]][i] * (1 << i)) %= mod;
                                } else {
                                        (ans += 1ll * num[1 - a[v][i]][i] * (1 << i)) %= mod;
                                }
                                num[a[v][i]][i]++;
                        }
                }
        }
        cout << ans << endl;

        return 0;
}
