#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        string s;
        cin >> s;
        int n = s.size();
        int ans = n;
        for (int d = 0; d < 26; ++d) {
                vector<int> a(26, 1);
                a[s[0] - 'a'] = 0;
                for (int i = 1; i < n; ++i) {
                        vector<int> temp(26, n);
                        for (int j = 0; j < 26; ++j) {
                                int p = (j + d) % 26;
                                temp[p] = min(temp[p], a[j] + (p != s[i] - 'a'));
                                p = (j - d + 26) % 26;
                                temp[p] = min(temp[p], a[j] + (p != s[i] - 'a'));
                        }
                        a.assign(temp.begin(), temp.end());
                }
                ans = min(ans, *min_element(a.begin(), a.end()));
        }
        cout << ans << '\n';

        return 0;
}
