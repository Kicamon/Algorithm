#include <algorithm>
#include <iostream>
#include <array>
#include <numeric>
#include <vector>
using namespace std;

class DSU {
    private:
        vector<int> fa, siz;

    public:
        DSU(int n) {
                fa.resize(n);
                iota(fa.begin(), fa.end(), 0);
                siz.resize(n, 1);
        }

        int find(int x) {
                while (x != fa[x]) {
                        x = fa[x] = fa[fa[x]];
                }
                return x;
        }

        bool merge(int a, int b) {
                a = find(a);
                b = find(b);
                if (a == b) {
                        return false;
                }
                fa[a] = b;
                siz[b] += siz[a];
                return true;
        }

        int size(int x) {
                return siz[x];
        }

        bool same(int a, int b) {
                return find(a) == find(b);
        }
};

void solve() {
        int n;
        cin >> n;
        DSU dsu(n);
        vector<int> a(n);
        vector<bool> vis(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
                a[i]--;
                dsu.merge(a[i], i);
        }

        bool flag = false;
        for (int i = 1; i < n; ++i) {
                flag |= (dsu.same(i, i - 1));
        }

        int cnt = 0;
        for (int i = 0, t; i < n; ++i) {
                t = dsu.find(a[i]);
                if (vis[t]) {
                        continue;
                }
                vis[t] = true;
                cnt += dsu.size(t) - 1;
        }
        int ans = cnt + 1;
        if (flag) {
                ans = cnt - 1;
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
