#include <iostream>
#include <vector>
using namespace std;
using ll = long long;
const ll inf = 1e18;

void solve() {
        int n;
        cin >> n;

        string s;
        cin >> s;
        s = ' ' + s + s;

        vector<ll> a((n << 1) + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                a[i + n] = a[i];
        }
        n <<= 1;

        vector<vector<ll>> dp(n + 1, vector<ll>(n + 1, inf));

        for (int i = 1; i < n; ++i) {
                if (s[i] == s[i + 1]) {
                        dp[i][i + 1] = a[i] * a[i + 1];
                }
        }

        for (int len = 3; len <= n / 2; len += 2) {
                for (int l = 1; l + len <= n; ++l) {
                        int r = l + len;
                        if (s[l] == s[r]) {
                                dp[l][r] = dp[l + 1][r - 1] + a[l] * a[r];
                        }
                        for (int k = l + 1; k < r; k += 2) {
                                dp[l][r] = min(dp[l][r], dp[l][k] + dp[k + 1][r]);
                        }
                }
        }

        ll ans = inf;
        for (int i = 1; i < n / 2; ++i) {
                int j = i + (n / 2) - 1;
                ans = min(ans, dp[i][j]);
        }
        cout << (ans == inf ? -1 : ans) << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
