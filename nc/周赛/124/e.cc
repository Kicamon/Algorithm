#include <iostream>
using namespace std;
using ll = long long;
const int mod = 1e9 + 7;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        n >>= 1;

        ll ans = 1, base = 2;
        while (n) {
                if (n & 1) {
                        ans = ans * base % mod;
                }

                (base *= base) %= mod;
                n >>= 1;
        }

        cout << ans << '\n';

        return 0;
}
