#include <iostream>
#include <queue>
#include <vector>
using namespace std;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        string s;
        cin >> s;
        int n = s.size();
        vector<bool> vis(n);
        queue<int> rno, numo;
        for (int i = n - 1; ~i; --i) {
                if (s[i] == '1') {
                        rno.push(i);
                }
        }
        for (int i = 1; i < n; ++i) {
                if (s[i] == '0' && s[i - 1] == '1') {
                        int j = i - 1;
                        while (~j && s[j] == '1') {
                                numo.push(j--);
                        }
                }
        }
        while (!rno.empty() && !numo.empty()) {
                int t1 = numo.front();
                numo.pop();
                int t2 = rno.front();
                rno.pop();
                while (vis[t2]) {
                        t2 = rno.front();
                        rno.pop();
                }
                if (t1 >= t2) {
                        break;
                }
                vis[t1] = true;
                s[t2] = '2';
        }
        for (int i = 0; i < n; ++i) {
                if (vis[i]) {
                        continue;
                }
                cout << s[i];
        }
        cout << '\n';

        return 0;
}
