#include <algorithm>
#include <iostream>
#include <vector>
#include <array>
using namespace std;

bool check(int a, int b, int c, int d) {
        array<int, 4> t = { a, b, c, d };
        sort(t.begin(), t.end());
        for (int i = 1; i < 4; ++i) {
                if (t[i] != t[i - 1] + 1) {
                        return false;
                }
        }
        return true;
}

void solve() {
        vector<vector<int>> a(5, vector<int>(5));
        for (int i = 1; i <= 4; ++i) {
                for (int j = 1; j <= 4; ++j) {
                        cin >> a[i][j];
                }
                if (!check(a[i][1], a[i][2], a[i][3], a[i][4])) {
                        cout << "NO\n";
                        return;
                }
        }

        for (int i = 1; i <= 4; ++i) {
                if (!check(a[1][i], a[2][i], a[3][i], a[4][i])) {
                        cout << "NO\n";
                        return;
                }
        }

        for (int i = 2; i <= 4; i += 2) {
                for (int j = 2; j <= 4; j += 2) {
                        if (!check(a[i - 1][j - 1], a[i - 1][j], a[i][j - 1], a[i][j])) {
                                cout << "NO\n";
                                return;
                        }
                }
        }

        cout << "YES\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
