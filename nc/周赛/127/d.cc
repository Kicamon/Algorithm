#include <iostream>
#include <vector>
using namespace std;
using ll = long long;
const int mod = 998244353;

void solve() {
        int n;
        cin >> n;
        int m = n / 2 + 1;
        vector<ll> a(m);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x < m) {
                        a[x]++;
                }
        }
        ll ans = 0;
        a[0] = 1;
        for (int i = 1; i < m; ++i) {
                a[i] = a[i] * (a[i] - 1) / 2 * a[i - 1] % mod;
                ans = (ans + a[i]) % mod;
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
