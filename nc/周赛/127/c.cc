#include <iostream>
#include <vector>
using namespace std;

void solve() {
        string s;
        cin >> s;
        vector<int> num(s.size());
        int t = 0;
        bool flag = true;
        for (int i = 0; i < s.size(); ++i) {
                if (s[i] < '5') {
                        num[i] = s[i] - '0';
                        continue;
                }
                t = 1;
                flag = false;
                for (int j = i - 1; ~j; --j) {
                        num[j] += t;
                        t = num[j] / 10;
                        num[j] %= 10;
                }
                break;
        }
        if (flag) {
                num.back() = 0;
        }
        if (t) {
                cout << t;
        }
        for (int x : num) {
                cout << x;
        }
        cout << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
