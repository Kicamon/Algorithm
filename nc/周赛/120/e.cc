#include <iostream>
#include <set>
#include <vector>
using namespace std;

vector<vector<int>> g;
vector<int> dep;
vector<set<int>> st;
vector<bool> vis;

void init(int u, int p) {
        dep[u] = dep[p] + 1;
        for (int v : g[u]) {
                if (v == p) {
                        continue;
                }
                vis[u] = true;
                if (st[u].empty()) {
                        st[u].insert(u);
                }
                init(v, u);
                if (!vis[v]) {
                        continue;
                }
                if (dep[*st[u].begin()] == dep[*st[v].begin()]) {
                        for (int x : st[v]) {
                                st[u].insert(x);
                        }
                } else if (dep[*st[u].begin()] < dep[*st[v].begin()]) {
                        st[u] = st[v];
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        g.resize(n + 1), dep.resize(n + 1), st.resize(n + 1), vis.resize(n + 1);
        for (int i = 1, u, v; i < n; ++i) {
                cin >> u >> v;
                g[u].push_back(v);
                g[v].push_back(u);
        }

        init(1, 0);

        for (int i = 1; i <= n; ++i) {
                cout << st[i].size() << " \n"[i == n];
        }

        return 0;
}
