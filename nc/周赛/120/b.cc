#include <iostream>
#include <array>
using namespace std;

array<int, 3> f(int x) {
        array<int, 3> res = { x / 3, x / 3, x / 3 };
        for (int i = 0; i < x % 3; ++i) {
                res[i]++;
        }
        return res;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int l, r;
        cin >> l >> r;
        auto a = f(l - 1), b = f(r);
        for (int i = 0; i < 3; ++i) {
                cout << b[i] - a[i] << " \n"[i == 2];
        }

        return 0;
}
