/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int bit(ll x) {
        int res = 0;
        while (x) {
                res++;
                x >>= 1;
        }
        return res;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll n;
        cin >> n;
        int b = bit(n);
        ll x = 1;
        x <<= b;
        x--;
        cout << x << endl;

        return 0;
}
