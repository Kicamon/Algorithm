/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int bit(int x) {
        int res = 0;
        while (x) {
                res++;
                x >>= 1;
        }
        return res;
}

void p_ans(vector<int> &va, int n) {
        sort(all(va));
        int idx = 0;
        for (int i = 1; i <= n; ++i) {
                if (!va.empty() && i == va[idx]) {
                        idx++;
                        continue;
                }
                cout << i << ' ';
        }
        for (int a : va) {
                cout << a << ' ';
        }
        cout << endl;
        cout << va.size() + 1 << endl;
        cout << 1 << ' ' << n - va.size() << endl;
        for (int i = 0; i < va.size(); ++i) {
                cout << n - i << ' ' << n - i << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        int b_n = bit(n), b_m = bit(m);
        vector<int> va;
        if (m % 2 == 0 || b_m > b_n) {
                cout << -1 << endl;
                return 0;
        } else if (m <= n && m != 1) {
                va.push_back(m);
        } else {
                int a = 1 << (b_m - 1), b = m - a;
                va.push_back(a), va.push_back(b);
        }
        p_ans(va, n);

        return 0;
}
