#include <iostream>
using namespace std;

int reverse(int a) {
        int res = 0;
        while (a) {
                res = res * 10 + a % 10;
                a /= 10;
        }
        return res;
}

void solve() {
        int a, b, k;
        cin >> a >> b >> k;

        if (a % 10 == 0) {
                if (a > b || (b - a) % k) {
                        cout << -1 << '\n';
                        return;
                }
                cout << (b - a) / k << '\n';
                return;
        }

        if (a > b) {
                a = reverse(a);
                if (a > b || (b - a) % k) {
                        cout << -1 << '\n';
                        return;
                }
                cout << (b - a) / k + 1 << '\n';
                return;
        }

        int t = 1e6;
        if ((b - a) % k == 0) {
                t = (b - a) / k;
        }
        a = reverse(a);
        if (a <= b && (b - a) % k == 0) {
                t = min(t, (b - a) / k + 1);
        }
        cout << t << '\n';

        return;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
