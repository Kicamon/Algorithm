/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        int ans = 0;
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
                ans ^= a[i];
                b[i] = a[i];
                if (i) {
                        b[i] ^= b[i - 1];
                }
        }
        for (int i = n - 1, temp = 0; i; --i) {
                temp ^= a[i];
                if ((temp & b[i - 1]) > ans) {
                        ans = temp & b[i - 1];
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
