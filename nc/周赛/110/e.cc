/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int get_num(int a, int b) {
        int t = a + b;
        if (t >= 10) {
                t = t % 10 + t / 10;
        }
        return t;
}

void solve() {
        string s;
        cin >> s;
        vector<vector<ll>> dp(2, vector<ll>(10));
        ll ans = 0;
        for (int i = 0, t; i < (int)s.size(); ++i) {
                fill(all(dp[i % 2]), 0);
                t = s[i] - '0';
                for (int j = 0; j < 10; ++j) {
                        dp[i % 2][get_num(t, j)] += dp[1 - i % 2][j];
                }
                dp[i % 2][t]++;
                for (int j = 0; j < 10; ++j) {
                        ans += dp[i % 2][j] * j;
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
