// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, h, k, q;
        cin >> n >> h >> k;
        array<int, 2> a;
        vector<ll> b(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[0] >> a[1];
                b[i] = ((a[0] / 4) * 3 + ((a[0] % 4) > 2) - (a[0] % 4 == 0)) * a[1];
        }
        sort(b.begin(), b.end());
        for (int i = 1; i < (int)b.size(); ++i) {
                b[i] += b[i - 1];
        }
        cin >> q;
        int x;
        while (q--) {
                cin >> x;
                ll t = 1ll * k * x + h;
                cout << lower_bound(b.begin(), b.end(), t) - b.begin() << ' ';
        }
        cout << endl;

        return 0;
}
