/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(9);
        for (int i = 0, x; i < n; i++) {
                cin >> x;
                a[x - 1]++;
        }
        if (n < 9) {
                cout << "NO" << endl;
        } else {
                if (*max_element(all(a)) - *min_element(all(a)) > 1) {
                        cout << "NO" << endl;
                } else {
                        cout << "YES" << endl;
                }
        }

        return 0;
}
