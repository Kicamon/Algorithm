/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, l1, r1, l2, r2;
        cin >> n >> l1 >> r1 >> l2 >> r2;
        r1 = min(r1, l1 + 9);
        r2 = min(r2, l2 + 9);
        vector<int> temp(n - 2);
        for (int &x : temp) {
                cin >> x;
        }
        if (n == 3) {
                for (int i = l1; i <= r1; ++i) {
                        for (int j = l2; j <= r2; ++j) {
                                if (((i % 10) * (j % 10)) % 10 == temp[0]) {
                                        cout << i << ' ' << j << endl;
                                        return;
                                }
                        }
                }
        } else {
                for (int i = l1; i <= r1; ++i) {
                        for (int j = l2; j <= r2; ++j) {
                                if (((i % 10) * (j % 10)) % 10 == temp[0]
                                    && (j % 10 * temp[0]) % 10 == temp[1]) {
                                        cout << i << ' ' << j << endl;
                                        return;
                                }
                        }
                }
        }
        cout << -1 << ' ' << -1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
