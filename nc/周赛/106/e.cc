/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll n, sum;
        cin >> n >> sum;
        if (sum < n) {
                cout << -1 << endl;
        }
        if (sum < 8) {
                return;
        }
        vector<ll> v(n, 8);
        ll t = accumulate(all(v), 0);
        int tet = 0;
        if (t > sum) {
                int temp = (t - sum) / 8 + bool((t - sum) % 8);
                fill(v.end() - temp, v.end(), 0);
                t -= temp * 8;
                tet = v.end() - v.begin() - temp;
        }
        ll idx = 1;
        if (!tet || tet == n) {
                idx *= 10;
                tet = 0;
        }
        while (t + idx * 8 <= sum) {
                for (; tet < n && t + idx * 8 <= sum; ++tet) {
                        v[tet] += idx * 8;
                        t += idx * 8;
                }
                if (tet == n) {
                        idx *= 10;
                        tet = 0;
                }
        }
        while (t + idx * 4 <= sum) {
                for (; tet < n && t + idx * 4 <= sum; ++tet) {
                        v[tet] += idx * 4;
                        t += idx * 4;
                }
                if (tet == n) {
                        idx *= 10;
                        tet = 0;
                }
        }
        for (ll x : v) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
