#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        if (n == 1) {
                cout << -1 << endl;
        } else {
                for (int i = 0; i < n; ++i) {
                        if (!i || i == n - 1) {
                                cout << 1;
                        } else {
                                cout << 0;
                        }
                }
                cout << endl;
                for (int i = 0; i < n; ++i) {
                        if (i % 2) {
                                cout << 2 + (n % 2);
                        } else {
                                cout << 1;
                        }
                }
                cout << endl;
        }

        return 0;
}
