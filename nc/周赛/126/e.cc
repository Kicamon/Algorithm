#include <cmath>
#include <iostream>
using namespace std;
using ll = long long;
const ll inf = 1e9;

void solve() {
        ll n;
        cin >> n;
        n = sqrt(n * inf) + 1;
        cout << n * n << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
