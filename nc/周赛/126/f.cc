#include <functional>
#include <iostream>
#include <vector>
using namespace std;

int change(int t) {
        t = t == 4 ? 1 : t;
        return t;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        vector<vector<int>> g(n + 1);
        vector<int> num(n + 1), c(n + 1);
        for (int i = 0, u, v; i < n; ++i) {
                cin >> u >> v;
                g[u].push_back(v);
                g[v].push_back(u);
                num[u]++;
                num[v]++;
        }

        int root = 0;
        for (int i = 1; i <= n; ++i) {
                if (num[i] == 1) {
                        root = i;
                        break;
                }
        }

        function<void(int, int, int)> dfs = [&](int u, int p, int t) {
                t = change(++t);
                int flag = 0;
                for (int v : g[u]) {
                        if (v == p) {
                                continue;
                        }
                        if (c[v]) {
                                if (c[u] == c[v]) {
                                        c[u] = t;
                                        t = change(++t);
                                        flag = v;
                                        break;
                                }
                                continue;
                        }
                        c[v] = t;
                        dfs(v, u, t);
                }
                if (!flag) {
                        return;
                }
                for (int v : g[u]) {
                        if (v == p || v == flag) {
                                continue;
                        }
                        c[v] = t;
                        dfs(v, u, t);
                }
        };

        c[root] = 1;
        dfs(root, 0, 1);

        for (int i = 1; i <= n; ++i) {
                cout << c[i] << " \n"[i == n];
        }

        return 0;
}
