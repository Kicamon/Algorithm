#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
using namespace std;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, k;
        cin >> n >> k;
        vector<string> s(n);
        map<string, int> mp, vis;
        for (int i = 0; i < n; ++i) {
                cin >> s[i];
                mp[s[i]]++;
        }

        sort(s.begin(), s.end(), [&](string a, string b) {
                if (a.size() != b.size()) {
                        return a.size() > b.size();
                }
                return a > b;
        });

        for (int i = 0; i < n; ++i) {
                if (vis[s[i]]) {
                        continue;
                }
                string t;
                int num = 0;

                for (char c : s[i]) {
                        t += c;
                        if (!mp[t]) {
                                continue;
                        }
                        num += mp[t];
                        vis[t] = true;
                        if (num == k) {
                                cout << t << '\n';
                                return 0;
                        } else if (num > k) {
                                break;
                        }
                }
        }
        cout << -1 << '\n';

        return 0;
}
