int input = 0;
const int mod = 1e9 + 7;
#define int long long

int qmi(int a, ll b, int p) {
        int res = 1;
        a %= p;
        while (b) {
                if (b & 1)
                        res = (long long)res * a % p;
                b >>= 1;
                a = (long long)a * a % p;
        }
        return res;
}

void solve() {
        ll n;
        cin >> n;
        cout << (n * n - n >> 1) % mod * qmi(2, n - 1, mod) % mod << endl;
}

