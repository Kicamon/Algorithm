//#pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
const int N = 2e5 + 10;

vint num[N];

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int n,m;
	cin >> n >> m;
	vint a(n);
	int vmax = 0;
	for(int i = 0;i < n;++i)
	{
		cin >> a[i];
		num[a[i]].pb(i);
		vmax = max(vmax,a[i]);
	}
	while(m--)
	{
		int op,x;
		cin >> op >> x;
		if(op == 1)
		{
			a.pb(x);
			num[x].pb(n++);
			if(x > vmax)
				vmax = x;
		}
		else 
		{
			int res = 0;
			for(int i = 1;(ll)a[x - 1] * i <= vmax;++i)
			{
				if(!num[a[x - 1] * i].empty())
				{
					int t = upper_bound(all(num[a[x - 1] * i]),x - 1) -  num[a[x - 1] * i].begin();
					res += num[a[x - 1] * i].size() - t;
				}
			}
			cout << res << endl;
		}
	}
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
