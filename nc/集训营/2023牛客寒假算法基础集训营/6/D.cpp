const int N = 2e5 + 10;

int u[N],su[N],ud[N],du[N];

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	string s;
	cin >> s;
	int n = s.length();
	s = ' ' + s;
	for(int i = 1;i <= n;++i)
	{
		u[i] = u[i - 1] + (s[i] == 'u');
		ud[i] = ud[i - 1] + (s[i] == 'd'?u[i]:0);
	}
	for(int i = n;i;--i)
	{
		su[i] = su[i + 1] + (s[i] == 'u');
		du[i] = du[i + 1] + (s[i] == 'd'?su[i]:0);
	}
	int val = 0,idx = 0;
	for(int i = 1;i <= n;++i)
	{
		int cnt = 0;
		if(s[i] == 'u')
			cnt = ud[i - 1] + du[i + 1];
		else if(s[i] == 'd')
			cnt = u[i - 1] * su[i + 1];
		if(cnt > val)
			idx = i,val = cnt;
	}
	s[idx] = 'a';
	for(int i = 1;i <= n;++i)
		cout << s[i];
	cout << endl;
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
