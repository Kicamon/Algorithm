#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
const int mod = 1e9 + 7;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        //clock_t start, finish;
        //start = clock();

        int n;
        cin >> n;
        vint a(n);
        int l = 0, r = n - 1;
        for (int i = 1; i <= n;) {
                a[l++] = i++;
                if (i <= n)
                        a[r--] = i++;
        }
        vint b = a;
        for (int i = 0; i < n; ++i) {
                vint c(b.size() - 1);
                for (int j = 0; j < n - i - 1; ++j)
                        c[j] = ((ll)b[j] + b[j + 1]) % mod;
                b = c;
        }
        cout << b[0] << endl;
        for (auto x : a)
                cout << x << ' ';
        cout << endl;

        //finish = clock();
        //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
