//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back

int change(int t) {
        int res = 0;
        while (t) {
                res++;
                t -= t & -t;
        }
        return res;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        //clock_t start, finish;
        //start = clock();

        int n, m;
        cin >> n >> m;
        int cnt = 0;
        priority_queue<int, vector<int>, less<int>> q;
        for (int i = 0, c; i < n; ++i) {
                cin >> c;
                cnt += change(c);
                q.push(c);
        }
        vint ans(cnt);
        for (int i = 0; i < cnt; ++i) {
                int t = q.top();
                q.pop();
                q.push(change(t));
                ans[i] = q.top();
        }
        while (m--) {
                int k;
                cin >> k;
                k = min(k, cnt);
                cout << ans[k - 1] << endl;
        }

        //finish = clock();
        //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
