// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n;
    cin >> n;
    string s;
    cin >> s;
    s = ' ' + s;
    int f[2] = {0, 0};
    ll ans = 0;
    for (int i = 1; i <= n; ++i)
    {
        if (s[i] == '1')
        {
            if (f[0] < f[1])
            {
                int len = i - f[1];
                ans += 1ll * len * (len + 1) >> 1;
                f[0] = i;
            }
            else if (f[0] == 0)
                f[0] = i;
        }
        else if (s[i] == '4')
        {
            if (f[1] < f[0])
            {
                int len = i - f[0];
                ans += 1ll * len * (len + 1) >> 1;
                f[1] = i;
            }
            else if (f[1] == 0)
                f[1] = i;
        }
    }
    int len = 0;
    if (s[n] != '1' && s[n] != '4')
        len = n - min(f[0], f[1]);
    ans += 1ll * len * (len + 1) >> 1;
    cout << ans << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
