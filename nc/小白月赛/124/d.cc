/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int a, b, l, r;
        cin >> a >> b >> l >> r;
        int L = (b - a) / (r - b);
        int R = (b - a) / (l - b);
        int ans = 0;
        for (int i = max(1, L); i <= R; ++i) {
                ans += (b - a) % i == 0;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
