// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, k;
        cin >> n >> m >> k;
        vector<array<int, 2>> w(m);
        for (int i = 0; i < m; ++i) {
                cin >> w[i][0] >> w[i][1];
        }
        sort(all(w));
        vector<ll> dp(n + 1);
        for (int i = 0; i < m; ++i) {
                dp[w[i][0]]++;
                dp[w[i][1]]--;
        }
        ll ans = dp[0];
        for (int i = 1; i < n; ++i) {
                dp[i] += dp[i - 1];
                ans = max(ans, dp[i]);
        }
        cout << (ans - 1) / k + 1 << endl;

        return 0;
}
