/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        map<char, int> mp;
        for (int i = 0; i < n; ++i) {
                mp[s[i]]++;
        }
        vector<int> ans;
        for (auto [_, x] : mp) {
                ans.push_back(x);
        }
        for (int i = 1; i < (int)ans.size(); ++i) {
                if (ans[i] != ans[i - 1] + 1) {
                        cout << "NO" << endl;
                        return;
                }
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
