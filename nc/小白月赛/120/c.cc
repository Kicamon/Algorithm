/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <utility>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        if (n % 2) {
                for (int i = 1; i <= n; ++i) {
                        cout << i << " \n"[i == n];
                }
        } else if (n > 2) {
                vector<int> c(n);
                iota(all(c), 1);
                swap(c[n - 2], c[n - 3]);
                for (int i = 0; i < n; ++i) {
                        cout << c[i] << " \n"[i == n];
                }
        } else {
                cout << -1 << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
