/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<pair<ll, int>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i].first;
                a[i].second = i;
        }
        sort(all(a));
        vector<int> ans(n, -1);
        queue<pair<ll, int>> q;
        for (int i = 0; i < n; ++i) {
                while (!q.empty()) {
                        auto t = q.front();
                        if (a[i].first % t.first) {
                                ans[a[i].second] = t.second + 1;
                                break;
                        } else {
                                q.pop();
                        }
                }
                q.push(a[i]);
        }
        for (int x : ans) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
