/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m;
        cin >> n >> m;
        vector<int> a(n), b(m), c, d;
        for (int &x : a) {
                cin >> x;
                if (c.empty() || x != c.back()) {
                        c.push_back(x);
                }
        }
        for (int &x : b) {
                cin >> x;
                if (d.empty() || x != d.back()) {
                        d.push_back(x);
                }
        }
        if (c != d) {
                cout << -1 << endl;
                return;
        }
        vector<int> ansa((int)c.size()), ansb((int)d.size());
        for (int i = 0, idx = 0; i < n; ++i) {
                if (a[i] != c[idx]) {
                        idx++;
                }
                ansa[idx]++;
        }
        for (int i = 0, idx = 0; i < m; ++i) {
                if (b[i] != d[idx]) {
                        idx++;
                }
                ansb[idx]++;
        }
        int ans = 0;
        for (int i = 0; i < (int)ansa.size(); ++i) {
                if (ansa[i] > ansb[i]) {
                        cout << -1 << endl;
                        return;
                }
                int t = 0;
                while (ansa[i] < ansb[i]) {
                        ansa[i] <<= 1;
                        t++;
                }
                ans = max(t, ans);
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
