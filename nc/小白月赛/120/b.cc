/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e4 + 10;

vector<int> prime;
vector<bool> vis(N);

void solve() {
        int n;
        cin >> n;
        for (int i = 1; i < n; ++i) {
                int t = i ^ n;
                if (vis[t]) {
                        cout << i << endl;
                        return;
                }
        }
        cout << -1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 2; i < N; ++i) {
                if (!vis[i]) {
                        prime.push_back(i);
                }
                for (int j : prime) {
                        if (i * j >= N) {
                                break;
                        }
                        vis[i * j] = true;
                        if (i % j == 0) {
                                break;
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
