/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <cstddef>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

bool check(char c) {
        if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
            c == '.') {
                return true;
        }
        return false;
}

bool check(string s, char c) {
        if (s[0] == c || s[s.size() - 1] == c) {
                return true;
        }
        return false;
}

void solve() {
        string s;
        cin >> s;
        int num = 0, idx = -1;
        for (size_t i = 0; i < s.size(); ++i) {
                if (s[i] == '@') {
                        num++;
                        idx = idx == -1 ? i : idx;
                }
        }
        if (num > 1 || !num) {
                cout << "No\n";
                return;
        }
        string prev = s.substr(0, idx);
        string next = s.substr(idx + 1);
        if (!prev.size() || prev.size() > 64 || check(prev, '.')) {
                cout << "No\n";
                return;
        }
        if (!next.size() || next.size() > 255 || check(next, '.') || check(next, '-')) {
                cout << "No\n";
                return;
        }
        for (size_t i = 0; i < prev.size(); i++) {
                if (!check(prev[i])) {
                        cout << "No\n";
                        return;
                }
        }
        for (size_t i = 0; i < next.size(); i++) {
                if (!check(next[i]) && next[i] != '-') {
                        cout << "No\n";
                        return;
                }
        }
        cout << "Yes\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
