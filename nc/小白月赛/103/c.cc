/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <cstddef>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll n;
        cin >> n;
        if (n <= 2) {
                cout << 1 << endl;
                return;
        }
        size_t size = 0;
        int idx = 0;
        while (n) {
                idx += n % 2;
                n >>= 1;
                size++;
        }
        cout << (1ll << (size - (idx == 1))) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
