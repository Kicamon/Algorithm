#include <functional>
#include <iostream>
#include <vector>
#include <array>
using namespace std;

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        string s;
        cin >> s;
        s = ' ' + s;
        vector<array<int, 3>> a(n + 1);
        for (int i = 1; i <= n; ++i) {
                a[i] = a[i - 1];
                if (s[i] == 'w') {
                        a[i][0]++;
                } else if (s[i] == 'y') {
                        a[i][1]++;
                } else {
                        a[i][2]++;
                }
        }

        function<bool(int)> check = [&](int len) {
                for (int i = len; i <= n; ++i) {
                        int t[3] = { a[i][0] - a[i - len][0], a[i][1] - a[i - len][1],
                                     a[i][2] - a[i - len][2] };
                        int res = 0;
                        res = max(t[1] + t[2] * 2, t[1] * 2 + t[2]);
                        res += min(t[0], m) * 2;
                        if (res >= k) {
                                return true;
                        }
                }
                return false;
        };

        int l = 1, r = n + 1;
        while (l < r) {
                int mid = (l + r) >> 1;
                if (check(mid)) {
                        r = mid;
                } else {
                        l = mid + 1;
                }
        }
        int ans = l == n + 1 ? -1 : l;
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
