#include <functional>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
using ll = long long;
const int mod = 998244353;

void solve() {
        int n;
        cin >> n;
        vector<vector<int>> g(n + 1);
        for (int i = 1, u, v; i < n; ++i) {
                cin >> u >> v;
                g[u].push_back(v);
                g[v].push_back(u);
        }

        function<array<int, 2>(int, int)> dfs = [&](int u, int p) {
                array<int, 2> res = { 1, 0 };

                for (int v : g[u]) {
                        if (v == p) {
                                continue;
                        }
                        array<int, 2> temp = dfs(v, u);
                        res[0] = ((ll)res[0] * temp[0]) % mod;
                        res[1] += temp[1];
                }

                res[0] = (res[0] + res[1]) % mod;
                res[1]++;

                return res;
        };

        array<int, 2> ans = dfs(1, 0);
        cout << ans[0] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
