#include <functional>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
using ll = long long;
const int mod = 998244353;

void solve() {
        int n;
        cin >> n;
        vector<vector<int>> g(n + 1);
        for (int i = 1, u, v; i < n; ++i) {
                cin >> u >> v;
                g[u].push_back(v);
                g[v].push_back(u);
        }

        function<array<int, 3>(int, int)> dfs = [&](int u, int p) {
                array<int, 3> res = { 0, 0, 0 };
                for (int v : g[u]) {
                        if (v == p) {
                                continue;
                        }
                        array<int, 3> temp = dfs(v, u);
                        res[0] = ((ll)res[0] + temp[0] + temp[2]) % mod;
                        res[0] = ((ll)res[0] + (ll)res[2] * temp[1] % mod) % mod;
                        res[0] = ((ll)res[0] + (ll)res[1] * temp[2] % mod) % mod;
                        res[1] = ((ll)res[1] + temp[1]) % mod;
                        res[2] = ((ll)res[2] + temp[2]) % mod;
                }

                res[2]++;
                res[1] = ((ll)res[1] + res[2]) % mod;

                return res;
        };

        array<int, 3> ans = dfs(1, 0);
        cout << ans[0] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
