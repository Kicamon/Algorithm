/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        if (a[0] == a[n - 1]) {
                cout << 1 << endl;
        } else {
                int d = 0, p = 0;
                bool f = true;
                for (int i = 0; i < n; ++i) {
                        if (a[i] == a[0] && (p == d || f)) {
                                p = i;
                        } else {
                                f = false;
                        }
                }
                if (p - d == 0) {
                        cout << -1 << endl;
                } else {
                        for (int i = p + 1; i < n; ++i) {
                                if (a[i] != a[0]) {
                                        if (i != n - 1) {
                                                cout << 2 << endl;
                                                break;
                                        } else {
                                                cout << -1 << endl;
                                                break;
                                        }
                                }
                        }
                }
        }

        return 0;
}
