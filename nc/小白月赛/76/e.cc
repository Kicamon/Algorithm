// #pragma GCC optimize(2)
#include <bits/stdc++.h>
#include <queue>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<int> a(n);
    int b = 0;
    queue<int> q;
    for (int i = 0; i < n; ++i) {
        if (s[i] == '(') {
            a[i] = -1;
        } else {
            a[i] = 1;
        }
        b += a[i];
    }
    if (b) {
        cout << -1 << endl;
        return;
    }
    int res = 0;
    int cnt = 0;
    for (int i = 0; i < n; ++i) {
        if (a[i] == 1) {
            q.push(i);
        } else if (!q.empty()) {
            res += i - q.front() - cnt;
            cout << i << ' ' << q.front() << endl;
            cnt++;
            q.pop();
        }
    }
    cout << res << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
