#include <algorithm>
#include <deque>
#include <iostream>
#include <vector>
using namespace std;
const int inf = 0x3f3f3f3f;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
        }
        sort(a.begin(), a.end());
        for (int i = 1; i <= n; ++i) {
                a.push_back(a[i]);
        }
        vector<int> b(n * 2 + 1);
        for (int i = 2; i <= n * 2; ++i) {
                b[i] = min(abs(a[i] - a[i - 1]), 8 - abs(a[i] - a[i - 1]));
                b[i] += b[i - 1];
        }
        int ans = inf;
        for (int i = n; i <= n * 2; ++i) {
                ans = min(ans, b[i] - b[i - n + 1]);
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
