#include <iostream>
using namespace std;

double to_num(string a) {
        double res;
        int t = 0;
        for (char c : a) {
                if (c != '+') {
                        t = t * 10 + (c - '0');
                }
        }
        res = t;
        if (a.back() == '+') {
                res += 0.5;
        }
        return res;
}

void solve() {
        string a, b;
        cin >> a >> b;
        double x = to_num(a), y = to_num(b);
        if (x > y) {
                cout << "Yes\n";
        } else {
                cout << "No\n";
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
