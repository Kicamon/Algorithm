#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n, q;
        cin >> n >> q;
        vector<int> a(n + 2);
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                if (x > y) {
                        swap(x, y);
                }
                if (x == y) {
                        a[x - 1]++, a[x]++, a[x + 1]++;
                } else if (y - x == 1) {
                        a[x - 1]++, a[x]++;
                        a[y]++, a[y + 1]++;
                } else if (y - x == 2) {
                        a[x - 1]++, a[x]++, a[x + 1]++;
                        a[y]++, a[y + 1]++;
                } else {
                        a[x - 1]++, a[x]++, a[x + 1]++;
                        a[y - 1]++, a[y]++, a[y + 1]++;
                }
        }
        for (int i = 0, x; i < q; ++i) {
                cin >> x;
                cout << n - a[x] << " \n"[i == q - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
