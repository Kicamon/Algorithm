// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

void solve()
{
    int n, k;
    cin >> n >> k;
    vector<ll> a(n + 1);
    vector<int> num(n + 1, 1);
    a[0] = 1;
    for (int i = 1; i <= n; ++i)
    {
        cin >> a[i];
        a[i] *= a[i - 1];
        ll b = a[i];
        while (b % 10 == 0)
        {
            num[i] *= 10;
            b /= 10;
        }
    }
    ll p = 1;
    int res = 0;
    while (k--)
        p *= 10;
    for (int i = 0; i <= n; ++i)
    {
        ll x = lower_bound(a.begin(), a.end(), p * a[i]) - a.begin();
        ll y = lower_bound(a.begin(), a.end(), p * 10 * num[i]) - a.begin();
        if (!x)
            x++;
        res += y - x;
    }
    cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
