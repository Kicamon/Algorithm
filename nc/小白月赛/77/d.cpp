// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n, m, k;
    cin >> n >> m >> k;
    int res = 1;
    for (int i = 1; i < m; ++i) {
        res *= 10;
    }
    string s;
    cin >> s;
    map<int, int> p;
    int cnt = 0;
    for (int i = 0; i < n; ++i) {
        if (cnt > res) {
            p[cnt]++;
            cnt %= cnt / res * res;
        }
        cnt = cnt * 10;
        cnt += s[i] - '0';
    }
    if (cnt > res) {
        p[cnt]++;
    }
    int ans = 0;
    for (auto x : p) {
        /* cout << x.first << endl; */
        if (x.second == k) {
            ans++;
        }
    }
    cout << ans << endl;

    return 0;
}
