// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int a, b;
    cin >> a >> b;
    vint x, y;
    while (a)
    {
        x.pb(a % 10);
        a /= 10;
    }
    while (b)
    {
        y.pb(b % 10);
        b /= 10;
    }
    if (x.size() != y.size())
        cout << -1 << endl;
    else
    {
        vint res;
        for (int i = 0; i < (int)x.size(); ++i)
            res.pb(x[i] * y[i]);
        reverse(all(res));
        for (int c : res)
            cout << c;
        cout << endl;
    }
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
