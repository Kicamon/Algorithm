#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n;
    cin >> n;
    char s[n + 2];
    cin >> s + 1;
    vector<array<int, 26>> num(n + 1);
    for (int i = 1; i <= n; ++i)
    {
        num[i] = num[i - 1];
        num[i][s[i] - 'a']++;
    }

    auto get = [&](int l, int r) {
        array<int, 26> res;
        for (int i = 0; i < 26; ++i)
            res[i] = num[r][i] - num[l - 1][i];
        double t = 0;
        for (int i = 0; i < 26; ++i)
            t += res[i] * (res[i] - 1) >> 1;
        return t / (r - l + 1);
    };

    double ans = 0;
    for (int i = 1; i <= n; ++i)
        for (int j = i; j <= n; ++j)
        {
            double res = get(i, j);
            ans = max(ans, res);
        }
    cout << ans << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
