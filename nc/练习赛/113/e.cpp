#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n, k;
    cin >> n >> k;
    vector<int> s(n);
    s[0] = 1;
    int l = 1, r = n - 1, idx = 0;
    for (int i = 2; i <= n; ++i) {
        if (abs(l - idx) > abs(r - idx)) {
            if (abs(l - idx) + n - i <= k) {
                k -= abs(l - idx);
                idx = l++;
                s[idx] = i;
            } else {
                idx = r--;
                s[idx] = i;
                k--;
            }
        } else {
            if (abs(r - idx) + n - i <= k) {
                k -= abs(r - idx);
                idx = r--;
                s[idx] = i;
            } else {
                idx = l++;
                s[idx] = i;
                k--;
            }
        }
    }
    for (int i : s) {
        cout << i << ' ';
    }
    cout << endl;

    return 0;
}
