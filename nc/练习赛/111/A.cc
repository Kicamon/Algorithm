void solve() {
        int n;
        cin >> n;
        int ans = 1;
        while (n) {
                if (n % 10 == 0)
                        ans *= 10;
                else {
                        ans *= 10 - n % 10;
                        break;
                }
                n /= 10;
        }
        cout << ans << endl;
}

