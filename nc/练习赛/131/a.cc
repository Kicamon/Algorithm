/* #pragma GCC optimize(2) */
#include <algorithm>
#include <array>
#include <iostream>
#include <set>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 2> > a(n);
        for (int i = 0; i < n; i++) {
                cin >> a[i][0];
                a[i][1] = i + 1;
        }
        sort(rall(a));
        ll m = 1, v = 0;
        set<int> ans;
        int idx = 0;
        for (int i = 0; i < n; i++, m++) {
                ll temp = m * m * a[i][0];
                if (temp > v) {
                        idx = i;
                        v = temp;
                }
        }
        for (int i = 0; i <= idx; i++) {
                ans.insert(a[i][1]);
        }
        cout << ans.size() << endl;
        for (const auto &it : ans) {
                cout << it << ' ';
        }
        cout << endl;

        return 0;
}
