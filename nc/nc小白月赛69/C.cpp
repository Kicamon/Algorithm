#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n, t, p;
    cin >> n >> t >> p;
    vint a(n), b(n), c(n), x(n), y(n);
    for (int i = 0; i < n; ++i)
        cin >> a[i] >> b[i] >> c[i] >> x[i] >> y[i];
    int res = 0;
    for (int i = 0; i < 1 << n; ++i)
    {
        int cnt = 0, k = 0;
        for (int j = 0; j < n; ++j)
            if (i >> j & 1)
                if (x[j] + k <= t)
                {
                    k += x[j];
                    cnt += max(c[j], a[j] - b[j] * k - y[j] * p);
                }
        res = max(res, cnt);
    }
    cout << res << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
