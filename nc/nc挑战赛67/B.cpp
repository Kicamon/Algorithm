// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;
const int N = 3e5;

void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    s = ' ' + s;
    int num[3] = {};
    ll res = 0;
    map<ll, ll> p;
    p[0]++;
    for (int i = 1; i <= n; ++i)
    {
        num[s[i] - '0']++;
        int k = min({num[0], num[1], num[2]});
        for (int j = 0; j < 3; ++j)
            num[j] -= k;
        ll cnt = 1ll * num[0] * N * N + 1ll * num[1] * N + num[2];
        res += p[cnt]++;
    }
    cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
