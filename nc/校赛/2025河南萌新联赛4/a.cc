/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e4 + 10;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> ans(N);
        map<int, int> a;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                a[x]++;
        }
        vector<array<int, 2>> d;
        for (auto [x, y] : a) {
                ans[x << 1] += y >> 1 << 1;
                for (auto [u, v] : d) {
                        ans[x + u] += min(y, v) << 1;
                }
                d.push_back({ x, y });
        }
        cout << *max_element(all(ans)) << endl;

        return 0;
}
