/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const ll inf = 1e18;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        vector<int> a(n + 2);
        vector<ll> pre(n + 2), net(n + 2), temp(n + 2);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                temp[i] = temp[i - 1] + a[i];
                pre[i] = pre[i - 1] + (temp[i - 1] + k - 1) / k;
        }
        ll ans = inf;
        fill(all(temp), 0);
        for (int i = n; i; --i) {
                temp[i] = temp[i + 1] + a[i];
                net[i] = net[i + 1] + (temp[i + 1] + k - 1) / k;
                ans = min(ans, net[i] + pre[i]);
        }
        cout << ans << endl;

        return 0;
}
