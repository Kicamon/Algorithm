/* #pragma GCC optimize(2) */
#include <csetjmp>
#include <iostream>
#include <set>
#include <type_traits>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        set<int> s;
        for (int &x : a) {
                cin >> x;
                s.insert(x);
        }
        bool flag = true;
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < i; ++j) {
                        if (!s.count(a[i] | a[j])) {
                                flag = false;
                                break;
                        }
                }
        }
        cout << (flag ? "YES" : "NO") << endl;

        return 0;
}
