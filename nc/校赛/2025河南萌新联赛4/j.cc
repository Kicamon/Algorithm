/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<ll> a(n);
        int me = 0, sme = 0;
        for (ll &x : a) {
                cin >> x;
                if (x > me) {
                        sme = me;
                        me = x;
                } else if (x > sme) {
                        sme = x;
                }
        }
        if (me) {
                if (a[0] == me) {
                        if (sme) {
                                for (int i = n - 1; i > 1; --i) {
                                        if (a[i] == sme) {
                                                a[0] += a[i];
                                                a.erase(a.begin() + i, a.begin() + i + 1);
                                                break;
                                        }
                                }
                        }
                } else {
                        for (int i = n - 1; i > 1; --i) {
                                if (a[i] == me) {
                                        a[0] += a[i];
                                        a.erase(a.begin() + i, a.begin() + i + 1);
                                        break;
                                }
                        }
                }
        }
        cout << (int)a.size() << endl;
        for (int x : a) {
                cout << x << ' ';
        }
        cout << endl;

        return 0;
}
