#include <cstddef>
#include <iostream>
#include <string>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        string s[3];
        for (int i = 0; i < 3; i++) {
                cin >> s[i];
        }
        ll ans = 0;
        for (int i = 0; i < 3; i++) {
                for (size_t j = 0; j < s[i].size(); j++) {
                        ans += s[i][j] - '0';
                }
                if (ans % 3 == 0) {
                        cout << "LaoSu Like Fake" << endl;
                        return;
                }
                ans = 0;
        }
        cout << "LaoSu Don't Like Fake" << endl;
}

