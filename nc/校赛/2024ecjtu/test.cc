/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define ull unsigned long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

ull dfs(ull u, ull &x, ull &y) {
        if (u == 1) {
                return x;
        } else if (u == 2) {
                return y;
        }
        return (dfs(u - 1, x, y) + dfs(u - 2, x, y));
}

void solve() {
        ull n, x, y, ans;
        cin >> x >> y >> n;
        ans = dfs(n, x, y);
        cout << ans % 2 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
