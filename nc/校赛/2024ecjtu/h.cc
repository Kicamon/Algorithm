/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

const int N = 1e5 + 10;
int n;

struct Node {
        int val;
        int pot;
};

Node node[N];

void bfs() {
        queue<int> q;
        q.push(1);
        while (!q.empty()) {
                int t = q.front();
                q.pop();
                if (t + 1 <= n && node[t].val + 1 < node[t + 1].val) {
                        node[t + 1].val = node[t].val + 1;
                        q.push(t + 1);
                }
                if (t - 1 > 0 && node[t].val + 1 < node[t - 1].val) {
                        node[t - 1].val = node[t].val + 1;
                        q.push(t - 1);
                }
                if (t + node[t].pot <= n && node[t].val + 1 < node[t + node[t].pot].val) {
                        node[t + node[t].pot].val = node[t].val + 1;
                        q.push(t + node[t].pot);
                }
                if (t - node[t].pot > 0 && node[t].val + 1 < node[t - node[t].pot].val) {
                        node[t - node[t].pot].val = node[t].val + 1;
                        q.push(t - node[t].pot);
                }
        }
}

void solve() {
        cin >> n;
        for (int i = 1; i <= n; i++) {
                node[i].val = n - 1;
                cin >> node[i].pot;
        }
        node[1].val = 0;
        bfs();
        cout << node[n].val << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
