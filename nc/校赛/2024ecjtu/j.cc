/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<ll> a(n + 1);
        ll res[2] = { 0, 0 };
        for (int i = 1; i <= n; i++) {
                cin >> a[i];
                if (a[i] >= 0) {
                        res[i % 2] += a[i] + 1;
                }
        }
        ll ans = min(res[0], res[1]);
        int tmp = !(res[0] > res[1]);
        for (int i = 1; i <= n; i++) {
                if (i % 2 == tmp && a[i] >= 0) {
                        a[i] = -1;
                }
        }
        res[0] = 0, res[1] = 0;
        int opt = 0;
        tmp = 0;
        for (int i = 1, idx = 0; i <= n; i++) {
                res[idx] = res[idx ^ 1] + a[i];
                tmp += res[idx] == opt;
                if (res[idx]) {
                        continue;
                }
                if (opt && !tmp) {
                        opt = 0;
                        res[idx] -= opt;
                        continue;
                }
                res[idx] = a[i] > 0 ? 1 : -1;
                opt = res[idx] << 1;
                tmp = 0;
                ans++;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
