/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define ull unsigned long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ull n, x, y, ans;
        cin >> x >> y >> n;
        if (n == 1) {
                ans = x;
        } else if (n == 2) {
                ans = y;
        } else if (n == 3) {
                ans = x + y;
        } else {
                ans = x * ((n - 2) % 3 != 0) + y * ((n - 1) % 3 != 0);
        }
        cout << ans % 2 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
