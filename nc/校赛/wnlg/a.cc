#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

bool check(string &s) {
        int l = 0, r = (int)s.size() - 1;
        while (l < r) {
                if (s[l] != s[r]) {
                        return false;
                }
                l++, r--;
        }
        return true;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        string s;
        cin >> s;
        bool f = check(s);
        bool change = false;
        while (m--) {
                int op;
                cin >> op;
                if (op == 1) {
                        char c;
                        cin >> c;
                        s += c;
                        change = true;
                } else {
                        int t;
                        cin >> t;
                        if (change) {
                                f = check(s);
                                change = false;
                        }
                        if (t % (int)s.size() == 0 && (f || (t / (int)s.size()) % 2 == 1)) {
                                cout << "Yes\n";
                        } else {
                                cout << "No\n";
                        }
                }
        }

        return 0;
}
