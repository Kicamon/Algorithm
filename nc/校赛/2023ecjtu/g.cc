// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll n, m;
        cin >> n >> m;
        if (n != 1 && m == 1) {
                cout << -1 << endl;
        } else {
                ll res = 1;
                n -= m;
                if (n) {
                        if (n <= m - 1) {
                                res += 2;
                        } else {
                                res += (n / (m - 1) + (n % (m - 1) > 0)) * 2;
                        }
                }
                cout << res << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
