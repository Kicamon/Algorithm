// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 2e5 + 10;

vector<array<int, 2>> e[N];
ll va[N];
bool vis[N];

void Clear(int n) {
        for (int i = 1; i <= n; ++i) {
                e[i].clear();
                va[i] = 0;
                vis[i] = false;
        }
}

void dfs(int u, int fa, ll val) {
        va[u] = val;
        for (auto [v, d] : e[u]) {
                if (v == fa) {
                        continue;
                }
                if (va[v] != val + d) {
                        vis[v] = true;
                }
        }
}

void solve() {
        int n, m;
        cin >> n >> m;
        for (int i = 0, a, b, c; i < m; ++i) {
                cin >> a >> b >> c;
                e[a].push_back({ b, c });
        }
        for (int i = 1; i <= n; ++i) {
                if (!va[i]) {
                        dfs(i, -1, 0);
                }
        }
        Clear(n);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
