#include <bits/stdc++.h>
#include <cstdlib>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n, a, b;
    cin >> n >> a >> b;
    vint num(n * 2 + 1);
    num[a] = b;
    if ((a <= n && b > n) || (a > n && b <= n))
        for (int i = 2 * n, j = 1; i; --i)
        {
            if (i == a)
                continue;
            if (j == b)
                j++;
            num[i] = j++;
        }
    else
        for (int i = 1, j = 1; i <= 2 * n; ++i)
        {
            if (i == a)
                continue;
            if (j == b)
                j++;
            num[i] = j++;
        }
    for (int i = 1; i <= 2 * n; ++i)
        cout << num[i] << ' ';
    cout << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
