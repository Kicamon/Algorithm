/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<ll> a(n);
        for (ll &x : a) {
                cin >> x;
        }
        sort(all(a));
        ll ans = 0;
        for (int i = 1, u, v; i < n; ++i) {
                ans += a[i] + a[0];
                cin >> u >> v;
        }
        cout << ans << endl;

        return 0;
}
