/* #pragma GCC optimize(2) */
#include <cstdio>
#include <cstdlib>
#include <fstream>
#include <iostream>
#include <istream>
#include <queue>
#include <sstream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        priority_queue<int, vector<int>, greater<int>> a, b;
        int x, y;
        ifstream fp("input.txt");
        string line;
        while (getline(fp, line)) {
                stringstream ss(line);
                ss >> x >> y;
                a.push(x), b.push(y);
        }
        ll ans = 0;
        while (!a.empty()) {
                ans += abs(a.top() - b.top());
                a.pop(), b.pop();
        }
        cout << ans << endl;

        return 0;
}
