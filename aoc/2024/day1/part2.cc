/* #pragma GCC optimize(2) */
#include <cstdio>
#include <cstdlib>
#include <fstream>
#include <iostream>
#include <istream>
#include <map>
#include <set>
#include <sstream>
#include <string>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        map<int, int> mp;
        set<int> st;
        int x, y;
        ifstream fp("input.txt");
        string line;
        while (getline(fp, line)) {
                stringstream ss(line);
                ss >> x >> y;
                st.insert(x);
                mp[y]++;
        }
        ll ans = 0;
        for (auto s : st) {
                ans += s * mp[s];
        }
        cout << ans << endl;

        return 0;
}
