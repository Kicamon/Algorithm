/* #pragma GCC optimize(2) */
#include <iostream>
#include <queue>
#include <array>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, k;
        cin >> n >> m >> k;
        vector<bool> vis(n + 1);
        vector<array<int, 2>> ans;
        for (int i = 0, x; i < m; ++i) {
                cin >> x;
                vis[x] = 1;
        }
        queue<int> p, q;
        for (int i = 1; i <= n; ++i) {
                if (vis[i]) {
                        p.push(i);
                } else {
                        q.push(i);
                }
        }
        if ((int)q.size() < k || m * (k + 1) < n) {
                cout << -1 << endl;
                return 0;
        }
        int t = p.front();
        p.pop();
        int st = t;
        for (int i = 0; i < k; ++i) {
                ans.push_back({ t, q.front() });
                t = q.front();
                q.pop();
        }
        while (!p.empty()) {
                ans.push_back({ t, p.front() });
                t = p.front();
                p.pop();
                if (!q.empty()) {
                        int len = min(k, (int)q.size());
                        for (int i = 1; i <= len; ++i) {
                                ans.push_back({ t, q.front() });
                                t = q.front();
                                q.pop();
                        }
                }
        }
        ans.push_back({ t, st });
        for (auto x : ans) {
                cout << x[0] << ' ' << x[1] << endl;
        }

        return 0;
}
