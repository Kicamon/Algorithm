#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;

void solve()
{
    int n;
    cin >> n;
    vector<int> num(n + 1);
    for (int i = 1; i <= n; ++i)
        for (int j = 1; i * j <= n; ++j)
            num[i * j]++;
    ll res = 0;
    for (int i = 1; i <= n; ++i)
        res += (ll)num[i] * num[n - i];
    cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
