#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
const int N = 2e5 + 10;
#define ai2 array<int, 2>

char s[N];

void solve()
{
    int n;
    cin >> n;
    cin >> s + 1;
    vector<ai2> f(n + 1);
    for (int i = 1; i <= n; ++i)
    {
        f[i] = f[i - 1];
        if (s[i] == 'R')
            f[i][0]++;
        else if (s[i] == 'L')
            f[i][0]--;
        else if (s[i] == 'U')
            f[i][1]++;
        else
            f[i][1]--;
    }
    sort(all(f), [&](ai2 a, ai2 b) {
        if (a[0] != b[0])
            return a[0] < b[0];
        return a[1] < b[1];
    });

    for (int i = 0; i < n; ++i)
        if (f[i] == f[i + 1])
        {
            cout << "Yes" << endl;
            return;
        }
    cout << "No" << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
