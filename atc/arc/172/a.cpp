/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int h, w, n;
    cin >> h >> w >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int k = 0; k <= 25; ++k) {
        ll cnt = 0;
        for (int i = 0; i < n; ++i) {
            if (a[i] >= k) {
                cnt += 1ll << (2 * (a[i] - k));
            }
        }
        ll have = (ll)(h >> k) * (w >> k);
        if (cnt > have) {
            cout << "No" << endl;
            return 0;
        }
    }
    cout << "Yes" << endl;

    return 0;
}
