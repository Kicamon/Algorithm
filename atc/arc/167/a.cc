// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<ll> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        sort(all(a));
        ll res = 0;
        int i = n - 1;
        for (; ~i; --i, --m) {
                if (i + 1 != m * 2) {
                        res += a[i] * a[i];
                } else {
                        break;
                }
        }
        int l = 0, r = i;
        while (l < r) {
                ll c = a[l] + a[r];
                res += c * c;
                l++, r--;
        }
        cout << res << endl;

        return 0;
}
