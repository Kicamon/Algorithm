// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve() {
        vint a(3);
        cin >> a[0] >> a[1] >> a[2];
        sort(all(a));
        int x = a[1] - a[0], y = a[2] - a[1];
        if (x > y)
                swap(x, y);
        if (x & 1 || y & 1)
                cout << -1 << endl;
        else {
                int res = 0;
                res += x / 2;
                y -= x;
                if (y % 6)
                        cout << -1 << endl;
                else
                        cout << res + y / 6 * 2 << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
