using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<array<int, 3>> a(n);
    vector<int> res(2);
    vector<array<int, 2>> dp;
    for (int i = 0; i < n; ++i) {
        cin >> a[i][0] >> a[i][1] >> a[i][2];
        if (a[i][0] > a[i][1]) {
            res[0] += a[i][2];
        } else {
            res[1] += a[i][2];
            dp.push_back({(a[i][1] + a[i][0] + 1) >> 1, a[i][2]});
        }
    }

    return 0;
}
