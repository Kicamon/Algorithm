// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        vector<array<int, 2>> dp(n);
        vector<int> pre(n + 1, -1);
        vector<int> ans(n);
        pre[a[0]] = 0;
        dp[0][1] = 1;
        for (int i = 1; i < n; ++i) {
                if (pre[a[i]] != -1) {
                        dp[i][0] = pre[a[i]];
                        dp[i][1] = dp[pre[a[i]]][1] + 1;
                        ans[i] = ans[dp[i][0]] + dp[dp[i][0]][1] * (i - dp[i][0] - 1);
                } else {
                        dp[i][1] = 1;
                }
                pre[a[i]] = i;
                /* cout << ans[i] << ' '; */
        }
        /* cout << endl; */
        int res = 0;
        for (int i = 0; i < n; ++i) {
                res += ans[i];
        }
        cout << res << endl;

        return 0;
}
