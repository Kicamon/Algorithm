/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int inf = 1e9;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> q(n), a(n), b(n);
        auto input = [&](vector<int> &x) {
                for (int i = 0; i < n; ++i) {
                        cin >> x[i];
                }
        };
        input(q), input(a), input(b);
        int max_a = inf, max_b = inf;
        for (int i = 0; i < n; ++i) {
                max_a = a[i] ? min(max_a, q[i] / a[i]) : max_a;
                max_b = b[i] ? min(max_b, q[i] / b[i]) : max_b;
        }
        int res = inf;
        for (int i = 0; i < n; ++i) {
                int cnt = 0;
                for (int x = 0; x <= max_a; ++x) {
                        cnt = max(cnt, x + (b[i] ? min(max_b, (q[i] - a[i] * x) / b[i]) : max_b));
                }
                res = min(res, cnt);
        }
        cout << res << endl;

        return 0;
}
