/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        vector<int> num(26);
        cin >> s;
        int len = 0;
        for (char c : s) {
                int idx = c - 'a';
                num[idx]++;
                if (num[idx] > len) {
                        len = num[idx];
                }
        }
        for (int i = 0; i < 26; ++i) {
                if (num[i] == len) {
                        cout << char(i + 'a') << endl;
                        break;
                }
        }

        return 0;
}
