// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 3>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
                a[i][2] = i + 1;
        }
        sort(a.begin(), a.end(), [&](array<int, 3> x, array<int, 3> y) {
                ll res = 1ll * y[0] * x[1];
                ll cnt = 1ll * x[0] * y[1];
                if (res != cnt) {
                        return res < cnt;
                }
                return x[2] < y[2];
        });
        for (int i = 0; i < n; ++i) {
                cout << a[i][2] << ' ';
        }
        cout << endl;

        return 0;
}
