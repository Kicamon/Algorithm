#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl;
const int N = 510;

int n, m;
map<char, char> c;
string s[N];
bool vis[N][N];
int mx[4]{ 0, -1, 0, 1 }, my[4]{ 1, 0, -1, 0 };

bool bfs() {
        if (s[0][0] != 's') {
                return false;
        }
        queue<array<int, 2>> q;
        q.push({ 0, 0 });
        vis[0][0] = true;
        while (!q.empty()) {
                auto [x, y] = q.front();
                q.pop();
                if (x == n - 1 && y == m - 1) {
                        return true;
                }
                for (int k = 0; k < 4; ++k) {
                        int X = x + mx[k], Y = y + my[k];
                        if (X < 0 || X >= n || Y < 0 || Y >= m) {
                                continue;
                        }
                        if (vis[X][Y] || s[X][Y] != c[s[x][y]]) {
                                continue;
                        }
                        vis[X][Y] = true;
                        q.push({ X, Y });
                }
        }
        return false;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> m;
        for (int i = 0; i < n; ++i) {
                cin >> s[i];
        }
        c['s'] = 'n', c['n'] = 'u', c['u'] = 'k', c['k'] = 'e', c['e'] = 's';
        cout << (bfs() ? "Yes" : "No") << endl;

        return 0;
}
