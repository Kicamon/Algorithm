// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> p(n);
        vector<array<int, 2>> d(m);
        for (int i = 0; i < n; ++i) {
                cin >> p[i];
        }
        for (int i = 0; i < m; ++i) {
                cin >> d[i][0];
        }
        for (int i = 0; i < m; ++i) {
                cin >> d[i][1];
        }
        sort(p.begin(), p.end());
        sort(d.begin(), d.end());
        ll res = 0;
        priority_queue<int, vector<int>, less<int>> q;
        for (int i = 0, j = 0; i < n; ++i) {
                while (j < m && p[i] >= d[j][0]) {
                        q.push(d[j++][1]);
                }
                res += p[i];
                if (!q.empty()) {
                        res -= q.top();
                        q.pop();
                }
        }
        cout << res << endl;

        return 0;
}
