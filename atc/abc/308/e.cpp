// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define int long long

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n + 1);
    vector<vector<int>> f(5, vector<int>(4));
    vector<int> p(5);
    string s;
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    cin >> s;
    s = ' ' + s;
    int res = 0;
    for (int i = 1; i <= n; ++i) {
        if (s[i] == 'M') {
            p[min(a[i], 4ll)]++;
        } else if (s[i] == 'E') {
            for (int j = 0; j < 4; ++j) {
                if (p[j]) {
                    f[j][min(a[i], 4ll)] += p[j];
                }
            }
        } else {
            for (int u = 0; u < 4; ++u) {
                for (int v = 0; v < 4; ++v) {
                    for (int j = 0; j < 4; ++j) {
                        if (j != u && j != v && j != a[i]) {
                            res += j * f[u][v];
                            break;
                        }
                    }
                }
            }
        }
        // cout << i << ' ' << res << endl;
    }
    cout << res << endl;

    return 0;
}
