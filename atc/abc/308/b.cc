#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) x.begin(), x.end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<string> c(n + 1);
        vector<int> p(m + 1);
        map<string, int> d;
        for (int i = 1; i <= n; ++i) {
                cin >> c[i];
        }
        for (int i = 1; i <= m; ++i) {
                string s;
                cin >> s;
                d[s] = i;
        }
        for (int i = 0; i <= m; ++i) {
                cin >> p[i];
        }
        int res = 0;
        for (int i = 1; i <= n; ++i) {
                res += p[d[c[i]]];
        }
        cout << res << endl;

        return 0;
}
