#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
#define ai2 array<ll, 2>
#define ing long long
const int mod = 998244353;

void solve()
{
    int n;
    cin >> n;
    vector<ai2> f(n + 1), a(n + 1);
    for (int i = 1; i <= n; ++i)
        cin >> a[i][0] >> a[i][1];
    f[0] = {1, 0};
    for (int i = 1; i <= n; ++i)
    {
        if (a[i][0] != a[i - 1][0])
            f[i][0] += f[i - 1][0];
        if (a[i][0] != a[i - 1][1])
            f[i][0] += f[i - 1][1];
        if (a[i][1] != a[i - 1][0])
            f[i][1] += f[i - 1][0];
        if (a[i][1] != a[i - 1][1])
            f[i][1] += f[i - 1][1];
        f[i][0] %= mod, f[i][1] %= mod;
    }
    cout << (f[n][0] + f[n][1]) % mod << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
