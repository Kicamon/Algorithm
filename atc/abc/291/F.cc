//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
const int inf = 0x3f3f3f3f;

void solve() {
        int n, m;
        cin >> n >> m;
        vint f(n + 1, inf), d(n + 1, inf);
        vector<string> a(n);
        f[0] = d[n - 1] = 0;
        for (int i = 0; i < n - 1; ++i) {
                cin >> a[i];
                for (int j = 0; j < m; ++j) {
                        if (a[i][j] == '1' && i + j + 1 < n)
                                f[i + j + 1] = min(f[i + j + 1], f[i] + 1);
                }
        }
        for (int i = n - 2; ~i; --i) {
                for (int j = 0; j < m; ++j) {
                        if (a[i][j] == '1' && i + j + 1 < n)
                                d[i] = min(d[i], d[i + j + 1] + 1);
                }
        }
        for (int i = 1; i < n - 1; ++i) {
                int res = inf;
                for (int j = max(0, i - m + 1); j < i; ++j) {
                        for (int k = i - j; k < m; ++k)
                                if (a[j][k] == '1')
                                        res = min(res, f[j] + 1 + d[j + k + 1]);
                }
                if (res == inf)
                        res = -1;
                cout << res << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
