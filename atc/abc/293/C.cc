// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;

int lowbit(int u) {
        return u & -u;
}

int check(int num) {
        int res = 0;
        while (num) {
                res++;
                num -= lowbit(num);
        }
        return res;
}

void solve() {
        int n, m;
        cin >> n >> m;
        vector<vint> w(n, vint(m));
        vint a(n * m);
        for (int i = 0, t = 0; i < n; ++i)
                for (int j = 0; j < m; ++j, ++t) {
                        cin >> w[i][j];
                        a[t] = w[i][j];
                }
        sort(all(a));
        a.erase(unique(all(a)), a.end());
        for (int i = 0; i < n; ++i)
                for (int j = 0; j < m; ++j)
                        w[i][j] = upper_bound(all(a), w[i][j]) - a.begin();
        int len = n + m - 2;
        int ans = 0;
        for (int i = 0; i < 1 << len; ++i) {
                if (check(i) != n - 1)
                        continue;
                int x = 0, y = 0;
                vector<bool> vis(n * m);
                vis[w[x][y]] = true;
                bool f = true;
                for (int j = 0; j < len; ++j) {
                        if (i >> j & 1)
                                x++;
                        else
                                y++;
                        if (vis[w[x][y]]) {
                                f = false;
                                break;
                        }
                        vis[w[x][y]] = true;
                }
                ans += f;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
