/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

int bit_len(int x) {
        int res = 0;
        while (x) {
                res++;
                x >>= 1;
        }
        return res;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; i++) {
                cin >> a[i];
        }
        sort(rall(a));
        int b_a = bit_len(a[0]);
        vector<int> b_n(b_a);

        function<void()> change = [&]() {
                for (int x : a) {
                        for (int i = 0; i < b_a; ++i) {
                                if (x & (1 << i)) {
                                        b_n[i]++;
                                }
                        }
                }
        };

        change();
        int res = 0;
        for (int i = b_a - 1; ~i; --i) {
                int temp = 1 << i;
                if (b_n[i] == 1) {
                        for (int &x : a) {
                                if (x & temp) {
                                        x -= temp;
                                }
                        }
                        sort(rall(a));
                } else if (b_n[i]) {
                        res += temp;
                        int j = 0;
                        for (; j < n; ++j) {
                                if (a[j] < res) {
                                        break;
                                }
                        }
                        a.erase(a.begin() + j, a.end());
                        n = a.size();
                }
                for (int j = 0; j < b_a; ++j) {
                        b_n[j] = 0;
                }
                change();
        }
        cout << (a[0] & a[1]) << endl;

        return 0;
}
