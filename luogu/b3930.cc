/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<ll> a(n);
        for (int i = 0; i < n; i++) {
                cin >> a[i];
        }
        vector<int> b_n(32);
        vector<ll> q = a;
        for (int i = 0; i < n; ++i) {
                int x = 0;
                while (a[i]) {
                        b_n[x++] += a[i] & 1;
                        a[i] >>= 1;
                }
        }
        for (int i = 0; i < 32; ++i) {
                if (b_n[i] != 1) {
                        continue;
                }
                ll t = 1 << i;
                for (ll &x : q) {
                        if (x & t) {
                                x -= t;
                        }
                }
        }
        sort(rall(q));
        cout << (q[0] & q[1]) << endl;

        return 0;
}
