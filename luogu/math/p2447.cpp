#include <bits/stdc++.h>
#include <bitset>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e3 + 10, M = 2 * N;

int n, m;
bitset<N> a[M];

int gauss() {
    int cnt = 0;
    for (int r = 1; r <= n; ++r) {
        int t = r;
        while (t <= m && !a[r].test(r)) {
            ++t;
        }
        if (t > m) {
            return 0;
        }
        cnt = max(cnt, t);
        if (t != r) {
            swap(a[r], a[t]);
        }
        for (int i = 1; i <= m; ++i) {
            if (i != r && a[i].test(r)) {
                a[i] ^= a[r];
            }
        }
    }
    return cnt;
}
signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> m;
    string s;
    for (int i = 0, res; i < m; ++i) {
        cin >> s >> res;
        for (int j = 0; j < n; ++j) {
            a[i].set(j, s[i] == '1');
        }
        a[i].set(m, res);
    }
    int ans = gauss();
    if (ans) {
        cout << ans << endl;
    } else {
        cout << "Cannot Determine" << endl;
    }

    return 0;
}
