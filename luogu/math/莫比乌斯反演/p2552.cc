#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int M = 5e4, N = M + 10;

int p[N], pr[N / 5], idx;
int mu[N], smu[N];

int f(int a, int b, int k) {
    a /= k, b /= k;
    if (a > b) {
        swap(a, b);
    }
    int ans = 0;
    for (int l = 1; l <= a; ++l) {
        int n = a / l, m = b / l;
        int r = min(a / n, b / m);
        ans += (smu[r] - smu[l - 1]) * n * m;
        l = r;
    }
    return ans;
}

void solve() {
    int a, b, c, d, k;
    cin >> a >> b >> c >> d >> k;
    cout << f(b, d, k) - f(a - 1, d, k) - f(b, c - 1, k) + f(a - 1, c - 1, k)
         << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    p[1] = mu[1] = 1;
    for (int i = 2; i <= M; ++i) {
        if (!p[i]) {
            p[i] = i, pr[++idx] = i, mu[i] = -1;
        }
        for (int j = 1; j <= idx && i * pr[j] <= M; ++j) {
            p[i * pr[j]] = pr[j];
            if (p[i] == pr[j]) {
                mu[i * pr[j]] = 0;
                break;
            } else {
                mu[i * pr[j]] = -mu[i];
            }
        }
    }
    for (int i = 1; i <= M; ++i) {
        smu[i] = smu[i - 1] + mu[i];
    }

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
