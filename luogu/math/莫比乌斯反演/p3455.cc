#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int M = 5e4, N = M + 10;

int p[N], pr[N / 5], idx;
int mu[N], smu[N];

void solve() {
    int n, m, k;
    cin >> n >> m >> k;
    n /= k, m /= k;
    if (n > m) {
        swap(n, m);
    }
    ll ans = 0;
    for (int l = 1; l <= n; ++l) {
        int x = n / l, y = m / l;
        int r = min(n / x, m / y);
        ans += (ll)(smu[r] - smu[l - 1]) * x * y;
        l = r;
    }
    cout << ans << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    p[1] = mu[1] = 1;
    for (int i = 2; i <= M; ++i) {
        if (!p[i]) {
            p[i] = i, mu[i] = -1, pr[++idx] = i;
        }
        for (int j = 1; j <= idx && i * pr[j] <= M; ++j) {
            p[i * pr[j]] = pr[j];
            if (p[i] == pr[j]) {
                mu[i * pr[j]] = 0;
                break;
            } else {
                mu[i * pr[j]] = -mu[i];
            }
        }
    }
    for (int i = 1; i <= M; ++i) {
        smu[i] = smu[i - 1] + mu[i];
    }

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
