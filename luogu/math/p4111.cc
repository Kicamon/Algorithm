// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 20, M = 100, mod = 1e9;
#define int long long

int n, m, idx, ans = 1;
char ch[N][N];
int num[N][N], a[M][M];

void add(int x, int y) {
    a[x][y]--, a[y][x]--;
    a[x][x]++, a[y][y]++;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> m;
    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            cin >> ch[i][j];
        }
    }
    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            if (ch[i][j] == '.') {
                num[i][j] = ++idx;
            }
        }
    }
    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            if (ch[i][j] != '.') {
                continue;
            }
            if (ch[i + 1][j] == '.') {
                add(num[i][j], num[i + 1][j]);
            }
            if (ch[i][j + 1] == '.') {
                add(num[i][j], num[i][j + 1]);
            }
        }
    }
    idx--;
    for (int i = 1; i < idx; ++i) {
        for (int j = i + 1; j <= idx; ++j) {
            while (a[j][i]) {
                int l = a[i][i] / a[j][i];
                for (int k = 1; k <= idx; ++k) {
                    a[i][k] = (a[i][k] - a[j][k] * l % mod + mod) % mod;
                }
                for (int k = 1; k <= idx; ++k) {
                    swap(a[i][k], a[j][k]);
                }
                ans *= -1;
            }
        }
    }
    for (int i = 1; i <= idx; ++i) {
        ans = (ans * a[i][i] % mod + mod) % mod;
    }
    cout << ans << endl;

    return 0;
}
