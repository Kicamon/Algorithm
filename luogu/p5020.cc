/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b;
        for (int &x : a) {
                cin >> x;
        }
        sort(all(a));
        int len = *max_element(all(a)), idx = 0;
        vector<int> dp(len + 1);
        dp[0] = 1;
        for (int i = 0; i < n; ++i) {
                for (int j = a[i]; j <= len; ++j) {
                        dp[j] += dp[j - a[i]];
                }
                if (dp[a[i]] == 1) {
                        idx++;
                }
        }
        cout << idx << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
