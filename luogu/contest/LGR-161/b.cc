// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, p;
        cin >> n >> p;
        if (n <= p * 2) {
                cout << -1 << endl;
                return;
        }
        string s = "1";
        for (int i = 0; i < p; ++i) {
                s += "01";
        }
        while (s.size() < n) {
                s += '0';
        }
        reverse(all(s));
        cout << s << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
