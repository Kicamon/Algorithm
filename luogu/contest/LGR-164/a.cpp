#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n + 1), b(n + 1), c(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    for (int i = 1; i <= n; ++i) {
        cin >> b[i];
    }
    for (int i = 1; i <= n; ++i) {
        cin >> c[i];
    }
    vector<int> dp(n + 1), dp2(n + 1);
    for (int i = 1; i <= n; ++i) {
        dp[i] = dp[i - 1] + b[i];
        dp2[i] = dp2[i - 1] + c[i];
        a[i] = min(a[i], a[i - 1] + b[i]);
    }
    int q;
    cin >> q;
    while (q--) {
        int m;
        cin >> m;
        vector<int> p(m);
        int res = a[n];
        for (int i = 0; i < m; ++i) {
            cin >> p[i];
            res += c[p[i]];
        }
        vector<int> dp3(m + 1);
        for (int i = 0; i < m; ++i) {
            int j = p[i];
            int cnt = min(a[j] + dp2[j], a[j - 1]);
            if (i) {
                cnt = min({cnt, dp[j] - dp[p[i - 1]] + c[j], dp[j - 1] - dp[p[i - 1]]});
            }
            dp3[i + 1] = dp3[i] + cnt;
        }
        cout << res << endl;
    }

    return 0;
}
