// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> a(n), b(n), c(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> c[i];
    }
    int q;
    cin >> q;
    while (q--) {
        int m;
        cin >> m;
        vector<int> p(m);
        for (int i = 0; i < m; ++i) {
            cin >> p[i];
            p[i]--;
        }
        int res = 0;
        for (int i = 0; i < n; ++i) {
            res += b[i];
        }
        for (int i = 0; i < m; ++i) {
            res -= b[p[i]];
        }
        int cnt = a[n - 1];
        for (int i = 0; i < m; ++i) {
            cnt += c[p[i]];
        }
        cout << min(res, cnt) << endl;
    }

    return 0;
}
