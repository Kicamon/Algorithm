#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 2e5 + 10;

vector<int> e[N];

void solve() {
        int n;
        cin >> n;
        for (int i = 1; i <= n; ++i) {
                e[i].clear();
        }
        int u, v;
        for (int i = 1; i < n; ++i) {
                cin >> u >> v;
                e[u].push_back(v);
                e[v].push_back(u);
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
