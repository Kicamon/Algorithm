// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, a;
        cin >> n >> a;
        a = n - a;
        vector<int> b(n + 1);
        int ans = 0;
        for (int i = 1; i <= n; ++i) {
                cin >> b[i];
                ans += b[i] * 3;
        }
        vector<int> res(n + 1);
        for (int i = 1; i <= n; ++i) {
                int l = i % n + 1, r = (i + n - 2) % n + 1;
                res[i] = (b[i] == 0) + (b[l] == 0) + (b[r] == 0);
        }
        sort(res.rbegin(), res.rend());
        for (int i = 0; i < a; ++i) {
                ans += res[i];
        }
        cout << ans << endl;

        return 0;
}
