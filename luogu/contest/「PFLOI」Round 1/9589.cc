#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int gcd(int a, int b) {
        return b ? gcd(b, a % b) : a;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> a(n), b(m);
        int gcda = 0;
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
                gcda = gcd(gcda, a[i]);
        }
        for (int i = 0; i < m; ++i) {
                cin >> b[i];
        }
        sort(all(a)), sort(all(b));

        return 0;
}
