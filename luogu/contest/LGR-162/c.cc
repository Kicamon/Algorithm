#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<int> t(n), b(n);
    for (int i = 0; i < n; ++i) {
        cin >> t[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }
    if (*max_element(all(t)) == *min_element(all(t))) {
        if (*max_element(all(b)) != *min_element(all(b)))
            cout << "No" << endl;
        else
            cout << "Yes" << endl;
    } else {
        for (int i = 0; i < n / 2 + 1; ++i) {
            if (t[i] != t[n - i - 1]) {
                cout << "No" << endl;
                return;
            }
        }
        cout << "Yes" << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
