/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<ll> a(n);
        for (ll &x : a) {
                cin >> x;
        }
        for (int i = 1; i < n; ++i) {
                int idx = 0;
                ll t = 0;
                for (int j = 0; j < i; ++j) {
                        if (abs(a[i] - a[j]) < t) {
                                t = abs(a[i] - a[j]);
                        } else if (abs(a[i] - a[j]) == t && a[j] < a[idx]) {
                                idx = j;
                        }
                }
                cout << abs(a[i] - a[idx]) << ' ' << idx + 1 << endl;
        }

        return 0;
}
