/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 1000000007;
const int N = 1005, M = 205;
ll dp[2][M][M][2];

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, p;
        cin >> n >> m >> p;
        string a, b;
        cin >> a >> b;
        a = ' ' + a, b = ' ' + b;
        dp[0][0][0][0] = dp[1][0][0][0] = 1;
        int val = 0;
        for (int i = 1; i <= n; ++i) {
                val ^= 1;
                for (int j = 1; j <= m; ++j) {
                        for (int k = 1; k <= p; ++k) {
                                if (a[i] == b[j]) {
                                        dp[val][j][k][0] =
                                            (dp[val ^ 1][j][k][0] + dp[val ^ 1][j][k][1]) % mod;
                                        dp[val][j][k][1] = (dp[val ^ 1][j - 1][k][1] +
                                                            dp[val ^ 1][j - 1][k - 1][1] +
                                                            dp[val ^ 1][j - 1][k - 1][0]) %
                                                           mod;
                                } else {
                                        dp[val][j][k][0] =
                                            (dp[val ^ 1][j][k][0] + dp[val ^ 1][j][k][1]) % mod;
                                        dp[val][j][k][1] = 0;
                                }
                        }
                }
        }
        cout << (dp[val][m][p][0] + dp[val][m][p][1]) % mod << endl;

        return 0;
}
