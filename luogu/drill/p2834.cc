/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mode = 1e9 + 7;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, m;
        cin >> n >> m;
        vector<ll> dp(m + 1);
        dp[0] = 1;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                for (int j = 0; j <= m; ++j) {
                        if ((ll)j + x > m) {
                                break;
                        }
                        (dp[j + x] += dp[j]) %= mode;
                }
        }
        cout << dp[m] << endl;

        return 0;
}
