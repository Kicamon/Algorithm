/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <algorithm>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, lul_lenth = 0;
        cin >> n;
        vector<int> a(n);
        vector<vector<int>> lul(n + 1);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                a[i] = x - i - 1;
        }

        function<int(int)> bs = [&](int i) {
                int l = 0, r = lul_lenth;
                while (l < r) {
                        int mid = (l + r + 1) >> 1;
                        if (a[lul[mid].back()] > a[i]) {
                                r = mid - 1;
                        } else {
                                l = mid;
                        }
                }
                return l;
        };

        for (int i = 0; i < n; ++i) {
                int idx = bs(i);
                lul[idx + 1] = lul[idx];
                lul[idx + 1].push_back(i);
                lul_lenth = max(lul_lenth, idx + 1);
        }
        cout << n - lul_lenth << endl;

        int ans = 0, idx = 0;
        for (int i = 0; i < n; ++i) {
                if (i == lul[lul_lenth][idx]) {
                        idx++;
                        continue;
                }
                ans += abs(a[i] - a[lul[lul_lenth][idx]]);
        }
        cout << ans << endl;

        return 0;
}
