#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
using ll = long long;

vector<int> mul(vector<int> a, int b) {
        vector<int> ans;
        int t = 0;
        for (int i = 0; i < a.size() || t; i++) {
                if (i < a.size()) {
                        t += a[i] * b;
                }
                ans.push_back(t % 10);
                t /= 10;
        }
        return ans;
}

vector<int> div(vector<int> a, int b) {
        vector<int> res;
        int t = 0;
        for (int i = 0; i < (int)a.size(); i++) {
                t = t * 10 + a[i];
                res.push_back(t / b);
                t = t % b;
        }
        reverse(res.begin(), res.end());
        while (res.size() > 1 && res.back() == 0) {
                res.pop_back();
        }
        reverse(res.begin(), res.end());
        return res;
}

void solve() {
        int k, p;
        cin >> k >> p;
        string s;
        cin >> s;
        vector<int> num;
        for (char c : s) {
                num.push_back(c - '0');
        }
        if (!p) {
                reverse(num.begin(), num.end());
                mul(num, 2);
                num.back() += 3;
                reverse(num.begin(), num.end());
        }
        int ans = 0;
        while (num.size() > 10) {
                ans += 18;
                num = div(num, 1162261467);
        }
        ll nnum = 0;
        for (int x : num) {
                nnum = nnum * 10 + x;
        }
        while (nnum) {
                ans++;
                nnum /= 3;
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
