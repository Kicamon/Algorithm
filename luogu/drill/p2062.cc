/* #pragma GCC optimize(2) */
#include <iostream>
#include <map>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        map<int, int> mp;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                mp[x]++;
        }
        int t = 0;
        int ans = 0;
        for (auto [a, b] : mp) {
                b += t;
                ans += b / a;
                t = b % a;
        }
        cout << ans << endl;

        return 0;
}
