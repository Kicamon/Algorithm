/* #pragma GCC optimize(2) */
#include <algorithm>
#include <bitset>
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int l, r;
        cin >> l >> r;
        int n = __lg(r) + 1;
        vector<ll> m(n + 1);
        m[0] = 1;
        for (int i = 1; i <= n; ++i) {
                m[i] = m[i - 1] * i;
                cout << m[i] << endl;
        }

        return 0;
}
