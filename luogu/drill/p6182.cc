/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n;
        cin >> n;
        char opt;
        int idx = 0;
        vector<array<int, 2>> a(n + 1);
        a[0] = { -1, 0 };
        for (int i = 1, x; i <= n; ++i) {
                cin >> opt;
                if (opt == 'a') {
                        cin >> x;
                        a[i] = { x, idx };
                        idx = i;
                } else if (opt == 's') {
                        idx = a[i - 1][1];
                        a[i] = a[idx];
                } else {
                        cin >> x;
                        idx = x - 1;
                        a[i] = a[idx];
                }
                cout << a[idx][0] << '\n';
        }

        return 0;
}
