/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n), b(n), num(n);
        for (int &x : a) {
                cin >> x;
                b[--x]++;
        }

        queue<int> q;
        for (int i = 0; i < n; ++i) {
                if (!b[i]) {
                        q.push(i);
                }
        }
        while (!q.empty()) {
                int t = q.front();
                q.pop();
                if (--b[a[t]] == 0) {
                        q.push(a[t]);
                }
        }

        function<int(int, int)> dfs = [&](int u, int val) {
                if (num[u]) {
                        if (val < 0) {
                                return num[u] + 1;
                        }
                        num[u] = val;
                        return val;
                }
                num[u] = dfs(a[u], val + 1);
                return num[u] + (val < 0);
        };

        for (int i = 0; i < n; ++i) {
                if (b[i] && !num[i]) {
                        num[i] = 1;
                        dfs(a[i], 1);
                }
        }
        for (int i = 0; i < n; ++i) {
                dfs(i, -n);
        }
        for (int x : num) {
                cout << x << endl;
        }

        return 0;
}
