/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<string> s(n);
        for (int i = 0; i < n; ++i) {
                cin >> s[i];
        }

        function<void(int u)> dfs = [&](int u) {
                cout << s[u][0];
                for (int i = 1; i <= 2; ++i) {
                        for (int j = 1; j < n; ++j) {
                                if (s[j][0] == s[u][i]) {
                                        dfs(j);
                                }
                        }
                }
        };

        dfs(0);
        cout << endl;

        return 0;
}
