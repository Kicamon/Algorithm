#define all(x) (x).begin(), (x).end()

void get(string a, string b) {
        if (!a.size()) {
                return;
        }
        int n = (int)b.size();
        cout << b[n - 1];
        int k = a.find(b[n - 1]);
        get(a.substr(0, k), b.substr(0, k));
        get(a.substr(k + 1), b.substr(k, (int)a.size() - k - 1));
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string a, b;
        cin >> a >> b;
        get(a, b);
        cout << endl;

        return 0;
}
