/* #pragma GCC optimize(2) */
#include <iostream>
#include <array>
#include <vector>
#include <queue>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e5 + 10, M = 2e5 + 10, inf = (1ll << 31) - 1;
int n, m, s;
array<int, N> val;
array<bool, N> vis;
vector<vector<array<int, 2> > > g(N);

void dijkstra() {
        priority_queue<array<int, 2>, vector<array<int, 2> >, greater<array<int, 2> > > q;
        q.push({ 0, s });
        val[s] = 0;
        while (!q.empty()) {
                auto t = q.top();
                q.pop();
                if (vis[t[1]]) {
                        continue;
                }
                vis[t[1]] = true;
                for (auto x : g[t[1]]) {
                        if (val[t[1]] + x[1] < val[x[0]]) {
                                val[x[0]] = val[t[1]] + x[1];
                                q.push({ val[x[0]], x[0] });
                        }
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        fill(all(val), inf);
        cin >> n >> m >> s;
        s--;
        int u, v, w;
        while (m--) {
                cin >> u >> v >> w;
                u--, v--;
                g[u].push_back({ v, w });
        }
        dijkstra();
        for (int i = 0; i < n; ++i) {
                cout << val[i] << " \n"[i == n - 1];
        }

        return 0;
}
