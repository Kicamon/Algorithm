/* #pragma GCC optimize(2) */
#include <iostream>
#include <queue>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 100003;
int n, m;

vector<vector<int> > g;

void dijkstra() {
        vector<int> dis(n, n), cnt(n);
        vector<bool> vis(n);
        priority_queue<array<int, 2>, vector<array<int, 2> >, greater<array<int, 2> > > q;
        q.push({ 0, 0 });
        dis[0] = 0, cnt[0] = 1;
        while (!q.empty()) {
                int t = q.top()[1];
                q.pop();
                if (vis[t]) {
                        continue;
                }
                vis[t] = true;
                for (int x : g[t]) {
                        if (x == t) {
                                continue;
                        }
                        if (dis[t] + 1 < dis[x]) {
                                dis[x] = dis[t] + 1;
                                cnt[x] = cnt[t];
                                q.push({ dis[x], x });
                        } else if (dis[t] + 1 == dis[x]) {
                                (cnt[x] += cnt[t]) %= mod;
                        }
                }
        }
        for (int x : cnt) {
                cout << x << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> m;
        g.resize(n);
        int u, v;
        while (m--) {
                cin >> u >> v;
                u--, v--;
                g[u].push_back(v), g[v].push_back(u);
        }
        dijkstra();

        return 0;
}
