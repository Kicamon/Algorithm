/* #pragma GCC optimize(2) */
#include <iostream>
#include <array>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e4 + 10, M = 5e5 + 10, inf = (1ll << 31) - 1;
int n, m, s;
array<int, N> val;
vector<vector<array<int, 2> > > g(N);

void dijkstra() {
        queue<int> q;
        q.push(s);
        val[s] = 0;
        while (!q.empty()) {
                int t = q.front();
                q.pop();
                for (auto x : g[t]) {
                        if (val[t] + x[1] < val[x[0]]) {
                                q.push(x[0]);
                                val[x[0]] = val[t] + x[1];
                        }
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        fill(all(val), inf);
        cin >> n >> m >> s;
        s--;
        int u, v, w;
        while (m--) {
                cin >> u >> v >> w;
                u--, v--;
                g[u].push_back({ v, w });
        }
        dijkstra();
        for (int i = 0; i < n; ++i) {
                cout << val[i] << " \n"[i == n - 1];
        }

        return 0;
}
