#include <iostream>
#include <queue>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<vector<array<int, 2> > > g;

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        g.resize(n + 1);
        vector<int> rd(n + 1);
        int u, v, w;
        while (m--) {
                cin >> u >> v >> w;
                if (u == v) {
                        continue;
                }
                g[u].push_back({ v, w });
                rd[v]++;
        }
        int idx = 1;
        for (int i = 1; i <= n; ++i) {
                if (rd[i] < rd[idx]) {
                        idx = i;
                }
                if (!rd[i]) {
                        g[0].push_back({ i, 0 });
                }
        }
        if (idx) {
                for (int i = 1; i <= n; ++i) {
                        if (rd[i] == rd[idx]) {
                                g[0].push_back({ i, 0 });
                        }
                }
        } else {
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
