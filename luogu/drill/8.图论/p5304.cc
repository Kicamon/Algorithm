/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <queue>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define pli pair<ll, int>
const ll inf = 5e18;

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        vector<vector<array<int, 2> > > g(n + 1);
        vector<int> rd(n + 1), nk(k);
        vector<ll> dis(n + 1, inf);

        int u, v, w;
        while (m--) {
                cin >> u >> v >> w;
                if (u == v) {
                        continue;
                }
                g[u].push_back({ v, w });
                rd[v]++;
        }
        for (int &x : nk) {
                cin >> x;
        }
        int idx = 1;
        for (int i = 1; i <= n; ++i) {
                if (rd[i] < rd[idx]) {
                        idx = i;
                }
        }
        for (int i = 1; i <= n; ++i) {
                if (rd[i] == rd[idx]) {
                        g[0].push_back({ i, 0 });
                        if (rd[idx]) {
                                break;
                        }
                }
        }

        // dijkstra
        priority_queue<pli, vector<pli>, greater<pli> > q;
        vector<bool> vis(n + 1);
        q.push({ 0, 0 });
        dis[0] = 0;
        while (!q.empty()) {
                int u = q.top().second;
                q.pop();
                if (vis[u]) {
                        continue;
                }
                vis[u] = true;
                for (auto [v, w] : g[u]) {
                        if (dis[u] + w < dis[v]) {
                                dis[v] = dis[u] + w;
                                q.push({ dis[v], v });
                        }
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
