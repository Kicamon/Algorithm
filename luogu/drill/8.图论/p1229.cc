/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string a, b;
        cin >> a >> b;
        int ans = 1;
        for (int i = 1; i < (int)a.size(); ++i) {
                int idx = b.find(a[i - 1]);
                if (idx && b[idx - 1] == a[i]) {
                        ans <<= 1;
                }
        }
        cout << ans << endl;

        return 0;
}
