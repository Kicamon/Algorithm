/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = (4 << 10) + 5;

int n;
string s;
int node[N];

void build(int u, int l, int r) {
        if (l == r) {
                node[u] = int(s[l] - '0');
                if (node[u] == 0) {
                        cout << 'B';
                } else {
                        cout << 'I';
                }
                return;
        }
        int mid = l + r >> 1;
        build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
        if (node[u << 1] != node[u << 1 | 1] || node[u << 1] == 2) {
                node[u] = 2;
        } else {
                node[u] = node[u << 1];
        }
        if (node[u] == 0) {
                cout << 'B';
        } else if (node[u] == 1) {
                cout << 'I';
        } else {
                cout << 'F';
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> s;
        s = " " + s;
        build(1, 1, 1 << n);
        cout << endl;

        return 0;
}
