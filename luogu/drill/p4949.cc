/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, m;
        cin >> n >> m;
        vector<vector<array<int, 2>>> g(n + 1);
        vector<int> du(n + 1);
        int root = 0;
        for (int i = 0, u, v, w; i < n; ++i) {
                cin >> u >> v >> w;
                g[u].push_back({ v, w });
                g[v].push_back({ u, w });
                du[u]++, du[v]++;
                if (du[u] == 3) {
                        root = u;
                } else if (du[v] == 3) {
                        root = v;
                }
        }

        return 0;
}
