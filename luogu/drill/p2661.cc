/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n), b(n);
        vector<bool> vis(n);
        for (int &x : a) {
                cin >> x;
                x--;
                b[x]++;
        }

        function<int(int)> dfs = [&](int x) {
                if (vis[x]) {
                        return 1;
                }
                vis[x] = true;
                return dfs(a[x]) + 1;
        };

        if (*min_element(all(b))) {
                cout << n - 1 << endl;
        } else {
                queue<int> q;
                for (int i = 0; i < n; ++i) {
                        if (!b[i]) {
                                q.push(i);
                                vis[i] = true;
                        }
                }
                while (!q.empty()) {
                        auto t = q.front();
                        q.pop();
                        if (--b[a[t]] == 0) {
                                q.push(a[t]);
                                vis[a[t]] = true;
                        }
                }
                int ans = n + 1;
                for (int i = 0; i < n; ++i) {
                        if (vis[i]) {
                                continue;
                        }
                        vis[i] = true;
                        ans = min(ans, dfs(a[i]));
                }
                cout << ans << endl;
        }

        return 0;
}
