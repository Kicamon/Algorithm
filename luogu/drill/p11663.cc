#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int n, m;
struct Node {
        int idx;
        ll sum;
};

vector<Node> node;
vector<int> a, b;

bool check(int x, int y) {
        return a[x] - b[x] > a[y] - b[y];
}

void push_up(int u) {
        Node &l = node[u << 1], &r = node[u << 1 | 1];
        node[u].idx = check(l.idx, r.idx) ? l.idx : r.idx;
        node[u].sum = l.sum + r.sum;
}

void build(int u, int l, int r) {
        if (l == r) {
                node[u] = { l, b[l] };
                return;
        }
        int mid = (l + r) >> 1;
        build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
        push_up(u);
}

Node search(int u, int l, int r, int L, int R) {
        if (L <= l && r <= R) {
                return node[u];
        }
        int mid = (l + r) >> 1;
        auto t = search(u << 1, l, mid, L, R);
        auto p = search(u << 1 | 1, mid + 1, r, L, R);
        return check(t.idx, p.idx) ? t : p;
}

