// #pragma GCC optimize(2)
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 3e6 + 10;

vector<vector<int> > a(N);

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, al = 0;
        cin >> n;
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                a[x].push_back(y);
                al = max(al, y);
        }
        vector<int> dp(al + 1);
        for (int i = 1; i <= al; ++i) {
                dp[i] = max(dp[i], dp[i - 1]);
                if (!a[i].size()) {
                        continue;
                }
                for (int x : a[i]) {
                        dp[x] = max(dp[x], dp[i - 1] + x - i + 1);
                }
        }
        cout << dp[al] << endl;

        return 0;
}
