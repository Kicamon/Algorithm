// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, d;
        cin >> n >> d;
        vector<int> v(n);
        for (int i = 1; i < n; ++i) {
                cin >> v[i];
        }
        vector<int> a(n + 1);
        vector<array<int, 2>> e;
        int idx = 1;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                if (a[i] < a[idx]) {
                        int len = 0;
                        for (int j = idx; j < i; ++j) {
                                len += v[j];
                        }
                        e.push_back({ len, idx });
                        idx = i;
                }
        }
        if (idx != n) {
                int len = 0;
                for (int i = idx; i < n; ++i) {
                        len += v[i];
                }
                e.push_back({ len, idx });
        }
        int res = 0, cnt = 0;
        for (auto x : e) {
                if (cnt < x[0]) {
                        int c = (x[0] - cnt + d - 1) / d;
                        cnt += c * d;
                        res += c * a[x[1]];
                }
                cnt -= x[0];
        }
        cout << res << endl;

        return 0;
}
