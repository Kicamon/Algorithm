#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e3 + 10, M = N << 1;

int n, m;
bitset<N> a[M];

int gauss() {
        int cnt = -1;
        for (int i = 0; i < n; ++i) {
                int t = i;
                while (t < m && !a[t].test(i)) {
                        t++;
                }
                if (t >= m) {
                        return 0;
                }
                cnt = max(cnt, t);
                if (t != i) {
                        swap(a[i], a[t]);
                }
                for (int j = 0; j < m; ++j) {
                        if (j != i && a[j].test(i)) {
                                a[j] ^= a[i];
                        }
                }
        }
        return cnt + 1;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> m;
        string s;
        for (int i = 0, res; i < m; ++i) {
                cin >> s >> res;
                for (int j = 0; j < n; ++j) {
                        a[i].set(j, s[j] == '1');
                }
                a[i].set(n, res);
        }
        int res = gauss();
        if (res) {
                cout << res << endl;
                for (int i = 0; i < n; ++i) {
                        cout << (a[i].test(n) ? "?y7M#" : "Earth") << endl;
                }
        } else {
                cout << "Cannot Determine" << endl;
        }

        return 0;
}
