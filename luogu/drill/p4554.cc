/* #pragma GCC optimize(2) */
#include <iostream>
#include <queue>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int mx[] = { 0, 1, 0, -1 };
int my[] = { 1, 0, -1, 0 };

void solve(int n, int m) {
        string s;
        vector<vector<int>> g(n, vector<int>(m)), dis(n, vector<int>(m, n * m + 1));
        for (int i = 0; i < n; ++i) {
                cin >> s;
                for (int j = 0; j < m; ++j) {
                        g[i][j] = s[j] == '@';
                }
        }
        int x1, y1, x2, y2;
        cin >> x1 >> y1 >> x2 >> y2;
        dis[x1][y1] = 0;
        priority_queue<array<int, 3>, vector<array<int, 3>>, greater<array<int, 3>>> q;
        q.push({ dis[x1][y1], x1, y1 });
        while (!q.empty()) {
                auto [_, x, y] = q.top();
                q.pop();
                int X, Y;
                for (int i = 0; i < 4; ++i) {
                        X = x + mx[i], Y = y + my[i];
                        if (X < 0 || X >= n || Y < 0 || Y >= m) {
                                continue;
                        }
                        if (dis[x][y] + (g[x][y] ^ g[X][Y]) < dis[X][Y]) {
                                dis[X][Y] = dis[x][y] + (g[x][y] ^ g[X][Y]);
                                q.push({ dis[X][Y], X, Y });
                        }
                }
        }
        cout << dis[x2][y2] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, m;
        while (cin >> n >> m && (n || m)) {
                solve(n, m);
        }

        return 0;
}
