/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int n, k;
        cin >> n >> k;
        vector<int> fa(n + 1), dis(n + 1);
        for (int i = 0; i <= n; ++i) {
                fa[i] = i;
        }

        function<int(int)> find = [&](int x) {
                if (fa[x] == x) {
                        return x;
                }
                int y = find(fa[x]);
                dis[x] += dis[fa[x]];
                return fa[x] = y;
        };

        int opt, a, b;
        int ans = 0;
        while (k--) {
                cin >> opt >> a >> b;
                if (a > n || b > n) {
                        ans++;
                        continue;
                }
                if (opt == 2 && a == b) {
                        ans++;
                        continue;
                }
                int x = find(a), y = find(b);
                if (x == y) {
                        int t = ((dis[a] - dis[b]) % 3 + 3) % 3;
                        if (t != opt - 1) {
                                ans++;
                        }
                } else {
                        fa[x] = y;
                        dis[x] = dis[b] - dis[a] + opt - 1;
                }
        }
        cout << ans << endl;

        return 0;
}
