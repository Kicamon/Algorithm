/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 1000000007;
const int N = 1005, M = 205;
int dp[N][M][M];

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, p;
        cin >> n >> m >> p;
        string a, b;
        cin >> a >> b;
        a = ' ' + a, b = ' ' + b;
        dp[1][1][0] = 1;
        for (int i = 1; i <= n; ++i) {
                for (int j = 1; j <= m; ++j) {
                        for (int k = 0; k <= p; ++k) {
                                if (i < n) {
                                        (dp[i + 1][j][k] += dp[i][j][k]) %= mod;
                                }
                                for (int t = 1; i + t <= n && j + t <= m; ++t) {
                                        if (a[i + t] == b[j + t]) {
                                                (dp[i + t][j + t][k + 1] += dp[i][j][k]) %= mod;
                                        } else {
                                                break;
                                        }
                                }
                        }
                }
        }
        cout << dp[n][m][p] << endl;

        return 0;
}
