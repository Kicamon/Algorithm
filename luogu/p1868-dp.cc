#include <algorithm>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 3e6 + 10;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 2> > a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
        }
        sort(all(a), [&](auto x, auto y) {
                if (x[1] != y[1]) {
                        return x[1] < y[1];
                }
                return x[0] < y[0];
        });

        return 0;
}
