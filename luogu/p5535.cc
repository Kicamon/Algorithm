/* #pragma GCC optimize(2) */
#include <cmath>
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll n, k;
        cin >> n >> k;
        n++, k++;
        int x = sqrt((double)k) + 1;
        bool npn = false;
        for (int i = 2; i < x; ++i) {
                if (k % i == 0) {
                        npn = true;
                        break;
                }
        }
        if (npn) {
                cout << 2 << endl;
        } else {
                cout << ((n >= k * 2) ? 2 : 1) << endl;
        }

        return 0;
}
