/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll n, m, p;
        cin >> n >> m >> p;
        vector<array<int, 2>> a(n);
        vector<int> b(n);
        ll sum = 0;
        for (int i = 0; i < n; ++i) {
                cin >> b[i];
                a[i][0] = b[i] % p;
                a[i][1] = a[i][0];
                sum += a[i][0];
        }
        if (sum > m) {
                cout << 0 << endl;
                return 0;
        }
        sum <<= 1;
        if (sum > m) {
                cout << 1 << endl;
                return 0;
        }
        int i = 2;
        for (;; ++i) {
                bool flag = 1;
                for (int j = 0; j < n; ++j) {
                        ll temp = (a[j][0] + a[j][1]) % p;
                        if (temp != 0 || a[j][0] != b[j]) {
                                flag = false;
                        }
                        a[j][0] = a[j][1], a[j][1] = temp;
                        sum += temp;
                }
                if (sum > m) {
                        break;
                }
                if (flag) {
                        int idx = m / sum;
                        // cout << idx << ' ' << sum << endl;
                        sum *= idx;
                        i *= idx;
                        // cout << i + 1 << ' ' << sum << endl;
                }
        }
        cout << i << endl;
        return 0;
}
