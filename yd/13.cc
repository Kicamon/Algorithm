/* #pragma GCC optimize(2) */
#include <cstdio>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int mx[4] = { 0, 1, 0, -1 };
int my[4] = { 1, 0, -1, 0 };

signed main() {
        int n;
        cin >> n;
        vector<vector<int> > m(n, vector<int>(n));
        m[0][0] = 1;
        for (int i = 2, x = 0, y = 0, idx = 0; i <= n * n; ++i) {
                int X = x + mx[idx], Y = y + my[idx];
                while (X < 0 || X >= n || Y < 0 || Y >= n || m[X][Y]) {
                        idx = (idx + 1) % 4;
                        X = x + mx[idx], Y = y + my[idx];
                }
                m[X][Y] = i;
                x = X, y = Y;
        }
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < n; ++j) {
                        printf("%3d", m[i][j]);
                }
                printf("\n");
        }

        return 0;
}
