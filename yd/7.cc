/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        cin >> s;
        string ss[] = { "boy", "girl" };
        vector<int> ans(2);
        for (int i = 0, n = (int)s.size(); i < n; ++i) {
                if (s[i] == '.') {
                        continue;
                }
                bool fb = false, fg = false;
                for (int j = 0; j < 3; ++j) {
                        if (i < n && ss[0][j] == s[i]) {
                                fb = true;
                                i++;
                        }
                }
                for (int j = 0; j < 4; ++j) {
                        if (i < n && ss[1][j] == s[i]) {
                                fg = true;
                                i++;
                        }
                }
                ans[0] += fb, ans[1] += fg;
                if (fb || fg) {
                        i--;
                }
        }
        cout << ans[0] << endl;
        cout << ans[1] << endl;

        return 0;
}
