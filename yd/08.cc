/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        string s;
        cin >> s;
        if (n == 1) {
                cout << 0 << endl;
        } else {
                int ans = 0;
                for (int i = 1; i < n; ++i) {
                        ans += s[i - 1] == 'V' && s[i] == 'K';
                }
                for (int i = 0; i < n; ++i) {
                        int temp = 0;
                        s[i] = s[i] == 'V' ? 'K' : 'V';
                        for (int j = 1; j < n; ++j) {
                                temp += s[j - 1] == 'V' && s[j] == 'K';
                        }
                        s[i] = s[i] == 'V' ? 'K' : 'V';
                        ans = max(ans, temp);
                }
                cout << ans << endl;
        }

        return 0;
}
