/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int mg[][2]
    = { { -2, -1 }, { -2, 1 }, { -1, 2 }, { 1, 2 }, { 2, 1 }, { 2, -1 }, { 1, -2 }, { -1, -2 } };

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, x, y;
        cin >> n >> m >> x >> y;
        vector<vector<ll>> g(n + 1, vector<ll>(m + 1));
        vector<vector<ll>> vis(n + 1, vector<ll>(m + 1));
        vis[x][y] = true;
        g[0][0] = 1;
        for (int i = 0; i < 8; ++i) {
                int X = x + mg[i][0], Y = y + mg[i][1];
                if (X < 0 || X > n || Y < 0 || Y > n) {
                        continue;
                }
                vis[X][Y] = true;
        }
        for (int i = 0; i <= n; ++i) {
                for (int j = 0; j <= m; ++j) {
                        if (vis[i][j]) {
                                continue;
                        }
                        if (i) {
                                g[i][j] += g[i - 1][j];
                        }
                        if (j) {
                                g[i][j] += g[i][j - 1];
                        }
                }
        }
        cout << g[n][m] << endl;

        return 0;
}
