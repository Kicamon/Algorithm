/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<array<int, 2> > a(m);
        for (auto &x : a) {
                cin >> x[0] >> x[1];
        }
        sort(all(a));
        int ans = 0;
        for (auto x : a) {
                if (n >= x[1]) {
                        ans += x[0] * x[1];
                        n -= x[1];
                } else {
                        ans += x[0] * n;
                        break;
                }
        }
        cout << ans << endl;

        return 0;
}
