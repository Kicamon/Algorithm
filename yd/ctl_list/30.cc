/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<vector<char>> g(n + 1, vector<char>(m + 1));
        vector<vector<int>> c(3, vector<int>(n + 1));
        for (int i = 1; i <= n; ++i) {
                for (int j = 1; j <= m; ++j) {
                        cin >> g[i][j];
                        if (g[i][j] == 'W') {
                                c[0][i]++;
                        } else if (g[i][j] == 'B') {
                                c[1][i]++;
                        } else {
                                c[2][i]++;
                        }
                }
                for (int j = 0; j < 3; ++j) {
                        c[j][i] = c[j][i - 1] + m - c[j][i];
                }
        }
        int ans = n * m;
        for (int i = 1; i < n - 1; ++i) {
                for (int j = i + 1; j < n; ++j) {
                        ans = min(ans, c[0][i] + c[1][j] - c[1][i] + c[2][n] - c[2][j]);
                }
        }
        cout << ans << endl;

        return 0;
}
