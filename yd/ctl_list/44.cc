/* #pragma GCC optimize(2) */
#include <algorithm>
#include <cmath>
#include <functional>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const double inf = 1e-6;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, p;
        cin >> n >> p;
        double use = 0, have = 0;
        vector<array<double, 2>> a(n);
        for (auto &x : a) {
                cin >> x[0] >> x[1];
                use += x[0];
                have += x[1];
        }

        function<bool(double)> check = [&](double u) {
                double temp = 0;
                for (auto x : a) {
                        if (x[0] * u <= x[1]) {
                                continue;
                        }
                        temp += x[0] * u - x[1];
                }
                return temp <= p * u;
        };

        if (p >= use) {
                cout << -1 << endl;
        } else {
                sort(all(a));
                double l = 0, r = have / min(a[0][0], (use - p));
                while (fabs(l - r) > inf) {
                        double mid = (l + r) / 2;
                        if (check(mid)) {
                                l = mid;
                        } else {
                                r = mid;
                        }
                }
                cout << l << endl;
        }

        return 0;
}
