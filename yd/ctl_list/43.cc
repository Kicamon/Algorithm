/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }

        function<bool(int)> check = [&](int x) {
                int res = 0;
                int j = 0;
                for (int i = 0; i < n; ++i) {
                        if (1ll * res + a[i] <= x) {
                                res += a[i];
                        } else {
                                j++;
                                i--;
                                res = 0;
                        }
                }
                return j < m;
        };

        int l = *max_element(all(a)), r = 1e9;
        while (l < r) {
                int mid = (l + r) >> 1;
                if (check(mid)) {
                        r = mid;
                } else {
                        l = mid + 1;
                }
        }
        cout << l << endl;

        return 0;
}
