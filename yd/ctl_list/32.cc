/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<vector<bool>> a(1, vector<bool>(1));
        a[0][0] = 1;
        int m = 1;
        for (int i = 1; i <= n; ++i) {
                int M = 1 << i;
                vector<vector<bool>> b(M, vector<bool>(M));
                for (int j = 0; j < m; ++j) {
                        for (int k = 0; k < m; ++k) {
                                b[j][k] = b[j][k + m] = b[j + m][k] = a[j][k];
                        }
                }
                a = b;
                m = M;
        }
        for (int i = m - 1; ~i; --i) {
                for (int j = m - 1; ~j; --j) {
                        cout << a[i][j] << " \n"[j == 0];
                }
        }

        return 0;
}
