/* #pragma GCC optimize(2) */
#include <algorithm>
#include <cmath>
#include <cstdio>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

ll get(int x, int y) {
        return 1ll * (x - y) * (x - y);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 3>> a(n);
        for (auto &x : a) {
                cin >> x[0] >> x[1] >> x[2];
        }
        sort(all(a), [&](auto x, auto y) { return x[2] < y[2]; });
        double ans = 0;
        for (int i = 1; i < n; ++i) {
                ans += sqrt(get(a[i - 1][0], a[i][0]) + get(a[i - 1][1], a[i][1]) +
                            get(a[i - 1][2], a[i][2]));
        }
        printf("%.3lf\n", ans);

        return 0;
}
