/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int m, n;
        cin >> m >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        sort(all(a));
        int l = 0, r = n - 1;
        int ans = 0;
        while (l <= r) {
                if (l == r) {
                        ans++;
                        break;
                } else if (a[l] + a[r] > m) {
                        ans++;
                        r--;
                } else {
                        ans++;
                        l++, r--;
                }
        }
        cout << ans << endl;

        return 0;
}
