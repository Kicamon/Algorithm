/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e5 + 10;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        vector<int> primes;
        vector<bool> vis(N);
        vis[1] = true;
        for (int i = 2; i < N; ++i) {
                if (!vis[i]) {
                        primes.push_back(i);
                }
                for (int j : primes) {
                        if (i * j >= N) {
                                break;
                        }
                        vis[i * j] = true;
                        if (i % j == 0) {
                                break;
                        }
                }
        }
        int n;
        cin >> n;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (!vis[x]) {
                        cout << x << ' ';
                }
        }
        cout << endl;

        return 0;
}
