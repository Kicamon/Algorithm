/* #pragma GCC optimize(2) */
#include <cmath>
#include <cstdio>
#include <iomanip>
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

double a, b, c, d;

bool check(double x) {
        double res = d;
        res += a * x * x * x;
        res += b * x * x;
        res += c * x;
        if (fabs(res) < 1e-3) {
                return true;
        }
        return false;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> a >> b >> c >> d;
        for (double i = -100; i <= 100; i += 0.005) {
                if (check(i)) {
                        printf("%.2lf ", i);
                }
        }
        printf("\n");

        return 0;
}
