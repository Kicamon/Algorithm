/* #pragma GCC optimize(2) */
#include <cstdio>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s, t;
        getline(cin, s);
        getline(cin, t);
        for (char &c : s) {
                if ('A' <= c && c <= 'Z') {
                        c += 32;
                }
        }
        for (char &c : t) {
                if ('A' <= c && c <= 'Z') {
                        c += 32;
                }
        }
        vector<string> ts;
        ts.push_back("");
        for (char c : t) {
                if (c == ' ') {
                        ts.push_back("");
                        continue;
                }
                ts.back() += c;
        }
        int ans = 0, idx = 0;
        for (string c : ts) {
                if (c == s) {
                        ans++;
                } else if (!ans) {
                        idx += (int)c.size() + 1;
                }
        }
        if (ans) {
                cout << ans << ' ' << idx << endl;
        } else {
                cout << -1 << endl;
        }

        return 0;
}
