#include <iomanip>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
int n, r;

vector<int> q;

void dfs(int u) {
        if ((int)q.size() == r) {
                for (int x : q) {
                        cout << setw(3) << x;
                }
                cout << endl;
                return;
        }
        if (u == n) {
                return;
        }
        q.push_back(u + 1);
        dfs(u + 1);
        q.pop_back();
        dfs(u + 1);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n >> r;
        dfs(0);

        return 0;
}
