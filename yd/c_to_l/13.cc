/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 2e6 + 10;

bool vis[N];

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        while (n--) {
                double a;
                int t;
                cin >> a >> t;
                for (int i = 1; i <= t; ++i) {
                        int x = a * i;
                        vis[x] = 1 - vis[x];
                }
        }
        for (int i = 1; i < N; ++i) {
                if (vis[i]) {
                        cout << i << endl;
                        break;
                }
        }

        return 0;
}
