/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }

        function<bool(int)> check = [&](int u) {
                ll res = 0;
                for (int x : a) {
                        res += x / u;
                }
                return res < k;
        };

        int l = 0, r = *max_element(all(a));
        while (l < r) {
                int mid = (l + r + 1) >> 1;
                if (check(mid)) {
                        r = mid - 1;
                } else {
                        l = mid;
                }
        }
        cout << l << endl;

        return 0;
}
