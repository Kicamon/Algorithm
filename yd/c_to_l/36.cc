#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

string dfs(int n, string s) {
        string t;
        int in_stack = 0;
        for (int i = 0; i < (int)s.size(); ++i) {
                if ('A' <= s[i] && s[i] <= 'Z') {
                        t += s[i];
                } else if (s[i] == '[') {
                        string temp;
                        int num = 0;
                        in_stack++;
                        for (++i; i < (int)s.size() && '0' <= s[i] && s[i] <= '9'; ++i) {
                                num = num * 10 + (s[i] - '0');
                        }
                        for (; i < (int)s.size(); ++i) {
                                if (s[i] == '[') {
                                        in_stack++;
                                } else if (s[i] == ']') {
                                        in_stack--;
                                }
                                if (!in_stack) {
                                        break;
                                }
                                temp += s[i];
                        }
                        t += dfs(num, temp);
                }
        }
        string res;
        for (int i = 0; i < n; ++i) {
                res += t;
        }
        return res;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        cin >> s;
        cout << dfs(1, s) << endl;

        return 0;
}
