/* #pragma GCC optimize(2) */
#include <iostream>
#include <string>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n + 1);
        for (int &x : a) {
                cin >> x;
        }
        string s;
        for (int i = 0; i < n; ++i) {
                if (!a[i]) {
                        continue;
                }
                if (i && a[i] > 0) {
                        s += '+';
                }
                if (a[i] == 1) {
                        s += 'x';
                } else if (a[i] == -1) {
                        s += "-x";
                } else {
                        s += to_string(a[i]) + 'x';
                }
                if (n - i > 1) {
                        s += "^" + to_string(n - i);
                }
        }
        if (s.empty()) {
                cout << a.back() << endl;
        } else {
                cout << s;
                if (a.back() > 0) {
                        cout << '+' << a.back();
                } else if (a.back() < 0) {
                        cout << a.back();
                }
                cout << endl;
        }

        return 0;
}
