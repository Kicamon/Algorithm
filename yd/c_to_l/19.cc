/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<pair<string, int> > a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i].first;
                a[i].second = i + 1;
        }
        sort(all(a), [&](pair<string, int> x, pair<string, int> y) {
                if (x.first.size() != y.first.size()) {
                        return x.first.size() > y.first.size();
                }
                return x.first > y.first;
        });
        cout << a[0].second << endl;
        cout << a[0].first << endl;

        return 0;
}
