/* #pragma GCC optimize(2) */
#include <iostream>
#include <algorithm>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll a, b;
        cin >> a >> b;
        ll n = a * b;
        int ans = 0;
        for (int i = a; i <= b; i += a) {
                if (n % i) {
                        continue;
                }
                if (__gcd(i, int(n / i)) == a) {
                        ans++;
                }
        }
        cout << ans << endl;

        return 0;
}
