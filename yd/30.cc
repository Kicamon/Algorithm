/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int inf = 1e9;

array<int, 2> a[11];
array<int, 2> temp;

int ans = inf, n, t;

void dfs(int u) {
        if (u == t) {
                ans = min(ans, abs(temp[0] - temp[1]));
                return;
        }
        temp[0] *= a[u][0], temp[1] += a[u][1];
        dfs(u + 1);
        temp[0] /= a[u][0], temp[1] -= a[u][1];
        dfs(u + 1);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        cin >> n;
        a[0] = { 1, 0 };
        for (int i = 1; i <= n; ++i) {
                cin >> a[i][0] >> a[i][1];
                t = i;
                temp = a[i];
                dfs(0);
        }
        cout << ans << endl;

        return 0;
}
