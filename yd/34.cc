/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> dp(n + 1, 1);
        dp[0] = 0;
        for (int i = 1; i <= n; ++i) {
                for (int j = 1; j * 2 <= i; ++j) {
                        dp[i] += dp[j];
                }
        }
        cout << dp[n] << endl;

        return 0;
}
