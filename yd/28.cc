#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<string> s(n);
        for (string &x : s) {
                cin >> x;
        }
        sort(rall(s));
        for (string x : s) {
                cout << x;
        }
        cout << endl;

        return 0;
}
