/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        int t = a[1] - a[0];
        for (int i = 2; i < n; ++i) {
                if (a[i] - a[i - 1] != t) {
                        cout << "NO" << endl;
                        return;
                }
        }
        if (t == 0) {
                for (int i = 0; i < n; ++i) {
                        if (a[i] % (n + 1)) {
                                cout << "NO" << endl;
                                return;
                        }
                }
        }
        if (t) {
                int x = (a[0] + t * n) / (n + 1);
                if (x < max(t, 0ll)) {
                        cout << "NO" << endl;
                        return;
                }
                for (int i = 0; i < n; ++i) {
                        if (x * (n + 1) - t * (n - i) != a[i]) {
                                cout << "NO" << endl;
                                return;
                        }
                }
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
