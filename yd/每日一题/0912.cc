/* #pragma GCC optimize(2) */
#include <array>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        array<int, 2> num = { 0, 0 };
        bool f = true;
        for (int i = 1, x; i <= n; ++i) {
                cin >> x;
                int t = n - i + 1;
                if (i == 1) {
                        while (x) {
                                if (x % t == 0) {
                                        num[1] = x / t;
                                        break;
                                }
                                x -= i;
                                num[0]++;
                        }
                } else {
                        if (x != num[0] * i + num[1] * t) {
                                f = false;
                        }
                }
        }
        cout << (f ? "YES" : "NO") << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
