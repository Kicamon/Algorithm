/* #pragma GCC optimize(2) */
#include <iostream>
#include <stack>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        stack<int> sk;
        if (!n) {
                sk.push(0);
        }
        while (n) {
                sk.push(n % 2);
                n >>= 1;
        }
        while (!sk.empty()) {
                cout << sk.top();
                sk.pop();
        }
        cout << endl;

        return 0;
}
