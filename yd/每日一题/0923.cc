#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        if (n == 1) {
                cout << 1 << endl;
        } else {
                int t = 2, k = 4;
                while (k < n) {
                        k *= 2;
                        k += 2;
                        t++;
                }
                if (t < 2) {
                        cout << 2 << endl;
                } else {
                        cout << t << endl;
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
