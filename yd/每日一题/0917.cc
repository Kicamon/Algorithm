/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        for (int i = 0, q; i < m; ++i) {
                cin >> q;
                int idx = lower_bound(all(a), q) - a.begin();
                if (a[idx] != q) {
                        cout << -1;
                } else {
                        cout << idx + 1;
                }
                cout << " \n"[i == m - 1];
        }

        return 0;
}
