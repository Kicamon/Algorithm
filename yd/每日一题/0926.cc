/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, p;
        cin >> n >> p;
        vector<array<int, 2> > a(n + 1);
        a[0] = { p, n };
        int m = 1e5;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i][1];
        }
        for (int i = 1; i <= n; ++i) {
                cin >> a[i][0];
                m = min(m, a[i][0]);
        }
        ll ans = 0;
        int num = 0;
        if (m != p) {
                ans += p;
                num++;
                a[0][1]--;
        }
        sort(all(a));
        for (int i = 0; i <= n; ++i) {
                if (num + a[i][1] >= n) {
                        ans += 1ll * (n - num) * a[i][0];
                        break;
                }
                num += a[i][1];
                ans += 1ll * a[i][0] * a[i][1];
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
