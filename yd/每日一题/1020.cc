/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t, n;
        cin >> t >> n;
        vector<int> dp(t + 1);
        vector<array<int, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
        }

        for (int i = 0; i < n; ++i) {
                for (int j = t; ~j; --j) {
                        if (j + a[i][0] <= t) {
                                dp[j + a[i][0]] = max(dp[j + a[i][0]], dp[j] + a[i][1]);
                        }
                }
        }
        cout << dp[t] << endl;

        return 0;
}
