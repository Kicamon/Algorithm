/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        sort(all(a));
        vector<int> b;
        int ans = n;
        for (int i = 1; i < n; ++i) {
                int j = upper_bound(all(b), a[i]) - b.begin();
                int k = a.end() - upper_bound(all(a), a[i]);
                ans = min(ans, j + k);
                b.push_back(a[i] + a[i - 1]);
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
