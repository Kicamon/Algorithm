/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<array<int, 2> > a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0];
                a[i][0] %= k;
                a[i][1] = i + 1;
        }
        sort(all(a));
        int idx = 0;
        for (; idx < n; ++idx) {
                if (a[idx][0] != 0) {
                        break;
                }
        }
        if (idx < n) {
                sort(a.begin() + idx, a.end(), [](auto x, auto y) {
                        if (x[0] != y[0]) {
                                return x[0] > y[0];
                        }
                        return x[1] < y[1];
                });
        }
        for (int i = 0; i < n; ++i) {
                cout << a[i][1] << " \n"[i == n - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
