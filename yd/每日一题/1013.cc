/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iomanip>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        iota(all(a), 1);
        do {
                for (int i = 0; i < n; ++i) {
                        cout << setw(5) << a[i];
                }
                cout << endl;
        } while (next_permutation(all(a)));

        return 0;
}
