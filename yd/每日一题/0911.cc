/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string a, b;
        cin >> a >> b;
        vector<int> num(2);
        for (int i = 0; i < n; ++i) {
                if (a[i] == '1') {
                        num[i % 2]--;
                }
                if (b[i] == '0') {
                        num[(i % 2) ^ 1]++;
                }
        }
        if (num[0] < 0 || num[1] < 0) {
                cout << "NO" << endl;
        } else {
                cout << "YES" << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
