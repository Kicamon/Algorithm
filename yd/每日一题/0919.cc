/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <array>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m;
        cin >> n >> m;
        vector<vector<ll>> a(n, vector<ll>(m));
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < m; ++j) {
                        cin >> a[i][j];
                }
        }
        sort(all(a), [](const vector<ll> &x, const vector<ll> &y) {
                return accumulate(all(x), 0ll) > accumulate(all(y), 0ll);
        });
        ll ans = 0, temp = 0;
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < m; ++j) {
                        ans += temp + a[i][j];
                        temp += a[i][j];
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
