/* #pragma GCC optimize(2) */
#include <iostream>
#include <set>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> ans;
        set<int> st;
        for (int i = 0, j = 1; j <= n; ++i, ++j) {
                int t = (i + j) % n;
                if (t == 0) {
                        t = n;
                }
                if (st.count(t)) {
                        cout << -1 << endl;
                        return;
                }
                ans.push_back(t);
                st.insert(t);
        }
        for (int x : ans) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
