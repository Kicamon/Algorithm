/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        ll ans = 0;
        function<void(int, int)> msort = [&](int l, int r) {
                if (l == r) {
                        return;
                }
                int mid = (l + r) >> 1;
                msort(l, mid), msort(mid + 1, r);
                queue<int> q;
                int i = l, j = mid + 1;
                while (i <= mid && j <= r) {
                        if (a[i] <= a[j]) {
                                q.push(a[i++]);
                        } else {
                                q.push(a[j++]);
                                ans += mid - i + 1;
                        }
                }
                while (i <= mid) {
                        q.push(a[i++]);
                }
                while (j <= r) {
                        q.push(a[j++]);
                }
                for (int i = l; i <= r; ++i, q.pop()) {
                        a[i] = q.front();
                }
        };
        msort(0, n - 1);
        cout << ans << endl;

        return 0;
}
