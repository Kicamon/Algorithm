/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> a(n), b(n + 1);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        while (m--) {
                int x, y, z;
                cin >> x >> y >> z;
                b[x - 1] += z;
                b[y] -= z;
        }
        for (int i = 1; i < n; ++i) {
                b[i] += b[i - 1];
        }
        int ans = 1e9;
        for (int i = 0; i < n; ++i) {
                ans = min(ans, a[i] + b[i]);
        }
        cout << ans << endl;

        return 0;
}
