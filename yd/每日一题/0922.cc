#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int gcd(int a, int b) {
        return b ? gcd(b, a % b) : a;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int a, b;
        cin >> a >> b;
        if (a < b) {
                swap(a, b);
        }
        cout << gcd(a, b) << endl;

        return 0;
}
