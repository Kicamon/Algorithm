/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(1001);
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                a[x]++;
        }
        for (int i = 0; i < n; ++i) {
                if (a[i] == 1) {
                        cout << "NO" << endl;
                        return;
                }
                a[i + 1] += max(0, a[i] - 2);
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
