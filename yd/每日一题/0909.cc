/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        int res = 0, tmp = 1e9, temp = 0;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x > a[i]) {
                        res++;
                        temp = x - a[i];
                }
                if (x < a[i]) {
                        tmp = min(a[i] - x, tmp);
                }
        }
        if (res < 2 && temp <= tmp) {
                cout << "YES" << endl;
        } else {
                cout << "NO" << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
