/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        vector<bool> st(n + 1);
        vector<int> a(m);
        for (int i = 0; i < m; ++i) {
                cin >> a[i];
        }
        for (int i = 0, x; i < k; ++i) {
                cin >> x;
                st[x] = true;
        }
        if (n - k > 1) {
                for (int i = 0; i < m; ++i) {
                        cout << 0;
                }
                cout << endl;
        } else if (n == k) {
                for (int i = 0; i < m; ++i) {
                        cout << 1;
                }
                cout << endl;
        } else {
                for (int i = 0; i < m; ++i) {
                        if (!st[a[i]]) {
                                cout << 1;
                        } else {
                                cout << 0;
                        }
                }
                cout << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
