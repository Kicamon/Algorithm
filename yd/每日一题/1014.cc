/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

ll opt(vector<array<int, 2>> a) {
        int n = (int)a.size();
        if (n < 1) {
                return 0;
        }
        auto b = a;
        vector<ll> res(n);
        for (int i = n - 2; ~i; --i) {
                res[i] += (a[i + 1][0] - a[i][1] - 1) * (a[i + 1][1] - a[i + 1][0] + 1);
                a[i][1] += a[i + 1][1] - a[i + 1][0] + 1;
        }
        for (int i = n - 2; ~i; --i) {
                res[i] += res[i + 1];
        }
        ll ans = res[0];
        ll temp = 0;
        for (int i = 1; i < n; ++i) {
                temp += (b[i][0] - b[i - 1][1] - 1) * (b[i - 1][1] - b[i - 1][0] + 1);
                b[i][0] -= b[i - 1][1] - b[i - 1][0] + 1;
                ans = min(ans, temp + res[i]);
        }
        return ans;
}

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        vector<array<int, 2>> a, b;
        for (int i = 0; i < n; ++i) {
                int l = i, r = i;
                while (r + 1 < n && s[r + 1] == s[r]) {
                        r++;
                }
                if (s[i] == 'a') {
                        a.push_back({ l, r });
                } else {
                        b.push_back({ l, r });
                }
                i = r;
        }
        ll ans = opt(a);
        ans = min(ans, opt(b));
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
