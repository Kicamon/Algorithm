/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <set>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        multiset<int> st;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                int t = x % k;
                int p = abs(t - k);
                st.insert(min(t, p));
        }
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        for (int x : a) {
                int t = x % k;
                int p = abs(t - k);
                auto it = st.find(min(t, p));
                if (it == st.end()) {
                        cout << "NO" << endl;
                        return;
                }
                st.erase(it);
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
