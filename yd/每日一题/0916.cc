/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        string s;
        cin >> s;
        int len = 1, n = (int)s.size();
        for (int i = 0; i < n; ++i) {
                if (s[i] == '0') {
                        len = n - i;
                        break;
                }
        }
        string b = string(s.begin() + n - len, s.end());
        int l = 1, res = 0;
        for (int i = 0; i + len < n; ++i) {
                int num = 0;
                for (int j = 0; j < len; ++j) {
                        if (s[i + j] != b[j]) {
                                num++;
                        } else {
                                break;
                        }
                }
                if (num > res) {
                        res = num;
                        l = i + 1;
                }
        }
        cout << 1 << ' ' << n << ' ' << l << ' ' << l + len - 1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
