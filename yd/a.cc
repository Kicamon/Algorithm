/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, k;
        cin >> n >> m >> k;
        ll res = 0, vis = 0;
        int x = -1, idx = 1;
        k--;
        while (m--) {
                if ((x + idx) % n == k) {
                        res++;
                }
                x = (x + idx) % n;
                idx = (idx + 1) % n;

                if (vis && x == 0 && idx == 2) {
                        res += res * ((m - 1) / vis);
                        m = (m - 1) % vis;
                        m++;
                }

                vis++;
        }
        cout << res << endl;

        return 0;
}
