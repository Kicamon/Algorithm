/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n + 1);
        for (int &x : a) {
                cin >> x;
        }
        for (int i = 0; i < n; ++i) {
                if (!a[i]) {
                        continue;
                }
                if (i && a[i] > 0) {
                        cout << '+';
                }
                if (a[i] == -1) {
                        cout << '-';
                } else if (a[i] != 1) {
                        cout << a[i];
                }
                cout << 'x';
                if (n - i > 1) {
                        cout << "^" << n - i;
                }
        }
        if (a[n]) {
                if (a[n] > 0) {
                        cout << '+';
                }
                cout << a[n];
        }
        cout << endl;

        return 0;
}
