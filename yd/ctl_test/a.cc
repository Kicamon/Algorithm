/* #pragma GCC optimize(2) */
#include <iostream>
#include <map>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, c;
        cin >> n >> c;
        map<int, int> m;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                m[x]++;
        }
        ll ans = 0;
        for (auto x : m) {
                if (x.first >= c && m.count(x.first - c)) {
                        ans += 1ll * m[x.first] * m[x.first - c];
                }
        }
        cout << ans << endl;

        return 0;
}
