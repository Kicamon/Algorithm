/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        cin >> s;
        int n = (int)s.size();
        int k;
        cin >> k;
        string ans;
        for (int i = 0; i < n; ++i) {
                for (int j = i + 1; j < n; ++j) {
                        if (s[i] > s[j] && k >= j - i) {
                                k -= j - i;
                                i = j;
                        }
                }
                if (ans.empty() && s[i] == '0') {
                        continue;
                }
                ans += s[i];
        }
        if ((int)ans.size() <= k) {
                cout << 0 << endl;
        } else {
                for (int i = 0; i < (int)ans.size() - k; ++i) {
                        cout << ans[i];
                }
                cout << endl;
        }

        return 0;
}
