/* #pragma GCC optimize(2) */
#include <iostream>
#include <utility>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<int> add(vector<int> a, vector<int> b) {
        if (a.size() < b.size()) {
                swap(a, b);
        }
        int t = 0;
        vector<int> ans;
        for (int i = 0; i < (int)a.size() || t; ++i) {
                if (i < (int)a.size()) {
                        t += a[i];
                }
                if (i < (int)b.size()) {
                        t += b[i];
                }
                ans.push_back(t % 10);
                t /= 10;
        }
        return ans;
}

vector<int> mul(vector<int> a, int b) {
        vector<int> ans;
        int t = 0;
        for (int i = 0; i < (int)a.size() || t; ++i) {
                if (i < (int)a.size()) {
                        t += a[i] * b;
                }
                ans.push_back(t % 10);
                t /= 10;
        }
        return ans;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll n;
        cin >> n;
        vector<int> idx, ans;
        idx.push_back(1), ans.push_back(0);
        for (int i = 1; i <= n; ++i) {
                idx = mul(idx, i);
                ans = add(ans, idx);
        }
        for (int x : vector<int>(ans.rbegin(), ans.rend())) {
                cout << x;
        }
        cout << endl;

        return 0;
}
