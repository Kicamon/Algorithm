/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <string>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

string num[] = { "one",     "two",     "three",     "four",     "five",     "six",      "seven",
                 "eight",   "nine",    "ten",       "eleven",   "twelve",   "thirteen", "fourteen",
                 "fifteen", "sixteen", "seventeen", "eighteen", "nineteen", "twenty" };
map<string, int> p;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        p["a"] = 1;
        p["both"] = 2;
        p["another"] = 1;
        p["first"] = 1;
        p["second"] = 2;
        p["third"] = 3;

        vector<string> s(7);
        vector<int> ans;
        for (string &c : s) {
                cin >> c;
                if (p.count(c)) {
                        ans.push_back(p[c] * p[c] % 100);
                }
        }
        for (string c : s) {
                for (int i = 0; i < 20; ++i) {
                        if (c == num[i]) {
                                int t = (i + 1) * (i + 1) % 100;
                                if (t) {
                                        ans.push_back(t);
                                }
                        }
                }
        }
        sort(all(ans));
        if (ans.size()) {
                for (int i = 0; i < (int)ans.size(); ++i) {
                        if (i && ans[i] < 10) {
                                cout << 0;
                        }
                        cout << ans[i];
                }
                cout << endl;
        } else {
                cout << 0 << endl;
        }

        return 0;
}
