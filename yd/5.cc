/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        string s;
        vector<int> num(26);
        for (int i = 0; i < 4; ++i) {
                getline(cin, s);
                for (int c : s) {
                        if ('A' <= c && c <= 'Z') {
                                num[c - 'A']++;
                        }
                }
        }
        int len = *max_element(all(num));
        vector<vector<char> > ch(26, vector<char>(len, ' '));
        for (int i = 0; i < 26; ++i) {
                for (int j = 0; j < num[i]; ++j) {
                        ch[i][j] = '*';
                }
                reverse(all(ch[i]));
        }
        for (int j = 0; j < len; ++j) {
                for (int i = 0; i < 26; ++i) {
                        cout << ch[i][j] << " \n"[i == 25];
                }
        }
        for (int i = 0; i < 26; ++i) {
                cout << char(i + 'A') << " \n"[i == 25];
        }

        return 0;
}
