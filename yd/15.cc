/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int a, b, c;
        cin >> a >> b >> c;
        int n = a + b + c;
        vector<int> ans(n + 1);
        int idx = 0;
        for (int i = 1; i <= a; ++i) {
                for (int j = 1; j <= b; ++j) {
                        for (int k = 1; k <= c; ++k) {
                                ans[i + j + k]++;
                                if (ans[idx] < ans[i + j + k]) {
                                        idx = i + j + k;
                                }
                        }
                }
        }
        cout << idx << endl;

        return 0;
}
