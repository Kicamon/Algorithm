/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int ntt(int n, string s) {
        ll num = 0;
        ll t = 1;
        for (char c : string(s.rbegin(), s.rend())) {
                int tn = 0;
                if ('0' <= c && c <= '9') {
                        tn = c - '0';
                } else {
                        tn = c - 'A' + 10;
                }
                num += t * tn;
                t *= n;
        }
        return num;
}

string ttn(ll t, int n) {
        if (!t) {
                return "0";
        }
        ll res = 1;
        while (res <= t) {
                res *= n;
        }
        res /= n;
        string s;
        while (t) {
                for (int i = n; ~i; --i) {
                        if (res * i <= t) {
                                s += i >= 10 ? char('A' + (i - 10)) : char(i + '0');
                                t -= res * i;
                                break;
                        }
                }
                res /= n;
        }
        while (res) {
                s += '0';
                res /= n;
        }
        return s;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        string s;
        cin >> n >> s >> m;
        ll t = ntt(n, s);
        s = ttn(t, m);
        cout << s << endl;

        return 0;
}
