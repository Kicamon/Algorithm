/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> prime;
        vector<bool> vis(n + 1);
        for (int i = 2, res = 0; i <= n; ++i) {
                if (!vis[i]) {
                        if (res + i > n) {
                                break;
                        }
                        prime.push_back(i);
                        res += i;
                }
                for (int j : prime) {
                        if (i * j > n) {
                                break;
                        }
                        vis[i * j] = true;
                        if (i % j == 0) {
                                break;
                        }
                }
        }
        for (int x : prime) {
                cout << x << endl;
        }
        cout << (int)prime.size() << endl;

        return 0;
}
