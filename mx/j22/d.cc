#include <bitset>
#include <functional>
#include <iostream>
#include <set>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int dfs(ll u, const ll &y, const ll &p, int ans) {
        if (u == y) {
                return ans;
        }
        if (u > y) {
                return -1;
        }
        ll t = -1, x = -1;
        if (((u + 1) | p) != u) {
                t = dfs((u + 1) | p, y, p, ans + 1);
        }
        if ((u | (p + 1)) != u) {
                x = dfs(u | (p + 1), y, p, ans + 1);
        }
        if (t == -1) {
                return x;
        } else if (x == -1) {
                return t;
        } else {
                return min(t, x);
        }
}

void solve() {
        ll x, y, p;
        cin >> x >> y >> p;
        if (x > y) {
                cout << -1 << endl;
                return;
        }
        if (max(x, y) <= 10) {
                bitset<5> a = x, b = y, c = p;
                Debug(a);
                Debug(b);
                Debug(c);
                cout << dfs(x, y, p, 0) << endl;
        } else {
                int ans = 0;
                while (x < y) {
                        if (((x + 1) | p) == x) {
                                cout << -1 << endl;
                                break;
                        } else if (((x + 1) | p) > y) {
                                cout << -1 << endl;
                                break;
                        }
                        x = (x + 1) | p;
                        ans++;
                }
                cout << ans << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
