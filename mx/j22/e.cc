#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<vector<int> > a;

void solve() {
        int x, r;
        cin >> x >> r;
        if ((int)a[x].size() <= r) {
                cout << -1 << endl;
        } else {
                cout << a[x][r] << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        a.resize(11);
        a[0].push_back(0), a[0].push_back(1);
        for (int i = 1; i <= 10; ++i) {
                for (int j = 0; j < (int)a[i - 1].size(); ++j) {
                        if (a[i - 1][j]) {
                                a[i].push_back(j % 2);
                                a[i].push_back(j % 2);
                                a[i].push_back(j % 2);
                        } else {
                                a[i].push_back(j % 2);
                        }
                }
        }
        while (t--) {
                solve();
        }

        return 0;
}
