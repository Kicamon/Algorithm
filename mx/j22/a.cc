/* #pragma GCC optimize(2) */
#include <cstdlib>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        string s;
        cin >> s;
        vector<int> num(2);
        for (char c : s) {
                if ('a' <= c && c <= 'z') {
                        num[0]++;
                } else {
                        num[1]++;
                }
        }
        cout << min(abs(num[0] + num[1] - 5), abs(5 - num[1])) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
