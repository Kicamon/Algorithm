/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        ll ans = n;
        for (int i = 0, x = 1; i < n; ++i) {
                ans += min(m, a[i] + x);
                if (x == 1 && a[i] < m - 1) {
                        x = 2;
                } else {
                        x = 1;
                }
        }
        cout << ans << endl;

        return 0;
}
