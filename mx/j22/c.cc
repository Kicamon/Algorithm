/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        map<int, int> mp;
        int idx = 0, jdx = -1, ti = 0;
        for (int &x : a) {
                cin >> x;
        }
        for (int i = 0; i < n; ++i) {
                cin >> b[i];
                mp[b[i]]++;
                if (a[i] > a[idx] || (a[i] == a[idx] && b[i] > b[idx])) {
                        idx = i;
                }
        }
        for (int i = 0; i < n; ++i) {
                if (!b[i]) {
                        continue;
                }
                if (jdx == -1 || a[jdx] < a[i]) {
                        jdx = i;
                }
        }
        for (int i = 1; i <= n; ++i) {
                while (n - mp[ti] < i || ti == b[idx]) {
                        ti++;
                }
                int ans = a[idx] - ti;
                if (n - mp[0] >= i) {
                        ans = max(ans, a[jdx]);
                }
                cout << ans << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
