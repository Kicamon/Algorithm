// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    int idx = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        if (a[i] > idx) {
            idx = a[i];
        }
    }
    queue<int> cnt;
    int res = 0, ii = 0;
    for (int i = 1; i < n; ++i) {
        if (a[i] == idx) {
            ii = i;
            while (res * idx + a[i] < a[i - 1]) {
                res++;
            }
        } else {
            while (a[i] < a[i - 1]) {
                a[i] += idx;
                cnt.push(i);
            }
        }
    }
    while (res--) {
        cnt.push(ii);
    }
    cout << (int)cnt.size() << endl;
    while (!cnt.empty()) {
        cout << cnt.front() << ' ' << cnt.front() << endl;
        cnt.pop();
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
