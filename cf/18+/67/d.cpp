#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> b(n + 1), num(n + 1);
    vector<bool> vis(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> b[i];
        num[b[i]]++;
    }
    for (int i = 1; i <= n; ++i) {
        if (i == b[i] && k != 1) {
            cout << "No" << endl;
            return;
        }
    }
    if (k == 1) {
        for (int i = 1; i <= n; ++i) {
            if (i != b[i]) {
                cout << "No" << endl;
                return;
            }
        }
        cout << "Yes" << endl;
        return;
    }
    queue<int> q;
    for (int i = 1; i <= n; ++i) {
        if (!num[i]) {
            q.push(i);
        }
    }
    while (!q.empty()) {
        int t = q.front();
        q.pop();
        vis[t] = true;
        if (--num[b[t]] == 0) {
            q.push(b[t]);
        }
    }
    for (int i = 1; i <= n; ++i) {
        if (!vis[i]) {
            int x = b[i];
            int res = 1;
            do {
                vis[x] = true;
                res++;
                x = b[x];
            } while (x != i);
            if (res != k) {
                cout << "No" << endl;
                return;
            }
        }
    }
    cout << "Yes" << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
