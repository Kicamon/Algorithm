// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<array<int, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0];
                a[i][1] = i;
        }
        sort(all(a), greater<>());
        vector<int> b(n);
        for (int i = 0, res = 1; i < n; ++i) {
                b[a[i][1]] = res++;
        }
        for (int i = 0; i < n; ++i) {
                cout << b[i] << " \n"[i == n - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
