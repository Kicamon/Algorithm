// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e4 * 100;

void solve() {
    int w, f;
    cin >> w >> f;
    int n;
    cin >> n;
    vector<int> s(n);
    for (int i = 0; i < n; ++i) {
        cin >> s[i];
    }
    bitset<N + 1> dp{};
    dp[0] = 1;
    int sum = 0;
    for (int i = 0; i < n; ++i) {
        dp |= dp << s[i];
        sum += s[i];
    }
    int ans = 1e9;
    for (int i = 0; i <= sum; ++i) {
        if (dp[i]) {
            ans = min(ans, max((i + w - 1) / w, (sum - i + f - 1) / f));
        }
    }
    cout << ans << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
