#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n, m;
        cin >> n >> m;
        vector<string> s(n);
        string res = "vika";
        int idx = 0;
        for (int i = 0; i < n; ++i) {
                cin >> s[i];
        }
        for (int i = 0; i < m; ++i) {
                bool f = false;
                for (int j = 0; j < n; ++j) {
                        if (s[j][i] == res[idx]) {
                                idx++;
                                if (idx == 4) {
                                        cout << "YES\n";
                                        return;
                                }
                                f = true;
                                break;
                        }
                }
                if (f) {
                        continue;
                }
        }
        cout << "NO\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
