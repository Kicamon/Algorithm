#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        vector<int> b;
        b.push_back(a[0]);
        for (int i = 1; i < n; ++i) {
                if (a[i] < a[i - 1]) {
                        b.push_back(a[i]);
                }
                b.push_back(a[i]);
        }
        cout << b.size() << endl;
        for (int x : b) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
