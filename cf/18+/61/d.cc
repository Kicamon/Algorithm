#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        int ans = 0;
        for (int i = 1; i < n; ++i) {
                ans += (a[i] <= a[i - 1]);
        }
        int res = ans;
        for (int i = 1; i < n; ++i) {
                res -= (a[i] <= a[i - 1]);
                if (i > 1)
                        res += (a[i - 1] >= a[i - 2]);
                ans = min(ans, res + 1);
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
