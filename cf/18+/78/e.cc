// #pragma GCC optimize(2)
#include <bits/stdc++.h>
#include <climits>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 2e5;

int n;
int val[(N << 2) + 10];
int a[N + 10];

void pushup(int u) {
        val[u] = val[u << 1] & val[u << 1 | 1];
}

void build(int u, int l, int r) {
        if (l == r) {
                val[u] = a[l];
                return;
        }
        int mid = (l + r) >> 1;
        build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
        pushup(u);
}

int query(int u, int l, int r, int L, int R) {
        if (L <= l && r <= R) {
                return val[u];
        }
        int mid = (l + r) >> 1;
        int res = INT_MAX;
        if (L <= mid) {
                res &= query(u << 1, l, mid, L, R);
        }
        if (R > mid) {
                res &= query(u << 1 | 1, mid + 1, r, L, R);
        }
        return res;
}

void solve() {
        cin >> n;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
        }
        build(1, 1, n);
        int q;
        cin >> q;
        int L, k;
        while (q--) {
                cin >> L >> k;
                int l = L, r = n;
                while (l < r) {
                        int mid = (l + r + 1) >> 1;
                        if (query(1, 1, n, L, mid) < k) {
                                r = mid - 1;
                        } else {
                                l = mid;
                        }
                }
                if (l == L && a[L] < k) {
                        l = -1;
                }
                cout << l << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
