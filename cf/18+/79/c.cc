// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 998244353;
const int N = 2e5 + 10;

ll f[N];

void solve() {
        string s;
        cin >> s;
        int ans = 0;
        ll res = 1;
        int len = 1;
        for (int i = 1; i < (int)s.size(); ++i) {
                if (s[i] == s[i - 1]) {
                        len++;
                } else {
                        res = res * len % mod;
                        ans += len - 1;
                        len = 1;
                }
        }
        res = res * len % mod;
        ans += len - 1;
        res = res * f[ans] % mod;
        cout << ans << ' ' << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        f[0] = 1;
        for (int i = 1; i <= N - 10; ++i) {
                f[i] = f[i - 1] * i % mod;
        }

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
