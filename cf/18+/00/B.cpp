#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n, m;
    cin >> n >> m;
    string s;
    cin >> s;
    vint numl(26), numb(26);
    for (int i = 0; i < n; ++i)
    {
        if ('a' <= s[i] && s[i] <= 'z')
            numl[s[i] - 'a']++;
        else
            numb[s[i] - 'A']++;
    }
    int res = 0;
    for (int i = 0; i < 26; ++i)
    {
        res += min(numl[i], numb[i]);
        if (abs(numl[i] - numb[i]) / 2 <= m)
        {
            res += abs(numl[i] - numb[i]) / 2;
            m -= abs(numl[i] - numb[i]) / 2;
        }
        else
        {
            res += m;
            m = 0;
        }
    }
    cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
