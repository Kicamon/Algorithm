//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;
#define PCI pair<char, int>

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        vector<PCI> q;
        q.pb({ s[0], 0 });
        for (int i = 0; i < n; ++i) {
                if (s[i] == q.back().first)
                        q.back().second++;
                else
                        q.pb({ s[i], 1 });
        }
        int len = (int)q.size();
        int res = 0;
        for (int i = 0; i < len; ++i) {
                if (q[i].second == 1) {
                        if (i == len - 1)
                                continue;
                        if (i == 0)
                                res += 1;
                        else
                                res += q[i + 1].first != q[i - 1].first;
                } else {
                        if (i == len - 1)
                                res++;
                        else
                                res += 2;
                }
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
