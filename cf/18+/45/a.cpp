using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve()
{
    int n, k, x;
    cin >> n >> k >> x;
    if (k == 1 && x == 1) {
        cout << "NO\n";
        return;
    } else if ((n & 1) && k == 2 && x == 1) {
        cout << "NO\n";
        return;
    } else {
        cout << "YES\n";
        if (k == x) {
            int res = n / (k - 1);
            int cnt = n % (k - 1);
            cout << res + bool(cnt) << endl;
            for (int i = 0; i < res; ++i) {
                cout << k - 1 << ' ';
            }
            cout << cnt << endl;
        } else {
            int res = n / k;
        }
    }
}

