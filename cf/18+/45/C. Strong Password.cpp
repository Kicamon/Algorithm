#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    queue<int> num[10];
    string s;
    cin >> s;
    for (int i = 0, c; i < (int)s.size(); ++i) {
        c = s[i] - '0';
        num[c].push(i);
    }
    int m;
    cin >> m;
    string l, r;
    cin >> l >> r;
    for (int i = 0; i < m; ++i) {
        int x = l[i] - '0', y = r[i] - '0';
        int f = 0;
        for (int j = y; j >= x; --j) {
            if (!num[j].empty()) {
                f = j;
            }
        }
        if (!f) {
            cout << "NO\n";
            return;
        }
        for (int j = x; j <= y; ++j) {
            if (!num[j].empty() && num[j].front() < num[f].front()) {
                f = j;
            }
        }
        num[f].pop();
    }
    cout << "YES\n";
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
