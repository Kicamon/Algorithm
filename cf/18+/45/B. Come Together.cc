#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        array<int, 2> a, b, c;
        cin >> a[0] >> a[1];
        cin >> b[0] >> b[1];
        cin >> c[0] >> c[1];
        b[0] -= a[0], b[1] -= a[1];
        c[0] -= a[0], c[1] -= a[1];
        if (b[0] < 0 && c[0] < 0)
                b[0] = -b[0], c[0] = -c[0];
        if (b[1] < 0 && c[1] < 0)
                b[1] = -b[1], c[1] = -c[1];
        int res = 1;
        res += max(0, min(b[0], c[0]));
        res += max(0, min(b[1], c[1]));
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
