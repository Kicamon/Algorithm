#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    int n, k, x;
    cin >> n >> k >> x;
    if (x != 1) {
        cout << "YES\n";
        cout << n << endl;
        for (int i = 0; i < n; ++i) {
            cout << 1 << ' ';
        }
        cout << endl;
    } else {
        if (k == 1 || (n % 2 && k == 2)) {
            cout << "NO\n";
        } else {
            cout << "YES\n";
            int res = n >> 1;
            cout << res << endl;
            cout << 2 + n % 2 << ' ';
            for (int i = 1; i < res; ++i) {
                cout << 2 << ' ';
            }
            cout << endl;
        }
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
