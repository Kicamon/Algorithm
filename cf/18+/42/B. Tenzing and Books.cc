#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n, m;
        cin >> n >> m;
        vector<vector<int>> a(3, vector<int>(n));
        int res = 0;
        for (int i = 0; i < 3; ++i) {
                for (int j = 0; j < n; ++j) {
                        cin >> a[i][j];
                }
        }
        for (int i = 0; i < 3; ++i) {
                for (int j = 0; j < n; ++j) {
                        if ((m | a[i][j]) != m) {
                                break;
                        }
                        res |= a[i][j];
                }
        }
        cout << (res == m ? "Yes" : "No") << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
