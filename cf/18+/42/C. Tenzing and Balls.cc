#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int inf = 1e9;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1), c(n + 1);
        vector<vector<int>> b(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                b[a[i]].push_back(i);
        }
        vector<int> mx(n + 1, -inf);
        for (int i = 1; i <= n; ++i) {
                c[i] = c[i - 1];
                c[i] = max(c[i], mx[a[i]] + i);
                mx[i] = max(mx[a[i]], c[i - 1] - (i - 1));
        }
        cout << c[n] << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);
        cout.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
