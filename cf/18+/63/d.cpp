#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

char cl[2] = {'W', 'B'};

void solve() {
    int n, m;
    cin >> n >> m;
    vector<string> s(n);
    for (int i = 0; i < n; ++i) {
        cin >> s[i];
    }
    vector<int> num(m);
    for (int i = 0; i < n; ++i) {
        int idx = 0;
        for (int j = 0; j < m; ++j) {
            if (s[i][j] == '.') {
                continue;
            }
            if (s[i][j] == 'L') {
                s[i][j] = cl[num[j]];
                num[j] ^= 1;
            } else if (s[i][j] == 'R') {
                if (s[i][j - 1] == 'W') {
                    s[i][j] = 'B';
                } else {
                    s[i][j] = 'W';
                }
            } else if (s[i][j] == 'U') {
                s[i][j] = cl[idx];
                idx ^= 1;
            } else {
                if (s[i - 1][j] == 'W') {
                    s[i][j] = 'B';
                } else {
                    s[i][j] = 'W';
                }
            }
        }
        if (idx) {
            cout << -1 << endl;
            return;
        }
    }
    for (int i = 0; i < m; ++i) {
        if (num[i]) {
            cout << -1 << endl;
            return;
        }
    }
    for (int i = 0; i < n; ++i) {
        cout << s[i] << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
