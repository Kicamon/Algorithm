#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve()
{
    int n;
    cin >> n;
    vector<int> a(101);
    for (int i = 0, c; i < n; ++i)
    {
        cin >> c;
        a[c]++;
    }
    int num = a[0];
    for (int i = 1; i <= 100; ++i)
    {
        if (num < a[i])
        {
            cout << "NO\n";
            return;
        }
        num = a[i];
    }
    cout << "YES\n";
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
