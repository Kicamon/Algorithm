// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, p;
        cin >> n >> p;
        vector<array<ll, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0];
        }
        for (int i = 0; i < n; ++i) {
                cin >> a[i][1];
        }
        sort(all(a), [&](array<ll, 2> x, array<ll, 2> y) { return x[1] < y[1]; });
        ll res = n - 1, ans = p;
        for (int i = 0; i < n; ++i) {
                if (a[i][1] < p) {
                        if (res > a[i][0]) {
                                res -= a[i][0];
                                ans += a[i][0] * a[i][1];
                        } else {
                                ans += res * a[i][1];
                                break;
                        }
                } else {
                        ans += res * p;
                        break;
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
