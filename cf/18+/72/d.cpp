// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    int n, x, y;
    cin >> n >> x >> y;
    int cnt = __gcd(x, y);
    int Z = n / (1ll * x * y / cnt), X = n / x - Z, Y = n / y - Z;
    /* Debug(X), Debug(Y); */
    ll res = (1ll * n + (n - X + 1)) * X / 2;
    res -= (1ll + (1ll * Y)) * Y / 2;
    cout << res << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
