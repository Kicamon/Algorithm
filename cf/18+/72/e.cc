#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    string s;
    cin >> s;
    vector<int> sum(n + 1);
    int res = 0;
    for (int i = 0; i < n; ++i) {
        if (s[i] == '0') {
            res ^= a[i];
        }
        sum[i + 1] = sum[i] ^ a[i];
    }
    int k;
    cin >> k;
    for (int _ = 0; _ < k; ++_) {
        int op;
        cin >> op;
        if (op == 1) {
            int l, r;
            cin >> l >> r;
            l--;
            res ^= sum[l] ^ sum[r];
        } else {
            int g;
            cin >> g;
            if (g == 0) {
                cout << res << ' ';
            } else {
                cout << (res ^ sum[n]) << ' ';
            }
        }
    }
    cout << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
