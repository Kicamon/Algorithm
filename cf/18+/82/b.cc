// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int num[51];

void solve() {
        memset(num, 0, sizeof num);
        int n;
        cin >> n;
        vector<vector<int>> a(n);
        vector<int> sum(n);
        int res = 0;
        for (int i = 0, m; i < n; ++i) {
                cin >> m;
                vector<int> b(m);
                for (int j = 0; j < m; ++j) {
                        cin >> b[i];
                        num[b[i]]++;
                        res += num[b[i]] == 1;
                }
                a[i] = b;
        }
        for (int i = 0; i < n; ++i) {
                for (int x : a[i]) {
                        if (num[x] == 1) {
                                sum[i]++;
                        }
                }
                cout << sum[i] << endl;
        }
        cout << res - *min_element(all(sum)) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
