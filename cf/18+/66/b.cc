// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 998244353;

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n, m;
    cin >> n;
    vector<int> a(n);
    map<int, int> b;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0, x; i < n; ++i) {
        cin >> x;
        b[a[i]] = x;
    }
    cin >> m;
    vector<int> c(m);
    map<int, int> d;
    for (int i = 0; i < m; ++i) {
        cin >> c[i];
    }
    for (int i = 0, x; i < m; ++i) {
        cin >> x;
        d[c[i]] = x;
    }
    for (int i = 0; i < m; ++i) {
        if (!b.count(c[i])) {
            cout << 0 << endl;
            return 0;
        }
    }
    ll res = 1;
    for (int i = 0; i < n; ++i) {
        if (d.count(a[i]) && b[a[i]] == d[a[i]]) {
            continue;
        }
        if (d.count(a[i]) && b[a[i]] < d[a[i]]) {
            res = 0;
            break;
        }
        res = res * 2 % mod;
    }
    cout << res << endl;

    return 0;
}
