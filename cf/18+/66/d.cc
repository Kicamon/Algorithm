// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m, k;
        cin >> n >> m >> k;
        vector<vector<int>> a(m, vector<int>(n));
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < m; ++j) {
                        cin >> a[j][i];
                }
        }
        priority_queue<int, vector<int>, less<int>> q[m + 1];
        for (int i = 0; i < m; ++i) {
                for (int j = 0; j < n; ++j) {
                        q[i].push(a[i][j]);
                }
        }
        ll res = 0;
        for (int i = 0; i + k <= m; ++i) {
                int cnt = i;
                for (int j = i; j < i + k; ++j) {
                        if (q[j].size() && (q[cnt].empty() || q[j].top() > q[cnt].top())) {
                                cnt = j;
                        }
                }
                if (q[cnt].size()) {
                        res += q[cnt].top();
                        q[cnt].pop();
                }
        }
        cout << res << endl;

        return 0;
}
