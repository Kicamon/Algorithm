#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n, m;
        cin >> n >> m;
        if (n < m)
                swap(n, m);
        int k = n * m;
        vector<int> a(k);
        for (int i = 0; i < k; ++i)
                cin >> a[i];
        sort(a.begin(), a.end());
        int x = a[k - 1] - a[0];
        int y = max(a[k - 2] - a[0], a[k - 1] - a[1]);
        cout << 1ll * x * (n - 1) * m + y * (m - 1) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t;
        cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
