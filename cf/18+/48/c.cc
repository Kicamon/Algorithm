int gcd(int a, int b) {
        if (a == 0) {
                return 0;
        }
        if (b == 0) {
                return 1;
        }
        if (a >= b) {
                int r = a % b;
                int k = a / b;
                if (k % 2 == 1) {
                        return gcd(b, r) + k + k / 2;
                } else {
                        return gcd(r, b) + k + k / 2;
                }
        }
        return 1 + gcd(b, abs(a - b));
}

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        for (int i = 0; i < n; ++i) {
                cin >> b[i];
        }
        set<int> c;
        for (int i = 0; i < n; ++i) {
                if (!a[i] && !b[i]) {
                        continue;
                }
                c.insert((a[i], b[i]) % 3);
        }
        if (c.size() <= 1) {
                cout << "YES\n";
        } else {
                cout << "NO\n";
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
