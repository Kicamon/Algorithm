#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n;
    cin >> n;
    vint a(n);
    for (int i = 0; i < n; ++i)
        cin >> a[i];
    sort(all(a));
    if (a[0] > 1)
    {
        cout << "NO\n";
        return;
    }
    int res = 1;
    for (int i = 1; i < n; ++i)
    {
        if (a[i] > res)
        {
            cout << "NO\n";
            return;
        }
        res += a[i];
    }
    cout << "YES\n";
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
