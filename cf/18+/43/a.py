def solve():
    n = int(input())
    a = input()
    a = sorted(list(map(int, a.split())))
    l, r = 0, n - 1
    res = 0
    while l < r:
        res += a[r] - a[l]
        r -= 1
        l += 1
    print(res)


t = int(input())
while t > 0:
    solve()
    t -= 1
