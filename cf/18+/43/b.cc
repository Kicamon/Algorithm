// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i)
                cin >> a[i];
        ll res = 0, cnt = 0;
        bool f = true;
        for (int i = 0; i < n; ++i) {
                if (a[i] < 0) {
                        if (f) {
                                cnt++;
                                f = false;
                        }
                        res += -a[i];
                } else if (a[i] > 0) {
                        res += a[i];
                        f = true;
                }
        }
        cout << res << ' ' << cnt << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
