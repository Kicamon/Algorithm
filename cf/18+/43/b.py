def solve():
    n = int(input())
    a = list(map(int, input().split()))
    f = True
    res, cnt = 0, 0
    for i in a:
        if i > 0:
            res += i
            f = True
        elif i < 0:
            res -= i
            if f:
                f = False
                cnt += 1
    print(res, cnt)


if __name__ == '__main__':
    t = int(input())
    while t > 0:
        solve()
        t -= 1
