// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n, m;
        cin >> n >> m;
        vector<array<int, 2>> len(m);
        for (int i = 0; i < m; ++i) {
                cin >> len[i][0] >> len[i][1];
        }
        sort(len.begin(), len.end());
        int q;
        cin >> q;
        vector<int> a(n + 1);
        for (int i = 1; i <= q; ++i) {
                cin >> a[i];
        }
        auto check = [&](int x) {
                vector<int> t(n + 1);
                for (int i = 1; i <= x; ++i) {
                        t[a[i]] = 1;
                }
                for (int i = 1; i <= n; ++i) {
                        t[i] += t[i - 1];
                }
                for (int i = 0; i < m; ++i) {
                        int res = t[len[i][1]] - t[len[i][0] - 1];
                        if (res * 2 > len[i][1] - len[i][0] + 1)
                                return true;
                }
                return false;
        };
        int l = 0, r = q + 1;
        while (l < r) {
                int mid = l + r >> 1;
                if (check(mid)) {
                        r = mid;
                } else {
                        l = mid + 1;
                }
        }
        if (l && l <= q) {
                cout << l << endl;
        } else {
                cout << -1 << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
