N = int(2e5 + 10)

e = [[] for i in range(N)]
num = []


def dfs(u, fa):
    res = 0
    for v in e[u]:
        if v == fa:
            continue
        res += dfs(v, u)

    res = max(res, 1)
    num[u] = res
    return res


def solve():
    n = int(input())
    e.clear()
    num.clear()
    for i in range(1, n, 1):
        a, b = map(int, input().split())
        e[a].append(b)
        e[b].append(a)
    dfs(1, 0)
    q = int(input())
    while q > 0:
        x, y = map(int, input().split())
        print(num[x] * num[y])
        q -= 1


if __name__ == '__main__':
    t = int(input())
    while t > 0:
        solve()
        t -= 1
