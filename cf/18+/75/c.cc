// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long
const int N = sqrt(1e9) + 1;

int lowbit(int x) {
        return x & -x;
}

void solve() {
        int n, m;
        cin >> n >> m;
        int d = __gcd(n, m);
        n /= d;
        m /= d;
        if (n % m == 0) {
                cout << 0 << endl;
                return;
        }
        if (lowbit(m) != m) {
                cout << -1 << endl;
                return;
        }
        ll res = 0;
        while (n) {
                while (n < m) {
                        res += n;
                        n <<= 1;
                }
                n %= m;
        }
        cout << res * d << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
