// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        vector<int> a(n), b(m);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        for (int i = 0; i < m; ++i) {
                cin >> b[i];
        }
        ll res = 0;
        sort(all(a)), sort(all(b));
        if (k >= 2) {
                if (a[0] > b[m - 1]) {
                        swap(b[0], a[n - 1]);
                } else {
                        if (a[n - 1] > b[m - 1]) {
                                swap(a[n - 1], b[m - 1]);
                        }
                        if (a[0] > b[0]) {
                                swap(a[0], b[0]);
                        }
                }
                k -= 2;
        }
        for (int i = 0; i < n; ++i) {
                res += a[i];
        }
        if (k & 1) {
                int x = *min_element(all(a)), y = *max_element(all(b));
                if (x < y) {
                        res = res - x + y;
                }
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
