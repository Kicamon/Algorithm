// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0, x; i < n; ++i) {
        cin >> x;
        if (x < n)
            a[x]++;
    }
    int m = 0;
    while (m < n && a[m])
        m++;
    vector<ll> dp(n + 1, INT_MAX);
    dp[m] = 0;
    for (int i = m; i >= 1; --i)
        for (int j = 0; j < i; ++j)
            dp[j] = min(dp[j], dp[i] + 1ll * i * a[j]);
    cout << dp[0] - m << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
