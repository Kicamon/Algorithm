void solve() {
        string s;
        cin >> s;
        int res = 0;
        for (int i = 0; i < 3; ++i) {
                if (s[i] - 'a' != i) {
                        res++;
                }
        }
        if (res == 3) {
                cout << "NO\n";
        } else {
                cout << "YES\n";
        }
}

