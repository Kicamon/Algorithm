// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<int> a(n), h(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        for (int i = 0; i < n; ++i) {
                cin >> h[i];
        }
        int ans = 0;
        int l = 0, r = 0, res = a[0];
        for (int i = 1; i < n; ++i) {
                if (h[i - 1] % h[i] == 0) {
                        if (res + a[i] > k) {
                                if (a[l] < a[i]) {
                                        ans = max(ans, r - l + 1);
                                }
                                res -= a[l++];
                        }
                        res += a[i];
                        r = i;
                } else {
                        ans = max(ans, r - l + 1);
                        res = a[i];
                        l = r = i;
                }
        }
        ans = max(ans, r - l + 1);
        if (*min_element(all(a)) > k) {
                ans = 0;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
