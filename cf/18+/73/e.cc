// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

void solve() {
    int n, x;
    cin >> n >> x;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(all(a));
    vector<int> h(n);
    for (int i = 1; i < n; ++i) {
        h[i] = h[i - 1] + i * (a[i] - a[i - 1]);
    }
    int idx = lower_bound(all(h), x) - h.begin();
    if (idx == n) {
        x -= h[idx - 1];
        x /= n;
        cout << a[idx - 1] + x << endl;
    } else if (h[idx] > x) {
        x -= h[idx - 1];
        x /= idx;
        cout << a[idx - 1] + x << endl;
    } else {
        cout << a[idx] << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
