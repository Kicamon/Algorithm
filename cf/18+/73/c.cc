#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int val[10][10];

void solve() {
        string s;
        int res = 0;
        for (int i = 0; i < 10; ++i) {
                cin >> s;
                for (int j = 0; j < 10; ++j) {
                        if (s[j] == 'X') {
                                res += val[i][j];
                        }
                }
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 0; i < 10; ++i) {
                for (int j = 0; j < 10; ++j) {
                        int res = min(abs(i - 4), abs(i - 5));
                        int cnt = min(abs(j - 4), abs(j - 5));
                        res = max(res, cnt);
                        val[i][j] = 5 - res;
                }
        }

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
