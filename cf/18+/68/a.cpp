// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n, m;
    cin >> n >> m;
    cout << (m == 1 ? 0 : (m == 2 ? 2 : min(n + 1, m))) << endl;
    for (int i = 0; i < min(n, m - 1); ++i) {
        for (int j = 0; j < m; ++j) {
            cout << (i + j) % m << ' ';
        }
        cout << endl;
    }
    for (int i = m - 1; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            cout << j << ' ';
        }
        cout << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
