// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<int> t(32);

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    ll res = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        res += a[i];
    }
    if (res % n) {
        cout << "No" << endl;
        return;
    }
    res /= n;
    sort(all(a));
    priority_queue<int, vector<int>, greater<int>> q, p;
    for (int i = 0; i < n; ++i) {
        if (a[i] < res) {
            q.push(res - a[i]);
        } else if (a[i] > res) {
            p.push(a[i] - res);
        }
    }
    while (!q.empty() && !p.empty()) {
        int l = q.top(), r = p.top();
        q.pop(), p.pop();
        if (l > r) {
            cout << "No" << endl;
            return;
        } else {
            int i = lower_bound(all(t), l) - t.begin();
            if (t[i] != l) {
                cout << "No" << endl;
                return;
            }
            r -= l;
            if (r) {
                p.push(r);
            }
        }
    }
    if (!q.empty() || !p.empty()) {
        cout << "No" << endl;
    } else {
        cout << "Yes" << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    for (int i = 1; i <= 31; ++i) {
        t[i] = 1 << (i - 1);
    }

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
