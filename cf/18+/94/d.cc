#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m;
        cin >> n >> m;
        vector<int> a(n), b(m);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        priority_queue<int, vector<int>, less<int>> q;
        for (int i = 0, c; i < m; ++i) {
                cin >> c;
                q.push(c);
        }
        vector<int> ans;
        for (int i = 0; i < n; ++i) {
                while (!q.empty() && a[i] <= q.top()) {
                        ans.push_back(q.top());
                        q.pop();
                }
                ans.push_back(a[i]);
        }
        while (!q.empty()) {
                ans.push_back(q.top());
                q.pop();
        }
        for (int x : ans) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
