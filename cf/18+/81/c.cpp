#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<string> s(n);
    vector<vector<bool>> vis(n, vector<bool>(n));
    for (int i = 0; i < n; ++i) {
        cin >> s[i];
    }
    int res = 0;
    for (int i = 0; i <= n / 2; ++i) {
        for (int j = 0; j <= n / 2; ++j) {
            if (vis[i][j]) {
                continue;
            }
            vis[i][j] = true;
            int x1 = n - i - 1, y1 = n - j - 1;
            bool f = true;
            char c = s[i][j];
            if (s[i][j] != s[x1][y1]) {
                c = max(c, s[x1][y1]);
                f = false;
            }
            vis[x1][y1] = true;
            int x2 = j, y2 = n - i - 1;
            if (s[i][j] != s[x2][y2]) {
                c = max(c, s[x2][y2]);
                f = false;
            }
            vis[x2][y2] = true;
            int x3 = n - j - 1, y3 = i;
            if (s[i][j] != s[x3][y3]) {
                c = max(c, s[x3][y3]);
                f = false;
            }
            vis[x3][y3] = true;
            if (f) {
                continue;
            }
            res += c - s[i][j] + c - s[x1][y1] + c - s[x2][y2] + c - s[x3][y3];
        }
    }
    cout << res << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
