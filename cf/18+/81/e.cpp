// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    vector<int> b(n);
    for (int i = 0; i < n; ++i) {
        if (i + a[i] < n) {
            b[i] = a[i];
        }
    }
    vector<int> dp(n);
    dp[b[0]] = b[0] + 1;
    for (int i = 1; i < n; ++i) {
        dp[i] += dp[i - 1];
        dp[i + b[i]] = dp[i] + b[i] + 1;
    }
    cout << n - *max_element(all(dp)) << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
