#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e3 + 10;

vector<int> primes;

void prime() {
        vector<bool> vis(N + 1);
        for (int i = 2; i <= N; ++i) {
                if (!vis[i]) {
                        primes.push_back(i);
                }
                for (int j : primes) {
                        if (i * j > N) {
                                break;
                        }
                        vis[i * j] = true;
                        if (i % j == 0) {
                                break;
                        }
                }
        }
}

void solve() {
        int n;
        cin >> n;
        map<int, int> a;
        for (int i = 0, c; i < n; ++i) {
                cin >> c;
                for (int j : primes) {
                        if (c < j) {
                                break;
                        }
                        int res = 0;
                        while (c % j == 0) {
                                res++;
                                c /= j;
                        }
                        a[j] += res;
                }
                if (c > 1) {
                        a[c] += 1;
                }
        }
        for (auto x : a) {
                if (x.second % n) {
                        cout << "NO" << endl;
                        return;
                }
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        prime();
        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
