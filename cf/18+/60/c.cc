#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n;
        cin >> n;
        vector<int> p(n);
        int a = n + 1, b = n + 1;
        int res = 0;
        for (int i = 0; i < n; ++i) {
                cin >> p[i];
                if (p[i] < a) {
                        a = p[i];
                } else if (p[i] < b) {
                        res++;
                        b = p[i];
                }
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
