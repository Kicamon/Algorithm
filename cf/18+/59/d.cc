// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n;
        cin >> n;
        vector<array<int, 2>> cnt(n);
        for (int i = 0; i < n; ++i) {
                int l, r, a, b;
                cin >> l >> r >> a >> b;
                cnt[i] = { l, b };
        }
        sort(cnt.begin(), cnt.end());
        vector<array<int, 2>> a;
        for (auto [l, r] : cnt) {
                if (!a.empty() && l <= a.back()[1]) {
                        a.back()[1] = max(r, a.back()[1]);
                } else {
                        a.push_back({ l, r });
                }
        }
        int q;
        cin >> q;
        for (int i = 0; i < q; ++i) {
                int x;
                cin >> x;
                int res = lower_bound(a.begin(), a.end(), array{ x + 1, 0 }) - a.begin() - 1;
                if (res >= 0) {
                        x = max(x, a[res][1]);
                }
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
