// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

void solve() {
        int n;
        cin >> n;
        ll res = 0;
        for (int i = 1; i <= n; ++i) {
                res += i * i;
        }
        ll ans = res - n * n;
        for (int i = 1; i <= n / 2; ++i) {
                ll cnt = 0, phi = 0, php = 0;
                for (int j = n, k = n - i; j >= k; --j, ++k) {
                        cnt += j * j;
                        phi += j * k;
                        php = max(php, 1ll * j * k);
                        if (j != k) {
                                cnt += k * k;
                                phi += j * k;
                        }
                }
                ans = max(ans, res - cnt + phi - php);
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
