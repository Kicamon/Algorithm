// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 2e5 + 10;

vector<int> e[N];
bool vis[N];

bool dfs(int u, int fa) {
        // cout << u << endl;
        if (vis[u])
                return true;
        vis[u] = true;
        for (auto v : e[u]) {
                if (v == fa)
                        continue;
                bool res = dfs(v, u);
                if (res)
                        return true;
        }
        return false;
}

void solve() {
        int n;
        cin >> n;
        set<ll> ins;
        for (int i = 1, a; i <= n; ++i) {
                cin >> a;
                if (ins.count(1ll * i * n + a) || ins.count(1ll * a * n + i))
                        continue;
                ins.insert(1ll * i * n + a);
                ins.insert(1ll * a * n + i);
                e[i].push_back(a);
                e[a].push_back(i);
        }
        int res = 0, cnt = 0;
        for (int i = 1; i <= n; ++i)
                if (!vis[i]) {
                        res += dfs(i, -1), cnt++;
                        // cout << endl;
                }
        cout << min(res + 1, cnt) << ' ' << cnt << endl;
        for (int i = 1; i <= n; ++i)
                e[i].clear(), vis[i] = false;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t;
        cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
