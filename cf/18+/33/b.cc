void solve() {
        int n, k;
        cin >> n >> k;
        vector<array<int, 2>> a(n), b(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0];
                a[i][1] = i;
        }
        for (int i = 0; i < n; ++i) {
                cin >> b[i][0];
                b[i][1] = i;
        }
        sort(a.begin(), a.end()), sort(b.begin(), b.end());
        vector<int> c(n);
        for (int i = 0; i < n; ++i) {
                int j = a[i][1];
                c[j] = b[i][0];
        }
        for (int i = 0; i < n; ++i)
                cout << c[i] << ' ';
        cout << endl;
}

