void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i)
        cin >> a[i];
    sort(a.begin(), a.end());
    if (a[0] % 2)
        cout << "YES\n" << endl;
    else
    {
        for (int i = 0; i < n; ++i)
            if (a[i] % 2)
            {
                cout << "NO\n";
                return;
            }
        cout << "YES\n";
    }
}

