// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

int mx[4] = {-1, 0, 1, 0}, my[4] = {0, -1, 0, 1};

void solve()
{
    int n, m;
    cin >> n >> m;
    vector<vector<int>> a(n, vector<int>(m));
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < m; ++j)
            cin >> a[i][j];
    vector<vector<bool>> vis(n, vector<bool>(m));
    int res = 0, cnt = 0;
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < m; ++j)
        {
            if (a[i][j] && !vis[i][j])
            {
                queue<array<int, 2>> q;
                q.push({i, j});
                vis[i][j] = true;
                cnt += a[i][j];
                while (!q.empty())
                {
                    auto t = q.front();
                    q.pop();
                    for (int k = 0; k < 4; ++k)
                    {
                        int x = t[0] + mx[k], y = t[1] + my[k];
                        if (x < 0 || x >= n || y < 0 || y >= m)
                            continue;
                        if (vis[x][y] || !a[x][y])
                            continue;
                        vis[x][y] = true;
                        q.push({x, y});
                        cnt += a[x][y];
                    }
                }
                res = max(res, cnt), cnt = 0;
            }
        }
    }
    cout << res << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t;
    cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
