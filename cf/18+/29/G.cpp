// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int N = 2000, M = 1e6 + 10;
ll f[N][N];
array<int, 2> num[M];

void solve()
{
    int n;
    cin >> n;
    cout << f[num[n][0]][num[n][1]] << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    for (int i = 1, k = 1; k <= M; ++i)
    {
        for (int j = 1; j <= i && k <= M; ++j, ++k)
        {
            f[i][j] = 1ll * k * k;
            f[i][j] += f[i - 1][j] + f[i - 1][j - 1];
            if (i >= 2)
                f[i][j] -= f[i - 2][j - 1];
            num[k] = {i, j};
        }
    }

    int t;
    cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
