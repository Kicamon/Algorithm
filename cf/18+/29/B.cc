void solve() {
        int n;
        cin >> n;
        int res = 0;
        for (int i = 0, len = 0, c; i < n; ++i) {
                cin >> c;
                if (!c) {
                        len++;
                        res = max(res, len);
                } else
                        len = 0;
        }
        cout << res << endl;
}

