#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
const int inf = 0x3f3f3f3f;

void solve()
{
    int n;
    cin >> n;
    vector<int> f(4, inf);
    f[0] = 0;
    for (int i = 0; i < n; ++i)
    {
        int m, k = 0;
        cin >> m;
        string s;
        cin >> s;
        if (s[1] == '1')
            k |= 1;
        if (s[0] == '1')
            k |= 2;
        for (int j = 3; j >= k; --j)
            if ((j ^ k) < j)
                f[j] = min(f[j], f[(j ^ k)] + m);
    }
    if (f[3] == inf)
        cout << -1 << endl;
    else
        cout << f[3] << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t;
    cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
