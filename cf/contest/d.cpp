// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

const int N = 2e5 + 10;

int row[N], col[N];

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    int res = 0, cnt = 0;
    for (int _ = 0; _ < n; ++_) {
        int x, y;
        cin >> x >> y;
        row[x]++, col[y]++;
        if (row[x] == 1) {
            res += 2;
        }
        if (x == 1 || row[x - 1] < row[x]) {
            res++;
        } else {
            res--;
        }
        if (x == 2e5 || row[x + 1] < row[x]) {
            res++;
        } else {
            res--;
        }
        if (col[y] == 1) {
            cnt += 2;
        }
        if (y == 1 || col[y - 1] < col[y]) {
            cnt++;
        } else {
            cnt--;
        }
        if (y == 2e5 || col[y + 1] < col[y]) {
            cnt++;
        } else {
            cnt--;
        }
        cout << res << ' ' << cnt << endl;
    }

    return 0;
}
