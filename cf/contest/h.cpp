// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define int long long

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n, H, S;
    cin >> n >> H >> S;
    vector<vector<int>> dp(H + 1, vector<int>(S + 1));
    while (n--) {
        int h, s, w;
        cin >> h >> s >> w;
        if (h >= H) {
            continue;
        }
        if (s > S && s - S + h >= H) {
            continue;
        }
        for (int i = H; i > h; --i) {
            for (int j = S; ~j; --j) {
                if (j < s) {
                    int x = s - j;
                    if (i <= h + x) {
                        break;
                    }
                    dp[i][j] = max(dp[i][j], dp[i - h - x][0] + w);
                } else {
                    dp[i][j] = max(dp[i][j], dp[i - h][j - s] + w);
                }
            }
        }
    }
    cout << dp[H][S] << endl;

    return 0;
}
