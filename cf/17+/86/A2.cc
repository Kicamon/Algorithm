int input = 1;

void solve() {
        int n;
        cin >> n;
        array<int, 2> a[2]{};
        int idx = 1, len = 1, cnt = 2;
        for (int i = 0; i < n; ++i) {
                if (i == len) {
                        idx++;
                        len += cnt++;
                }
                a[idx >> 1 & 1][i & 1]++;
        }
        cout << a[0][0] << ' ' << a[0][1] << ' ' << a[1][0] << ' ' << a[1][1] << endl;
}

