int input = 1;

void solve()
{
	int n;
	cin >> n;
	string s;
	cin >> s;
	s = ' ' + s;
	vint vis(26);
	vint num(26);
	for(int i = 1;i <= n;++i)
		vis[s[i] - 'a'] ++;
	auto sum = [&]()
	{
		int res = 0;
		for(int i = 0;i < 26;++i)
		{
			res += num[i] != 0;
			res += vis[i] != 0;
		}
		return res;
	};
	int res = 0;
	for(int i = 1;i <= n;++i)
	{
		int t = s[i] - 'a';
		vis[t]--,num[t]++;
		res = max(res,sum());
	}
	cout << res << endl;
}

