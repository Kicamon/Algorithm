int input = 1;

void solve()
{
	int n;
	cin >> n;
	string s;
	cin >> s;
	int x = 0,y = 0;
	bool f = false;
	for(int i = 0;i < n;++i)
	{
		if(s[i] == 'L')
			x--;
		else if(s[i] == 'R')
			x++;
		else if(s[i] == 'D')
			y--;
		else 
			y++;
		if(x == 1 && y == 1)
			f = true;
	}
	cout << (f?"YES":"NO") << endl;
}

