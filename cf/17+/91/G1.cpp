void solve()
{
	int n,m;
	cin >> n >> m;
	vint a(n + 1);
	for(int i = 1;i <= n;++i)
	{
		cin >> a[i];
		a[i] += i;
	}
	sort(a.begin() + 1,a.end());
	for(int i = 1;i <= n;++i)
	{
		if(m < a[i])
		{
			cout << i - 1 << endl;
			return;
		}
		m -= a[i];
	}
	cout << n << endl;
}

