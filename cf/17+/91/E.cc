void solve() {
        std::vector<int> a;
        ll n, cnt = 0, mi = 0x7f7f7f7f, ans = 0;
        cin >> n;
        for (int i = 1; i <= n; i++) {
                ll now;
                cin >> now;
                cnt += now < 0;
                now = now < 0 ? -now : now;
                mi = std::min(mi, now);
                ans += now;
        }
        printf("%lld\n", cnt & 1 ? ans - mi - mi : ans);
}

