void solve()
{
	int n;
	cin >> n;
	string s;
	cin >> s;
	if(n == 0)
	{
		cout << 0 << endl;
		return;
	}
	int l = 0,r = n - 1;
	while(l <= r && s[l] != s[r])
		l++,r--;
	if(r < l)
		cout << 0 << endl;
	else 
		cout << r - l + 1 << endl;
}

