int input = 1;

void solve() {
        int n;
        cin >> n;
        int m = n * 2 - 2;
        string x, s[2]{};
        for (int i = 0, idx = 0; i < m; ++i) {
                cin >> x;
                if (x.length() == n - 1)
                        s[idx++] = x;
        }
        for (int i = 0; i < n - 1; ++i) {
                if (s[0][i] != s[1][n - 2 - i]) {
                        cout << "No\n";
                        return;
                }
        }
        cout << "Yes\n";
}

