//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;
const int inf = 1e9 + 7;
#define ai2 array<int, 2>
#define al2 array<long long, 2>

void solve() {
        int n, s;
        cin >> n >> s;
        vector<array<int, 2>> a(n);
        for (int i = 0, c; i < n; ++i) {
                cin >> c;
                if (!i)
                        a[i] = { inf, c };
                else if (i == n - 1)
                        a[i] = { c, inf };
                else if (c >= 2 * s)
                        a[i] = { s, c - s };
                else
                        a[i] = { max(0, c - s), min(s, c) };
        }
        vector<al2> f(n);
        for (int i = 1; i < n; ++i) {
                f[i][0] = min(f[i - 1][0] + 1ll * a[i - 1][1] * a[i][0],
                              f[i - 1][1] + 1ll * a[i - 1][0] * a[i][0]);
                f[i][1] = min(f[i - 1][0] + 1ll * a[i - 1][1] * a[i][1],
                              f[i - 1][1] + 1ll * a[i - 1][0] * a[i][1]);
        }
        cout << f[n - 1][0] << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        //clock_t start, finish;
        //start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        //finish = clock();
        //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
