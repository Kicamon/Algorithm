//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve() {
        int n;
        cin >> n;
        vint a(n);
        for (int i = 0; i < n; ++i)
                cin >> a[i];
        int l = 1, r = n;
        for (int i = 0, j = n - 1; i < j;) {
                if (a[i] != l && a[i] != r && a[j] != l && a[j] != r) {
                        cout << i + 1 << ' ' << j + 1 << endl;
                        return;
                }
                if (a[i] == l)
                        i++, l++;
                else if (a[i] == r)
                        i++, r--;
                if (a[j] == l)
                        j--, l++;
                else if (a[j] == r)
                        j--, r--;
        }
        cout << -1 << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        //clock_t start, finish;
        //start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        //finish = clock();
        //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
