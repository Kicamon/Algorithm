#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
#define int long long

void solve()
{
	int n;
	cin >> n;
	vint q(n),p(n),qn(n),pn(n);
	for(int i = 0;i < n;++i)
	{
		cin >> q[i];
		q[i]--;
		qn[q[i]] = i;
	}
	for(int i = 0;i < n;++i)
	{
		cin >> p[i];
		p[i]--;
		pn[p[i]] = i;
	}
	auto get = [&](int x)
	{
		return x * (x + 1) >> 1;
	};
	int l = n - 1,r = 0;
	ll res = 0;
	for(int i = 0;i < n;++i)
	{
		int x = qn[i],y = pn[i];
		if(x > y)
			swap(x,y);
		if(i)
		{
			if(r < x)	
				res += (l + 1)  * (x - r);
			else if(y < l)
				res += (l - y) * (n - r);
			else if(x < l && r < y)
				res += (l - x) * (y - r);
		}
		else 
			res += get(x) + get(n - 1 - y) + get(y - x - 1);
		l = min(l,x),r = max(r,y);
	}
	cout << res + 1 << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
