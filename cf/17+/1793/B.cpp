int input = 1;

void solve()
{
	ll n,m;
	cin >> n >> m;
	int len = n - m << 1;
	cout << len << endl;
	for(int i = 0,j = m,aj = 1;i < len;++i)
	{
		cout << j << ' ';
		if(j == n)
			aj = -1;
		j += aj;
	}
	cout << endl;
}

