//#pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve() {
        int n;
        cin >> n;
        vector<ll> a(n);
        for (int i = 0; i < n; ++i)
                cin >> a[i];
        sort(all(a));
        for (int i = 1; i < n; ++i) {
                if (a[i] == a[i - 1]) {
                        cout << "NO\n";
                        return;
                }
        }
        for (int i = 2; i * 2 <= n; ++i) {
                vint m(i);
                for (ll x : a)
                        m[x % i]++;
                if (*min_element(all(m)) >= 2) {
                        cout << "NO\n";
                        return;
                }
        }
        cout << "YES\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
