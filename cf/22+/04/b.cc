#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1), b(n + 1);
        int ans = 0;
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                b[i] = b[i - 1];
                if (a[i] >= a[b[i]]) {
                        b[i] = i;
                        ans++;
                }
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
