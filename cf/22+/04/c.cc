#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
using ll = long long;

void solve() {
        ll a, b, c, m;
        cin >> a >> b >> c >> m;
        vector<ll> ans(3);
        ll t = a * b / gcd(a, b);
        t = t * c / gcd(t, c);
        ans[0] = m / a * 6 - m / lcm(a, b) * 3 - m / lcm(a, c) * 3 + m / t * 2;
        ans[1] = m / b * 6 - m / lcm(a, b) * 3 - m / lcm(b, c) * 3 + m / t * 2;
        ans[2] = m / c * 6 - m / lcm(a, c) * 3 - m / lcm(b, c) * 3 + m / t * 2;
        cout << ans[0] << ' ' << ans[1] << ' ' << ans[2] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
