#include <iostream>
#include <numeric>
#include <vector>
using namespace std;

void solve() {
        vector<int> a(7);
        for (int i = 0; i < 7; i++) {
                cin >> a[i];
        }
        cout << *max_element(a.begin(), a.end()) * 2 - accumulate(a.begin(), a.end(), 0) << '\n';
}

