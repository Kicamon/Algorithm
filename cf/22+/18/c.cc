#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(3 * n);
        int m = n * 3;
        for (int i = 0, j = 1, k = m; i < m; i += 3, j++, k -= 2) {
                a[i] = j;
                a[i + 1] = k - 1;
                a[i + 2] = k;
        }
        for (int i = 0; i < m; ++i) {
                cout << a[i] << " \n"[i == m - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
