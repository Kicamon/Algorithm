#include <iostream>
#include <set>
#include <vector>
using namespace std;
using ll = long long;
const int N = 1e4 + 10;

vector<int> primes;

void solve() {
        int n;
        cin >> n;
        for (int i = 0; i < n; ++i) {
                cout << primes[i] * primes[i + 1] << " \n"[i == n - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int i = 2;
        set<int> vis;
        while ((int)primes.size() < N) {
                if (!vis.count(i)) {
                        primes.push_back(i);
                }
                for (int j : primes) {
                        if (i * j >= N) {
                                break;
                        }
                        vis.insert(i * j);
                        if (i % j == 0) {
                                break;
                        }
                }
                i++;
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
