#include <iostream>
#include <map>
#include <vector>
using namespace std;
using ll = long long;
const int mod = 676767677;

void solve() {
        int n, m;
        cin >> n >> m;
        map<int, int> num;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                num[x]++;
        }
        vector<map<int, ll>> dp(n + 1);
        dp[0][0] = 1;
        int t = num[0];
        for (int i = 1; i <= n; ++i) {
                for (int j = i; j <= t; ++j) {
                        dp[i][j] = (dp[i][j - 1] + dp[i - 1][j - 1]) % mod;
                }
                t += num[i];
        }
        ll ans = 0;
        for (int i = m; i <= n; ++i) {
                for (auto [_, x] : dp[i]) {
                        ans = (ans + x) % mod;
                }
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
