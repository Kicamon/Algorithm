#include <iostream>
using namespace std;

void solve() {
        int x, y;
        cin >> x >> y;
        if (x > y || (y % 2 == 0 && x == 0)) {
                cout << "NO\n";
                return;
        }
        cout << "YES\n";
        if ((x + y) % 2 == 0) {
                x--;
        } else {
                y--;
        }
        int idx = 2;
        while (x--) {
                cout << 1 << ' ' << idx++ << '\n';
                cout << idx - 1 << ' ' << idx++ << '\n';
                y--;
        }
        while (y--) {
                cout << 1 << ' ' << idx++ << '\n';
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
