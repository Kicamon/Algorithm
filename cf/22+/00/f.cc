#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
using namespace std;
using ll = long long;

void solve() {
        int n, m;
        cin >> n >> m;
        map<int, vector<ll>> a;
        map<int, vector<ll>> b;
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                a[y].push_back(x);
        }
        ll maxn = 0;
        for (auto &[x, y] : a) {
                sort(y.rbegin(), y.rend());

                b[x] = y;
                for (int i = 1; i < (int)b[x].size(); ++i) {
                        b[x][i] += b[x][i - 1];
                }

                ll temp = 0;
                int ed = min(x + 1, (int)y.size());
                for (int i = 0; i < ed; ++i) {
                        temp += y[i];
                }
                maxn = max(maxn, temp);
        }

        for (int i = 0, x, y; i < m; ++i) {
                cin >> x >> y;
                int l = 0, r = a[y].size();
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (a[y][mid] > x) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                ll ans;
                if (l == a[y].size() || l > y) {
                        ans = maxn;
                } else {
                        ans = b[y][y] - a[y][l + 1] + x;
                }
                cout << ans << ' ';
                // cout << "---\n";
                // cout << l << ' ' << x << ' ';
                // cout << ans << '\n';
        }
        cout << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
