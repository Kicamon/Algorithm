#include <iostream>
using namespace std;

void solve() {
        int n;
        cin >> n;
        int maxn = 0, num = 0;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x > maxn) {
                        maxn = x;
                        num = 1;
                } else if (x == maxn) {
                        num++;
                }
        }
        cout << num << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
