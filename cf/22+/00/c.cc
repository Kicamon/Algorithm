#include <iostream>
#include <stack>
using namespace std;

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        stack<char> st;
        for (int i = 0; i < n; ++i) {
                if (st.empty() || s[i] != st.top()) {
                        st.push(s[i]);
                } else {
                        st.pop();
                }
        }
        if (st.empty()) {
                cout << "YES\n";
        } else {
                cout << "NO\n";
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
