#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        if (is_sorted(a.begin(), a.end()) && a.back() >= *a.begin()) {
                cout << n << '\n';
        } else {
                cout << 1 << '\n';
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
