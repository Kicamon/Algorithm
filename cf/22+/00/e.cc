#include <algorithm>
#include <iostream>
#include <set>
#include <vector>
using namespace std;
const int N = 1e6 + 10;

vector<int> primes;
vector<set<int>> st(N);
void prime() {
        vector<bool> vis(N);
        for (int i = 2; i < N; ++i) {
                if (!vis[i]) {
                        primes.push_back(i);
                        st[i].insert(i);
                }
                for (int j : primes) {
                        if (i * j >= N) {
                                break;
                        }
                        vis[i * j] = true;
                        st[i * j] = st[i];
                        st[i * j].insert(j);
                        if (i % j == 0) {
                                break;
                        }
                }
        }
}

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        if (is_sorted(a.begin(), a.end()) && a[0] <= a.back()) {
                cout << "Bob\n";
                return;
        }
        int maxn = 0;
        for (int i = 0; i < n; ++i) {
                if (*st[a[i]].begin() != *st[a[i]].rbegin()) {
                        cout << "Alice\n";
                        return;
                }
                if (*st[a[i]].begin() < maxn) {
                        cout << "Alice\n";
                        return;
                }
                maxn = *st[a[i]].begin();
        }
        cout << "Bob\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        prime();
        st[1].insert(1);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
