#include <iostream>
using namespace std;

void solve() {
        int n;
        string s;
        cin >> n >> s;
        int minn = 0, maxn = 0;
        for (int i = 1; i < n - 1; ++i) {
                if (s[i - 1] == '1' && s[i + 1] == '1') {
                        s[i] = '1';
                }
        }
        for (int i = 0; i < n; ++i) {
                maxn += s[i] == '1';
        }
        for (int i = 1; i < n - 1; ++i) {
                if (s[i - 1] == '1' && s[i + 1] == '1') {
                        s[i] = '0';
                }
        }
        for (int i = 0; i < n; ++i) {
                minn += s[i] == '1';
        }
        cout << minn << ' ' << maxn << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
