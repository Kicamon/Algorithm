#include <iostream>
#include <set>
#include <vector>
using namespace std;

void solve() {
        int n, m, l;
        cin >> n >> m >> l;
        vector<int> a(n + 1);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        a[n] = l - a[n - 1];
        for (int i = 0, x = 0, y; i < n; ++i) {
                y = a[i];
                a[i] = y - x;
                x = y;
        }
        multiset<int> st;
        for (int i = 0; i < min(m, n + 1); ++i) {
                st.insert(0);
        }
        for (int i = 0; i < n; ++i) {
                while (a[i]--) {
                        auto it = st.begin();
                        st.insert(*it + 1);
                        st.erase(it);
                }

                st.erase(--st.end());
                if (st.size() < min(m, n - i)) {
                        st.insert(0);
                }
        }
        cout << *st.rbegin() + a[n] << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
