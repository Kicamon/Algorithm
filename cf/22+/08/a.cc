#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n * n + 1);
        int maxn = 0;
        for (int i = 0, x; i < n; ++i) {
                for (int j = 0; j < n; ++j) {
                        cin >> x;
                        a[x]++;
                        maxn = max(maxn, a[x]);
                }
        }
        if (maxn > n * n - n) {
                cout << "NO\n";
        } else {
                cout << "YES\n";
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
