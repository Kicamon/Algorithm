#include <iomanip>
#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<pair<int, long double>> a(n + 1);
        for (int i = 0, x, y; i < n; ++i) {
                cin >> x >> y;
                a[i] = { x, 1 - y * 0.01 };
        }
        long double ans = 0;
        for (int i = n - 1; ~i; --i) {
                ans = max(ans, a[i].first + ans * a[i].second);
        }
        cout << fixed << setprecision(10) << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
