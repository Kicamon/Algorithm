#include <functional>
#include <iostream>
#include <queue>
#include <array>
#include <vector>
using namespace std;

void solve() {
        int n, k, p, m;
        cin >> n >> k >> p >> m;
        queue<array<int, 2>> q;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                q.push({ i == p - 1 ? 0 : 1, x });
        }
        priority_queue<array<int, 2>, vector<array<int, 2>>, greater<array<int, 2>>> pq;
        while ((int)pq.size() < k) {
                pq.push(q.front());
                q.pop();
        }
        int ans = 0;
        while (m > 0) {
                auto t = pq.top();
                pq.pop();
                q.push(t);

                if (t[0] == 0 && m >= t[1]) {
                        ans++;
                }
                m -= t[1];

                pq.push(q.front());
                q.pop();
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
