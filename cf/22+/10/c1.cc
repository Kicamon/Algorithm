#include <iostream>
#include <numeric>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        for (int i = 0; i < n; ++i) {
                cin >> b[i];
        }
        if (n == 1) {
                cout << (a[0] != 1) << '\n';
                return;
        }
        if (n == 2) {
                int t = gcd(a[0], a[1]);
                cout << 2 - (t == a[0]) - (t == a[1]) << '\n';
                return;
        }
        int ans = 0;
        for (int i = 1; i < n - 1; i++) {
                int pr = gcd(a[i], a[i - 1]);
                int ne = gcd(a[i], a[i + 1]);
                if (lcm(pr, ne) < a[i]) {
                        ans++;
                        a[i] = lcm(pr, ne);
                }
        }
        ans += gcd(a[0], a[1]) < a[0];
        ans += gcd(a[n - 1], a[n - 2]) < a[n - 1];

        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
