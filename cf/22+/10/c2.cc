#include <iostream>
#include <vector>
using namespace std;
const int N = 1e5 + 10;
vector<int> prime;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        for (int i = 0; i < n; ++i) {
                cin >> b[i];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        vector<bool> vis(N);
        for (int i = 2; i < N; ++i) {
                if (!vis[i]) {
                        prime.push_back(i);
                }
                for (int j : prime) {
                        if (i * j >= N) {
                                break;
                        }
                        vis[i * j] = true;
                        if (i % j == 0) {
                                break;
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
