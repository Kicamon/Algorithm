#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<bool> vis(n + 1);
        int ans = 0;
        for (int i = 1, x; i <= n; ++i) {
                cin >> x;
                if (!vis[i]) {
                        ans++;
                }
                vis[x] = true;
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
