#include <algorithm>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        iota(a.begin(), a.end(), 1);
        reverse(a.begin(), a.end());
        for (int x : a) {
                cout << x << ' ';
        }
        cout << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
