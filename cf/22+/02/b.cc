#include <iostream>
using namespace std;

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        if (n % 2 && s[0] == 'b') {
                cout << "NO\n";
                return;
        }
        for (int i = n % 2; i + 1 < n; i += 2) {
                if (s[i] == '?' || s[i + 1] == '?') {
                        continue;
                }
                if (s[i] == s[i + 1]) {
                        cout << "NO\n";
                        return;
                }
        }
        cout << "YES\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
