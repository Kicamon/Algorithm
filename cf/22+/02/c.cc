#include <iostream>
#include <map>
#include <vector>
using namespace std;
using ll = long long;
const int N = 3e5 + 10;

ll num[N], o[N];

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1);
        map<int, int> mp;
        ll ans = 0;
        for (int i = 1; i <= n; ++i) {
                ans += i;
                cin >> a[i];
                int t = mp[a[i - 1]];
                ans += num[i - t];
                mp[a[i]] = i;
        }
        cout << ans << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        num[1] = o[1] = 1;
        for (int i = 2; i < N; ++i) {
                num[i] = num[i - 1] + i;
                o[i] = (1ll * i * i * i + 6ll * i * i + 8 * i) / 48;
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
