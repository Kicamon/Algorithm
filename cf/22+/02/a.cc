#include <iostream>
using namespace std;
using ll = long long;

void solve() {
        ll x, y;
        cin >> x >> y;
        int a;
        a = y * (y > 0 ? 2 : -4);
        if (a > x || (x - a) % 3) {
                cout << "NO\n";
        } else {
                cout << "YES\n";
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
