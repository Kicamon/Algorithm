// #pragma GCC optimize(2)
#include <algorithm>
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> k(n);
    for (int i = 0; i < n; ++i)
        cin >> k[i];
    sort(all(k));
    for (int i = 0; i < m; ++i)
    {
        int a, b, c;
        cin >> a >> b >> c;
        int l = -1, r = k.size() - 1;
        while (l < r)
        {
            int mid = l + r + 1 >> 1;
            if (k[mid] < b)
                l = mid;
            else
                r = mid - 1;
        }
        if (r != -1)
        {
            if (1ll * (b - k[r]) * (b - k[r]) < 4ll * a * c)
            {
                cout << "YES\n";
                cout << k[r] << endl;
                continue;
            }
        }
        l = 0, r = k.size();
        while (l < r)
        {
            int mid = l + r >> 1;
            if (k[mid] < b)
                l = mid + 1;
            else
                r = mid;
        }
        if (r != k.size())
        {
            if (1ll * (b - k[r]) * (b - k[r]) < 4ll * a * c)
            {
                cout << "YES\n";
                cout << k[r] << endl;
                continue;
            }
            else
            {
                cout << "NO\n";
                continue;
            }
        }
        else
            cout << "NO\n";
    }
    cout << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
