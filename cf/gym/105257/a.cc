/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int a;
        cin >> a;
        string s = "";
        while (a) {
                int res = a % 10;
                a /= 10;
                string stemp = "---";
                if (res >= 4) {
                        stemp[2] = 'r';
                        res -= 4;
                }
                if (res >= 2) {
                        stemp[1] = 'w';
                        res -= 2;
                }
                if (res) {
                        stemp[0] = 'x';
                        res--;
                }
                s += stemp;
        }
        while (s.length() < 9) {
                s += '-';
        }
        reverse(all(s));
        cout << s << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
