/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        int max_num = 0, idx = -1;
        for (int i = 0; i < n; i++) {
                cin >> a[i];
                if (a[i] > max_num) {
                        max_num = a[i];
                        idx = i;
                } else if (a[i] == max_num && !(idx % 2)) {
                        idx = i;
                }
        }
        max_num += n + (1 - idx % 2) >> 1;
        cout << max_num << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
