/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <numeric>
#include <tuple>
#include <utility>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int get(int len) {
        len--;
        int res = 0;
        for (int i = 0; i < len; i++) {
                res += 1 << i;
        }
        return res;
}

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1);
        iota(all(a), 0);
        if (n & 1) {
                cout << n << endl;
                swap(a[n - 2], a[3]);
                swap(a[n - 3], a[1]);
                for (int i = 1; i <= n; i++) {
                        cout << a[i] << ' ';
                }
                cout << endl;
                return;
        }
        int len = 0;
        while ((1 << len) <= n) {
                len++;
        }
        cout << (1 << len) - 1 << endl;
        int x = get(len);
        swap(a[n - 1], a[x]);
        swap(a[n - 2], a[x - 1]);
        swap(a[n - 4], a[1]);
        for (int i = 1; i <= n; i++) {
                cout << a[i] << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
