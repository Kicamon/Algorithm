/* #pragma GCC optimize(2) */
#include <iostream>
#include <map>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

void solve() {
        int n, k;
        cin >> n >> k;
        map<int, int> mp;
        for (int i = 0, x, y; i < k; i++) {
                cin >> x >> y;
                mp[x] += y;
        }
        priority_queue<int, vector<int>, less<int> > pq;
        for (auto x : mp) {
                pq.push(x.second);
        }
        int sum = 0;
        while (n && !pq.empty()) {
                n--;
                sum += pq.top();
                pq.pop();
        }
        cout << sum << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
