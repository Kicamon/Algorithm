#include <cstdlib>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        for (int i = 1; i < n; i++) {
                int temp = abs(a[i] - a[i - 1]);
                if (temp != 5 && temp != 7) {
                        cout << "NO" << endl;
                        return;
                }
        }
        cout << "YES" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
