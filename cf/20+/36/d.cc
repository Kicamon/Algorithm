/* #pragma GCC optimize(2) */
#include <algorithm>
#include <cstddef>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

string str = "1543";

void solve() {
        int n, m;
        cin >> n >> m;
        vector<string> a(n);
        for (int i = 0; i < n; i++) {
                cin >> a[i];
        }
        int t = min(n, m) >> 1;
        int ans = 0;
        for (int k = 0, idx = 0; k < t; k++) {
                string s;
                for (int j = k; j < m - k; j++) {
                        s += a[k][j];
                }
                for (int i = k + 1; i < n - k; i++) {
                        s += a[i][m - k - 1];
                }
                for (int j = m - k - 2; j >= k; j--) {
                        s += a[n - k - 1][j];
                }
                for (int i = n - k - 2; i > k; i--) {
                        s += a[i][k];
                }
                for (int i = 0; i < 3; i++) {
                        s += s[i];
                }
                for (size_t i = 0; i < s.size(); i++) {
                        if (s[i] == str[idx]) {
                                idx++;
                        } else {
                                idx = 0;
                        }
                        if (idx == 4) {
                                ans++;
                                idx = 0;
                        }
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
