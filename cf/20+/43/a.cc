/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll n;
        cin >> n;
        int l = 0, r = 63;
        while (l < r) {
                int mid = (l + r + 1) >> 1;
                if (1ll << 2 * mid > n) {
                        r = mid - 1;
                } else {
                        l = mid;
                }
        }
        cout << (1 << l) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
