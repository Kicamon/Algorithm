/* #pragma GCC optimize(2) */
#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

const int N = 2e5 + 1;
int a[N];
struct Node {
        int max_val, min_val;
} node[N << 2];

void pushup(int u) {
        Node &rt = node[u], &l = node[u << 1], &r = node[u << 1 | 1];
        rt.max_val = max(l.max_val, r.max_val);
        rt.min_val = min(l.min_val, r.min_val);
}

void build(int u, int l, int r) {
        if (l == r) {
                node[u].max_val = node[u].min_val = a[l];
                return;
        }
        int mid = (l + r) >> 1;
        build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
        pushup(u);
}

void modify(int u, int l, int r, int q, int x) {
        if (l == r) {
                node[u].max_val = node[u].min_val = x - l;
                return;
        }
        int mid = (l + r) >> 1;
        if (q <= mid) {
                modify(u << 1, l, mid, q, x);
        } else {
                modify(u << 1 | 1, mid + 1, r, q, x);
        }
        pushup(u);
}

void solve() {
        int n, p;
        cin >> n >> p;
        for (int i = 1; i <= n; i++) {
                cin >> a[i];
        }
        build(1, 1, n);
        cout << node[1].max_val - node[1].min_val << endl;
        while (p--) {
                int q, x;
                cin >> q >> x;
                modify(1, 1, n, q, x);
                cout << node[1].max_val - node[1].min_val << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
