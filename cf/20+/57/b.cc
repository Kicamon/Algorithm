/* #pragma GCC optimize(2) */
#include <iostream>
#include <map>
#include <queue>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        map<int, int> mp;
        for (int i = 0, x; i < n; i++) {
                cin >> x;
                mp[x]++;
        }
        priority_queue<int, vector<int>, greater<int> > q;
        for (auto x : mp) {
                q.push(x.second);
        }
        int ans = mp.size();
        while (k && !q.empty()) {
                if (k >= q.top()) {
                        ans--;
                        k -= q.top();
                        q.pop();
                } else {
                        break;
                }
        }
        cout << (ans ? ans : 1) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
