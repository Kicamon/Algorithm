#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        if (n == 1) {
                cin >> n;
                cout << 1 << endl;
                return;
        }
        if (n == 2) {
                int x, y;
                cin >> x >> y;
                cout << y - x << endl;
                return;
        }
        vector<int> a;
        int k[2], idx = 0;
        cin >> k[idx];
        for (int i = 1; i < n; i++) {
                idx ^= 1;
                cin >> k[idx];
                a.push_back(k[idx] - k[idx ^ 1]);
        }
        if (n % 2) {
                int max_val = *max_element(all(a));
                int num = count(all(a), max_val);
                sort(all(a));
                a.pop_back();
                cout << a.back() << endl;
                return;
        }
        int max_ele = 0;
        for (size_t i = 0; i < a.size(); i++) {
                if (i % 2) {
                        continue;
                }
                max_ele = max(max_ele, a[i]);
        }
        cout << max_ele << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
