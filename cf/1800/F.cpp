// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;

void solve()
{
    int n;
    cin >> n;
    vector<string> s(n);
    for (int i = 0; i < n; ++i)
        cin >> s[i];
    vector<array<int, 26>> cnt(n);
    vector<int> mask(n);
    vint f(1 << 26);
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < (int)s[i].size(); ++j)
            cnt[i][s[i][j] - 'a']++;
        for (int j = 0; j < 26; ++j)
            mask[i] |= (cnt[i][j] % 2) << j;
    }
    ll ans = 0;
    for (int c = 0; c < 26; ++c)
    {
        for (int i = 0; i < n; ++i)
        {
            if (cnt[i][c] == 0)
            {
                ans += f[(1 << 26) - 1 - (1 << c) - mask[i]];
                f[mask[i]]++;
            }
        }
        for (int i = 0; i < n; ++i)
        {
            if (cnt[i][c] == 0)
                f[mask[i]]--;
        }
    }
    cout << ans << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int t = 1;
    if (input)
        cin >> t;
    while (t--)
        solve();

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
