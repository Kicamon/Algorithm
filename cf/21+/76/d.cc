/* #pragma GCC optimize(2) */
#include <algorithm>
#include <functional>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define all(x) (x).begin(), (x).end()
using ll = long long;

void solve() {
        int n, m;
        cin >> n >> m;
        vector<array<ll, 2>> a(n + 1);
        vector<vector<int>> g(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i][0];
                a[i][1] = i;
        }
        for (int i = 0, u, v; i < m; ++i) {
                cin >> u >> v;
                g[u].push_back(v);
        }
        sort(all(a));

        function<int(ll)> find = [&](int x) {
                int l = 1, r = n;
                while (l < r) {
                        int mid = (l + r) >> 1;
                        if (a[mid][0] < x) {
                                l = mid + 1;
                        } else {
                                r = mid;
                        }
                }
                return l;
        };
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
