#include <iostream>
#include <queue>
#include <vector>
using namespace std;
using ll = long long;
const int inf = 0x3f3f3f3f;

void solve() {
        int n;
        cin >> n;
        priority_queue<int, vector<int>, less<int>> q, p;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                if (x & 1) {
                        q.push(x);
                } else {
                        p.push(x);
                }
        }
        if (q.empty()) {
                for (int i = 1; i <= n; ++i) {
                        cout << 0 << " \n"[i == n];
                }
                return;
        }
        if (p.empty()) {
                int ans = q.top();
                for (int i = 1; i <= n; ++i) {
                        cout << (i & 1 ? ans : 0) << " \n"[i == n];
                }
                return;
        }
        vector<ll> ans;
        int tmp;
        ans.push_back(q.top());
        q.pop();
        while (!p.empty()) {
                ans.push_back(ans.back() + p.top());
                tmp = p.top();
                p.pop();
        }
        ll temp = ans.back();
        for (int i = ans.size() + 1, j = 1; i < n; ++i, j ^= 1) {
                ans.push_back(temp - j * tmp);
        }
        if (q.size() & 1) {
                ans.push_back(0);
        } else {
                ans.push_back(temp);
        }
        for (int i = 0; i < n; ++i) {
                cout << ans[i] << " \n"[i == n - 1];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
