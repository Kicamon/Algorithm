/* #pragma GCC optimize(2) */
#include <algorithm>
#include <array>
#include <iostream>
#include <utility>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        ll ans = 0;
        cin >> n >> k;
        vector<array<ll, 2>> a(n);
        for (auto &x : a) {
                cin >> x[0];
        }
        for (auto &x : a) {
                cin >> x[1];
                if (x[1] < x[0]) {
                        swap(x[0], x[1]);
                }
                ans += x[1] - x[0];
        }
        sort(all(a));
        for (int i = 1; i < n; ++i) {
                auto x = a[i - 1], y = a[i];
                if (x[1] >= y[0]) {
                        cout << ans << endl;
                        return;
                }
        }
        ll res = 1e10;
        for (int i = 1; i < n; ++i) {
                auto x = a[i - 1], y = a[i];
                res = min(res, (y[0] - x[1]) << 1);
        }
        cout << ans + res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
