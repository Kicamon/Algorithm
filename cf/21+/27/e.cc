#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<array<int, 2> > cw(n + 1);
        vector<vector<int> > g(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> cw[i][0];
        }
        for (int i = 1; i <= n; ++i) {
                cin >> cw[i][1];
        }
        for (int i = 1, a, b; i < n; ++i) {
                cin >> a >> b;
                g[a].push_back(b);
                g[b].push_back(a);
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
