#include <iostream>
using namespace std;
#define endl '\n'
// #define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, x;
        cin >> n >> x;
        x--;
        string s;
        cin >> s;
        if (x == 0 || x == n - 1) {
                cout << 1 << endl;
                return;
        }
        int l = -1, r = n;
        for (int i = 0; i < n; ++i) {
                if (s[i] == '#') {
                        if (i < x) {
                                l = i;
                        } else if (r == n) {
                                r = i;
                        }
                }
        }
        if (l == -1 && r == n) {
                cout << 1 << endl;
                return;
        }
        int ansl = n, ansr = n;
        if (l + 1 < n - r) {
                ansr = n - r + 1;
        } else {
                ansl = l + 2;
        }
        cout << min(min(x + 1, n - x), min(ansl, ansr)) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
