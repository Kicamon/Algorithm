/* #pragma GCC optimize(2) */
#include <algorithm>
#include <array>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int get(int x, int y, int z) {
        int i = 0;
        while (i == x || i == y || i == z) {
                i++;
        }
        return i;
}

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        sort(all(a));
        int temp = -1;
        for (int x : a) {
                if (!x) {
                        cout << "No" << endl;
                        return;
                }
                if (temp != -1 && temp != x) {
                        cout << "No" << endl;
                        return;
                }
                if (x != -1) {
                        temp = x;
                }
        }
        cout << "Yes" << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
