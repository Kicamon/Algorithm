/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(4);
        ll res = 0;
        for (int i = 0; i < n; ++i) {
                for (int j = 0; j < 4; ++j) {
                        cin >> a[j];
                }
                if (a[0] > a[2]) {
                        res += a[0] - a[2];
                }
                if (a[1] > a[3]) {
                        res += min(a[0], a[2]) + a[1] - a[3];
                }
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
