#include <algorithm>
#include <array>
#include <iostream>
#include <vector>
using namespace std;

void solve() {
        int n, m;
        cin >> n;
        m = n / 2;
        vector<array<int, 3>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0] >> a[i][1];
                a[i][2] = i + 1;
        }
        sort(a.begin(), a.end());
        sort(a.begin(), a.begin() + m, [&](auto x, auto y) { return x[1] < y[1]; });
        sort(a.begin() + m, a.end(), [&](auto x, auto y) { return x[1] > y[1]; });
        for (int i = 0; i < m; ++i) {
                cout << a[i][2] << ' ' << a[i + m][2] << '\n';
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
