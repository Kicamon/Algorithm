/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

struct DSU {
        vector<int> f, siz;

        DSU(int n) {
                f.resize(n);
                iota(all(f), 0);
                siz.assign(n, 1);
        }

        int find(int x) {
                while (x != f[x]) {
                        x = f[x] = f[f[x]];
                }
                return x;
        }

        bool merge(int x, int y) {
                x = find(x);
                y = find(y);
                if (x == y) {
                        return false;
                }
                f[y] = x;
                siz[x] += siz[y];
                return true;
        }

        bool same(int x, int y) {
                return find(x) == find(y);
        }

        bool size(int x) {
                return siz[find(x)];
        }
};

void solve() {
        int n;
        cin >> n;
        DSU dsu(n << 1 | 1);
        vector<int> ans;
        for (int i = 1, x, y; i <= n; ++i) {
                cin >> x >> y;
                if (dsu.merge(x, y)) {
                        ans.push_back(i);
                }
        }
        cout << (int)ans.size() << endl;
        for (int x : ans) {
                cout << x << " \n"[x == ans.back()];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
