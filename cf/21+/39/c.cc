/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll k, x;
        cin >> k >> x;
        ll y = (1ll << (k + 1)) - x;
        vector<int> ans;
        while (y != x) {
                if (x < y) {
                        ans.push_back(1);
                        y -= x;
                        x *= 2;
                } else {
                        ans.push_back(2);
                        x -= y;
                        y *= 2;
                }
        }
        cout << ans.size() << endl;
        for (int t : vector<int>(ans.rbegin(), ans.rend())) {
                cout << t << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
