#include <iostream>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m;
        cin >> n >> m;
        if (n < m) {
                swap(n, m);
        }
        if (n == m) {
                cout << 0 << endl;
        } else if (n % m == 0) {
                cout << 1 << endl;
        } else {
                cout << 2 << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
