/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

void solve() {
        int n, m;
        cin >> n >> m;
        vector<int> a(n);
        ll ans = 0;
        for (int &x : a) {
                cin >> x;
        }
        sort(all(a));
        for (int i = n - 1, j = m; ~i && ~j; --i, --j) {
                ans += 1ll * a[i] * j;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
