/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<vector<int> > g(n);
        vector<int> dep(n), dep_num(n);
        for (int i = 1, x; i < n; ++i) {
                cin >> x;
                g[x - 1].push_back(i);
        }

        function<void(int, int)> init = [&](int u, int d) {
                dep[u] = d;
                dep_num[d]++;
                for (int v : g[u]) {
                        init(v, d + 1);
                }
        };
        init(0, 0);
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
