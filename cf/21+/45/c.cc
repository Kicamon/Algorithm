/* #pragma GCC optimize(2) */
#include <iostream>
#include <map>
#include <numeric>
#include <stack>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        s = ' ' + s;
        vector<int> a(n + 1), f(n + 1);
        for (int i = 1; i <= n; ++i) {
                a[i] = s[i] == 'a' ? 1 : -1;
        }
        if (accumulate(all(a), 0) < 0) {
                for (int &x : a) {
                        x = -x;
                }
        }
        map<int, stack<int> > mp;
        mp[0].push(0);
        for (int i = 1, temp = 0; i <= n; ++i) {
                temp += a[i];
                mp[temp].push(i);
        }
        int ans = n;
        for (int i = n, temp = 0; i; --i) {
                while (!mp[-temp].empty() && mp[-temp].top() > i) {
                        mp[-temp].pop();
                }
                if (!mp[-temp].empty()) {
                        ans = min(ans, max(0, i - mp[-temp].top()));
                }
                temp += a[i];
        }
        cout << (ans == n ? -1 : ans) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
