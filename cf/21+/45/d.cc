/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 30, M = N * (N - 1) >> 1;
int dp[N + 1][M + 1];
bool vis[N + 1][M + 1];

void solve() {
        int n, m;
        cin >> n >> m;
        int k = n * (n - 1) >> 1;
        k -= m;
        if (!dp[n][k]) {
                cout << 0 << endl;
                return;
        }
        m = n;
        vector<int> ans(n + 1);
        iota(all(ans), 0);
        reverse(ans.begin() + 1, ans.end());
        int idx = 1;
        while (m) {
                int t = dp[m][k];
                reverse(ans.begin() + idx, ans.begin() + idx + t);
                idx += t;
                k -= t * (t - 1) >> 1;
                m -= t;
        }
        for (int i = 1; i <= n; ++i) {
                cout << ans[i] << " \n"[i == n];
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        vis[0][0] = true;
        for (int i = 0; i < N; ++i) {
                for (int j = 0; j <= M; ++j) {
                        if (!vis[i][j]) {
                                continue;
                        }
                        for (int k = 1; i + k <= N; ++k) {
                                int t = k * (k - 1) >> 1;
                                dp[i + k][j + t] = k;
                                vis[i + k][j + t] = true;
                        }
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
