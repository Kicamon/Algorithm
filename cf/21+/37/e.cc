/* #pragma GCC optimize(2) */
#include <iostream>
#include <numeric>
#include <set>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, k;
        cin >> n >> k;
        vector<int> a(n), b(n);
        for (int &x : a) {
                cin >> x;
        }
        k--;
        set<int> s;
        for (int i = 0; i <= n; ++i) {
                s.insert(i);
        }
        k = k % 2 + 1;
        while (k--) {
                for (int i = 0; i < n; ++i) {
                        set<int> t = s;
                        for (int j = 0; j < n; ++j) {
                                if (i == j) {
                                        continue;
                                }
                                t.erase(a[j]);
                        }
                        b[i] = *t.begin();
                }
                a = b;
        }
        cout << accumulate(all(a), 0) << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
