/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n + 1), c(n + 1);
        for (int &x : a) {
                cin >> x;
                b[x]++;
        }
        for (int i = 1; i <= n; ++i) {
                if (b[i] % i) {
                        cout << -1 << endl;
                        return;
                }
                b[i] = 0;
        }
        int idx = 1;
        for (int i : a) {
                if (!c[i]) {
                        b[i] = idx++;
                }
                c[i] = (c[i] + 1) % i;
                cout << b[i] << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
