/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, q;
        cin >> n >> q;
        vector<int> a(n + 1);
        vector<array<int, 3>> b(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                int x = __lg(a[i]);
                b[i] = { b[i - 1][0] + x, b[i - 1][1] + (a[i] - 1 == (1 << x)),
                         b[i - 1][2] + (a[i] != (1 << x) && a[i] - 1 != (1 << x)) };
        }
        while (q--) {
                int l, r;
                cin >> l >> r;
                cout << (b[r][0] - b[l - 1][0]) + (b[r][1] - b[l - 1][1]) / 2 +
                            (b[r][2] - b[l - 1][2])
                     << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
