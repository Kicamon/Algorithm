/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, q;
        cin >> n >> q;
        vector<int> a(n + 1), b(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i];
                if (a[i] != a[i - 1]) {
                        b[i] = b[i - 1];
                } else {
                        b[i] = i;
                }
        }
        for (int i = 1; i <= n; ++i) {
                a[i] += a[i - 1];
        }
        while (q--) {
                int l, r;
                cin >> l >> r;
                if (((r - l + 1) % 3) || ((a[r] - a[l - 1]) % 3)) {
                        cout << -1 << endl;
                        continue;
                }
                cout << (r - l + 1) / 3 + (b[r] <= l) << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
