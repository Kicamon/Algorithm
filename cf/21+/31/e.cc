/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int &x : a) {
                cin >> x;
        }
        reverse(all(a));
        for (int &x : b) {
                cin >> x;
        }
        reverse(all(b));
        if (a[0] != b[0]) {
                cout << "NO\n";
                return;
        }
        vector<int> c = a;
        for (int i = 1; i < n; ++i) {
                if (a[i] == b[i]) {
                        continue;
                }
                if ((a[i] ^ a[i - 1]) != b[i] && (a[i] ^ c[i - 1]) != b[i]) {
                        cout << "NO\n";
                        return;
                }
                a[i] = b[i];
        }
        cout << "YES\n";
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
