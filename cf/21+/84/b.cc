#include <iostream>
using namespace std;

void solve() {
        int s, k, m;
        cin >> s >> k >> m;
        int t = s;
        if (s > k) {
                t = m / k % 2 ? k : s - k;
        }
        cout << max(0, t - m % k) << '\n';
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
