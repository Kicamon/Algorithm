#include <iostream>
using namespace std;

void solve() {
        int n, k;
        cin >> n >> k;
        int l = k, r = k;
        int ans = 0;
        while (true) {
                if (l <= n && n <= r) {
                        cout << ans << '\n';
                        break;
                }
                if (n < l) {
                        cout << -1 << '\n';
                        break;
                }
                l = l * 2 - 1;
                r = r * 2 + 1;
                ans++;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
