/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int l, r;
        cin >> l >> r;
        int n = r - l + 1;
        vector<int> a(n);
        iota(all(a), l);
        int L = 0, R = n;
        for (int i = 29; ~i; --i) {
                int x = L;
                while (x < R && (~a[x] >> i & 1)) {
                        x++;
                }
                if (x - L < R - x) {
                        int m = x * 2 - L;
                        reverse(a.begin() + L, a.begin() + m);
                        L = m;
                } else {
                        int m = x * 2 - R;
                        reverse(a.begin() + m, a.begin() + R);
                        R = m;
                }
        }
        ll ans = 0;
        for (int i = 0; i < n; ++i) {
                ans += a[i] | (l + i);
        }
        cout << ans << endl;
        for (int x : a) {
                cout << x << ' ';
        }
        cout << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
