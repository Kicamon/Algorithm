/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <map>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()

void solve() {
        int n;
        cin >> n;
        map<int, int> mp;
        for (int i = 0, x; i < n; ++i) {
                cin >> x;
                mp[x]++;
        }
        ll ans = 0;
        int num = 0;
        vector<ll> a;
        a.push_back(0);
        for (auto [x, y] : mp) {
                if (y % 2) {
                        a.push_back(x);
                        y--;
                }
                ans += 1ll * x * y;
                num += y;
        }
        sort(all(a));
        ll temp = 0;
        for (int i = 1; i < (int)a.size(); ++i) {
                if (ans + a[i - 1] > a[i]) {
                        temp = max(temp, a[i] + a[i - 1]);
                }
        }
        if (num > 2 || temp > 0) {
                ans += temp;
                cout << ans << endl;
        } else {
                cout << 0 << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
