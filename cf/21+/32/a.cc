#include <algorithm>
#include <iostream>
#include <iterator>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        int m;
        cin >> m;
        string s_;
        cin >> s_;
        string c;
        cin >> c;
        string st, sb;
        for (int i = 0; i < m; ++i) {
                if (c[i] == 'V') {
                        st += s_[i];
                } else {
                        sb += s_[i];
                }
        }
        reverse(all(st));
        s = st + s + sb;
        cout << s << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
