/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        ll sn;
        cin >> sn;
        string s = to_string(sn);
        int n = (int)s.size();
        vector<ll> ans;
        int m = n >> 1;
        ll temp = 1;
        for (int i = 1; i <= m; ++i) {
                temp *= 10;
                ll num = 0;
                for (int j = 0; j < n; j += i * 2) {
                        num *= temp;
                        for (int k = j; k < j + i; ++k) {
                                num *= 10;
                                num += s[k] - '0';
                        }
                }
                if (num + num * temp == sn) {
                        ans.push_back(num);
                }
        }
        cout << (int)ans.size() << endl;
        if (ans.size()) {
                sort(all(ans));
                for (ll x : ans) {
                        cout << x << ' ';
                }
                cout << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
