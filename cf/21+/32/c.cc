/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

vector<ll> a;

ll get(int t) {
        return a[t + 1] + a[t - 1] * t;
}

void solve() {
        int n;
        cin >> n;
        ll ans = 0;
        while (n) {
                int t = upper_bound(all(a), n) - a.begin() - 1;
                if (t) {
                        ans += get(t);
                        n -= a[t];
                } else {
                        ans += 3 * n;
                        break;
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        ll x = 1;
        while (x < 1e9) {
                a.push_back(x);
                x *= 3;
        }
        a.push_back(x * 3);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
