#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        vector<ll> dp(n);
        for (int &x : a) {
                cin >> x;
        }
        dp[0] = 1, dp[1] = a[1] > a[0] ? 4 : 3;
        for (int i = 2; i < n; ++i) {
                dp[i] = max(dp[i - 2] + i, dp[i - 1] + i);
        }
        // for (int i = 2; i < n; ++i) {
        //         if (a[i] < a[i - 1]) {
        //                 temp[0] = dp[i - 1] + i;
        //         }
        //         if (a[i] < a[i - 2]) {
        //                 temp[1] = dp[i - 2] + i;
        //         }
        //
        //         temp = { 0, 0 };
        // }
        ll temp = 0;
        for (int i = 0; i < n; ++i) {
                temp += dp[i];
        }
        cout << temp << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
