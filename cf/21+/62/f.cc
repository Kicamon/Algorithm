/* #pragma GCC optimize(2) */
#include <algorithm>
#include <iostream>
#include <numeric>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, m;
        cin >> n >> m;
        vector<int> a(n + 1);
        for (int i = 0, l, r; i < m; ++i) {
                cin >> l >> r;
                l--;
                a[l]++, a[r]--;
        }
        for (int i = 1; i < n; ++i) {
                a[i] += a[i - 1];
        }
        if (*max_element(all(a)) == m) {
                vector<int> c(n);
                iota(all(c), 0);
                sort(all(c), [&](int x, int y) { return a[x] > a[y]; });
                for (int i = 0; i < n; ++i) {
                        a[c[i]] = i;
                }
                for (int i = 0; i < n; ++i) {
                        cout << a[i] << " \n"[i == n - 1];
                }
        } else {
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
