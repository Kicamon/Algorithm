/* #pragma GCC optimize(2) */
#include <functional>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        int ans = 2;
        vector<bool> b(n);

        function<bool()> check = [&]() {
                vector<int> c(n, 1);
                for (int i = 1, x = 0; i < n; ++i) {
                        if (!b[i - 1]) {
                                x++;
                        }
                        c[i] += x;
                }
                for (int i = n - 2, x = 0; ~i; --i) {
                        if (b[i + 1]) {
                                x++;
                        }
                        c[i] += x;
                }
                return a == c;
        };

        for (int f = 0; f <= 1; ++f) {
                b[0] = f;
                bool flag = true;
                for (int i = 1; i < n; ++i) {
                        if (a[i - 1] == a[i]) {
                                b[i] = !b[i - 1];
                        } else if (a[i - 1] + 1 == a[i]) {
                                if (b[i - 1]) {
                                        ans--;
                                        flag = false;
                                        break;
                                }
                        } else if (a[i - 1] == a[i] + 1) {
                                if (!b[i - 1]) {
                                        ans--;
                                        flag = false;
                                        break;
                                }
                                b[i] = true;
                        } else {
                                ans--;
                                flag = false;
                                break;
                        }
                }
                if (flag && !check()) {
                        ans--;
                }
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
