/* #pragma GCC optimize(2) */
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
vector<vector<int> > vis(4, vector<int>());

int check(ll t) {
        for (int i = 0; i < 4; ++i) {
                if (t % vis[0][i] == 0) {
                        return 0;
                }
        }
        return 1;
}

void solve() {
        ll l, r;
        cin >> l >> r;
        ll res = 0;
        for (int i = 0; i < 4; ++i) {
                ll temp = 0;
                for (int j : vis[i]) {
                        temp += (r / j) - (l / j);
                }
                if (i % 2) {
                        res -= temp;
                } else {
                        res += temp;
                }
        }
        res = r - l - res + check(l);
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        vis[0] = { 2, 3, 5, 7 };
        for (int i = 0; i < 4; ++i) {
                for (int j = i + 1; j < 4; ++j) {
                        vis[1].push_back(vis[0][i] * vis[0][j]);
                        for (int k = j + 1; k < 4; ++k) {
                                vis[2].push_back(vis[0][i] * vis[0][j] * vis[0][k]);
                        }
                }
        }
        vis[3].push_back(210);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
