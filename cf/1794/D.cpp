#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
const int N = 1e6 + 10;

vint primes;
bool vis[N];

void prime(int m)
{
    for (int i = 2; i <= m; ++i)
    {
        if (!vis[i])
            primes.pb(i);
        for (int x : primes)
        {
            if ((ll)i * x > m)
                break;
            vis[i * x] = true;
            if (i % x == 0)
                break;
        }
    }
}

