// #pragma GCC optimize(2)
#include <bits/stdc++.h>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    int sum = 0;
    vector<int> f(n + 1, 0);
    int len = 0;
    for (int i = n - 1; ~i; --i) {
        int l = 0, r = len;
        while (l < r) {
            int mid = (l + r + 1) >> 1;
            if (f[mid] <= a[i]) {
                l = mid;
            } else {
                r = mid - 1;
            }
        }
        f[r + 1] = a[i];
        len = max(r + 1, len);
    }
    int len_ = 0;
    for (int i = n - 1; ~i; --i) {
        int l = 0, r = len_;
        while (l < r) {
            int mid = (l + r + 1) >> 1;
            if (f[mid] <= a[i]) {
                l = mid;
            } else {
                r = mid - 1;
            }
        }
        f[r + 1] = a[i];
        len_ = max(len_, r + 1);
        if (r + 1 == len) {
            int idx = 1, t = 0;
            for (int j = n - 1; ~j; --j) {
                if (idx <= len && a[j] == f[idx]) {
                    idx++;
                } else {
                    sum += a[j] < t;
                    t = a[j];
                }
            }
            break;
        }
    }
    cout << sum << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
