/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int mod = 2147483647;

void solve() {
    int n;
    cin >> n;
    map<int, int> p;
    for (int i = 0, c; i < n; ++i) {
        cin >> c;
        p[c]++;
    }
    int ans = 0;
    for (auto x : p) {
        int res = x.first ^ mod;
        ans += x.second;
        if (p.count(res) && p[res]) {
            p[res] -= min(x.second, p[res]);
        }
    }
    cout << ans << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
