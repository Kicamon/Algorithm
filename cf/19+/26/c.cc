/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

const int N = 2e5 + 10;
ll ele[N];

void solve() {
        int n;
        cin >> n;
        cout << ele[n] << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        for (int i = 1; i < N; ++i) {
                int res = i;
                ele[i] = ele[i - 1];
                while (res) {
                        ele[i] += res % 10;
                        res /= 10;
                }
        }

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
