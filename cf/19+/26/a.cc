#define all(x) (x).begin(), (x).end()

void solve() {
        string s;
        cin >> s;
        int res = 0;
        for (int i = 0; i < 5; ++i) {
                res += s[i] == 'A';
        }
        if (res >= 3) {
                cout << 'A' << endl;
        } else {
                cout << 'B' << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
