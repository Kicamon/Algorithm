/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<string> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    int idx = -1;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            if (a[i][j] == '1') {
                if (idx != -1 && idx != j) {
                    cout << "TRIANGLE" << endl;
                    return;
                } else {
                    idx = j;
                    break;
                }
            }
        }
    }
    cout << "SQUARE" << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
