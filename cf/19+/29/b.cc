#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n, k;
    cin >> n >> k;
    if (k == 4 * n - 2) {
        cout << n * 2 << endl;
    } else if (k == 4 * n - 3) {
        cout << n * 2 - 1 << endl;
    } else {
        int res = 0;
        res = (k + 1) >> 1;
        cout << res << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
