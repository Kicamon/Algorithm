/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    ll k, x, a;
    cin >> k >> x >> a;
    ll res = 1;
    a--;
    for (int i = 0; i < x; ++i) {
        if (a * (k - 1) <= res) {
            cout << "NO" << endl;
            return;
        }
        int p = res / (k - 1) + 1;
        res += p, a -= p;
    }
    cout << "YES" << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
