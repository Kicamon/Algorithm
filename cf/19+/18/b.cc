#include <bits/stdc++.h>
#include <functional>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<array<int, 2>> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i][0];
        }
        for (int i = 0; i < n; ++i) {
                cin >> a[i][1];
        }
        function<int(int)> check = [&](int opt) {
                int res = 0;
                for (int i = 0; i < n; ++i) {
                        res += a[i][opt] - i - 1;
                }
                return res;
        };
        sort(all(a));
        int res = check(1);
        sort(all(a), [&](auto x, auto y) { return x[1] < y[1]; });
        if (res < check(0)) {
                sort(all(a));
        }
        for (int _ = 0; _ < 2; ++_) {
                for (int i = 0; i < n; ++i) {
                        cout << a[i][_] << ' ';
                }
                cout << endl;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
