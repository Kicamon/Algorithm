/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        string s;
        cin >> s;
        int res = 1;
        bool f = false;
        for (int i = 1; i < s.size(); ++i) {
                if (s[i] != s[i - 1]) {
                        res++;
                }
                if (s[i] == '1' && s[i - 1] == '0') {
                        f = true;
                }
        }
        cout << res - f << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
