/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define int long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n, k, q;
    cin >> n >> k >> q;
    vector<int> a(k + 1), b(k + 1);
    for (int i = 1; i <= k; ++i) {
        cin >> a[i];
    }
    for (int i = 1; i <= k; ++i) {
        cin >> b[i];
    }
    while (q--) {
        int d;
        cin >> d;
        int l = 0, r = k;
        while (l < r) {
            int mid = (l + r + 1) >> 1;
            if (a[mid] > d) {
                r = mid - 1;
            } else {
                l = mid;
            }
        }
        int left = l;
        l = 0, r = k;
        while (l < r) {
            int mid = (l + r) >> 1;
            if (a[mid] < d) {
                l = mid + 1;
            } else {
                r = mid;
            }
        }
        int right = l;
        if (a[left] == a[right]) {
            cout << b[left] << ' ';
        } else {
            int time = b[left];
            time += floor(((double)(d - a[left]) * (b[right] - b[left]) / (a[right] - a[left])));
            cout << time << ' ';
        }
    }
    cout << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
