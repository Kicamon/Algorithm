/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <functional>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<string> s(n << 1);
    function<void(string, string, int, int)> add = [&](string a, string b, int i, int j) {
        if (j & 1) {
            s[i << 1] += a;
            s[i << 1 | 1] += a;
        } else {
            s[i << 1] += b;
            s[i << 1 | 1] += b;
        }
    };
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            if (i & 1) {
                add("##", "..", i, j);
            } else {
                add("..", "##", i, j);
            }
        }
    }
    for (int i = 0; i < n << 1; ++i) {
        cout << s[i] << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
