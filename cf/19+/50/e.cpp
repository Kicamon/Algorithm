/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    string s;
    cin >> s;
    for (int len = 1; len <= n; ++len) {
        if (n % len != 0) {
            continue;
        }
        int res = 0;
        int tmp = n / len;
        for (int i = 0; i < len; ++i) {
            map<char, int> num;
            for (int j = i; j < n; j += len) {
                num[s[j]]++;
            }
            int t = max(num.begin()->second, num.rbegin()->second);
            if (t < tmp) {
                res += tmp - t;
            }
        }
        if (res <= 1) {
            cout << len << endl;
            return;
        }
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
