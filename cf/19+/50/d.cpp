/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 1e5;

bool dp[N + 10];

void solve() {
    int n;
    cin >> n;
    cout << (dp[n] ? "YES" : "NO") << endl;
}

int get_num(int num) {
    int res = 0;
    for (int i = 4; ~i; --i) {
        res = res * 10 + (num >> i & 1);
    }
    return res;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    dp[1] = dp[N] = true;
    vector<int> num(1 << 5);
    for (int i = 1; i < (1 << 5); ++i) {
        num[i] = get_num(i);
    }
    for (int i = 1; i <= N; ++i) {
        for (int j = 2; j < (1 << 5); ++j) {
            ll t = (ll)i * num[j];
            if (t >= N) {
                break;
            }
            if (dp[i]) {
                dp[t] = dp[i];
            }
        }
    }

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
