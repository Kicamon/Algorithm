#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string ans = "aaa";
        int idx = 2;
        while (~idx && n >= 28) {
                ans[idx--] = 'z';
                n -= 26;
        }
        if (idx == 2) {
                ans[idx--] = char('a' + n - 3);
                n = 2;
        }
        if (idx == 1) {
                ans[idx--] = char('a' + n - 2);
                n = 1;
        }
        if (idx == 0) {
                ans[idx] = char('a' + n - 1);
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
