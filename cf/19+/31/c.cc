/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        int idx = 1;
        int len = 1;
        while (idx < n && a[idx] == a[idx - 1]) {
                idx++;
                len++;
        }
        idx = n - 2;
        int len_2 = 1;
        while (~idx && a[idx] == a[idx + 1]) {
                idx--;
                len_2++;
        }
        if (a[0] == a[n - 1]) {
                len += len_2;
        } else {
                len = max(len, len_2);
        }
        len = min(len, n);
        cout << n - len << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
