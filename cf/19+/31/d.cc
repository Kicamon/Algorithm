/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <map>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n, x, y;
        cin >> n >> x >> y;
        vector<int> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        map<array<int, 2>, int> p;
        ll ans = 0;
        for (int i = 0; i < n; ++i) {
                auto val = p.find(array{ (x - a[i] % x) % x, a[i] % y });
                if (val != p.end()) {
                        ans += val->second;
                }
                p[array{ a[i] % x, a[i] % y }]++;
        }
        cout << ans << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
