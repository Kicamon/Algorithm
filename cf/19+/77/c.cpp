#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

ll get_lcm(ll a, ll b) {
    return a * b / gcd(a, b);
}

void solve() {
    int n;
    cin >> n;
    map<ll, int> p, s;
    ll tmp = 1;
    for (int i = 0, c; i < n; ++i) {
        cin >> c;
        tmp = get_lcm(tmp, (ll)c);
        cout << i << ' ';
        Debug(tmp);
        p[c]++;
    }
    if (!p.count(tmp)) {
        cout << n << endl;
        return;
    }
    for (auto val : p) {
        for (auto val2 : p) {
            if (val.first < val2.first) {
                continue;
            }
            ll x = get_lcm(val.first, val2.first);
            if (!p.count(x)) {
                s[x] = 1;
            }
        }
        for (auto val2 : s) {
            if (val.first < val2.first) {
                continue;
            }
            ll x = get_lcm(val.first, val2.first);
            if (!p.count(x)) {
                s[x] = 1;
            }
        }
    }
    int res = 0;
    for (auto val : s) {
        for (auto val2 : p) {
            if (val2.first >= val.first) {
                break;
            }
            if (val.first % val2.first) {
                continue;
            }
            s[val.first] += val2.second;
        }
        res = max(res, s[val.first] - 1);
    }
    cout << res << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
