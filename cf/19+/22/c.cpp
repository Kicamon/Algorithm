/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const ll inf = 1e10;

void solve() {
    int n;
    cin >> n;
    vector<ll> a(n + 2);
    a[0] = -inf, a[n + 1] = inf;
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    vector<int> e(n + 1);
    for (int i = 1; i <= n; ++i) {
        if (a[i] - a[i - 1] < a[i + 1] - a[i]) {
            e[i] = i - 1;
        } else {
            e[i] = i + 1;
        }
    }
    vector<ll> f(n + 1);
    for (int i = 2; i <= n; ++i) {
        if (e[i - 1] > i - 1) {
            f[i] = f[i - 1] + 1;
        } else {
            f[i] = f[i - 1] + a[i] - a[i - 1];
        }
    }
    vector<ll> d(n + 1);
    for (int i = n - 1; i; --i) {
        if (e[i + 1] < i + 1) {
            d[i] = d[i + 1] + 1;
        } else {
            d[i] = d[i + 1] + a[i + 1] - a[i];
        }
    }
    int m;
    cin >> m;
    while (m--) {
        int x, y;
        cin >> x >> y;
        if (x < y) {
            cout << f[y] - f[x] << endl;
        } else {
            cout << d[y] - d[x] << endl;
        }
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
