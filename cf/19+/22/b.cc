/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        vector<int> a(n + 1);
        for (int i = 0, c; i < n; ++i) {
                cin >> c;
                a[c]++;
        }
        ll res = 0, sum = 0;
        for (int x : a) {
                res += 1ll * x * (x - 1) * (x - 2) / 6;
                res += 1ll * x * (x - 1) / 2 * sum;
                sum += x;
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
