/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <functional>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
    int n;
    cin >> n;
    vector<int> a(n), d(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> d[i];
    }
    set<int> alive, die;
    for (int i = 0; i < n; ++i) {
        alive.insert(i);
    }
    for (int i = 0; i < n; ++i) {
        if ((i ? a[i - 1] : 0) + (i + 1 < n ? a[i + 1] : 0) > d[i]) {
            die.insert(i);
        }
    }
    for (int i = 0; i < n; ++i) {
        cout << die.size() << ' ';
        set<int> to_die;
        for (int x : die) {
            alive.erase(x);
        }
        auto check = [&](auto it) {
            if ((it != alive.begin() ? a[*prev(it)] : 0) +
                    (next(it) != alive.end() ? a[*next(it)] : 0) >
                d[*it]) {
                to_die.insert(*it);
            }
        };
        for (int x : die) {
            auto it = alive.lower_bound(x);
            if (it != alive.end()) {
                check(it);
            }
            if (it != alive.begin()) {
                check(prev(it));
            }
        }
        to_die.swap(die);
    }
    cout << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
