#include <algorithm>
#include <iostream>
#include <set>
#include <vector>
using namespace std;
using ll = long long;
const ll N = 1e8;

void solve() {
        int n;
        cin >> n;
        vector<ll> a(n);
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
        }
        sort(a.begin(), a.end());
        ll t = 2;
        while (t <= a.back() * 2) {
                set<ll> st;
                for (int i = 0; i < n; ++i) {
                        st.insert(a[i] % t);
                        if (st.size() > 2) {
                                break;
                        }
                }

                if (st.size() == 2) {
                        cout << t << '\n';
                        break;
                }
                t <<= 1;
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(nullptr);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
