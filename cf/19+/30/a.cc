/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        int m = n << 1;
        vector<int> a(m);
        for (int i = 0; i < m; ++i) {
                cin >> a[i];
        }
        sort(all(a));
        ll res = 0;
        for (int i = 0; i < m; i += 2) {
                res += a[i];
        }
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--) {
                solve();
        }

        return 0;
}
