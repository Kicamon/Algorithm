#define all(x) (x).begin(), (x).end()
const int N = 3e5 + 10;

ll a[N];
ll tr[N << 2];

void build(int u, int l, int r) {
        tr[u] = 0;
        if (l == r) {
                tr[u] = a[l];
                return;
        }
        int mid = l + r >> 1;
        build(u << 1, l, mid), build(u << 1 | 1, mid + 1, r);
}

array<int, 2> check(int u, int l, int r) {
        if (l == r) {
                return { l, u };
        }
        int mid = l + r >> 1;
        array<int, 2> L = check(u << 1, l, mid);
        array<int, 2> R = check(u << 1 | 1, mid + 1, r);
        if (L[0] >= R[0]) {
                return L;
        }
        return R;
}

