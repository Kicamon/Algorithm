#define all(x) (x).begin(), (x).end()

void solve() {
        int n;
        cin >> n;
        string s;
        cin >> s;
        int j = 0, res = 0;
        for (int i = 0; i < n; ++i) {
                if (s[i] == '.') {
                        j++;
                } else {
                        if (j > 2) {
                                res = 2;
                                j = 0;
                                break;
                        }
                        res += j;
                        j = 0;
                }
        }
        if (j > 2) {
                res = 2;
                j = 0;
        }
        res += j;
        cout << res << endl;
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int t;
        cin >> t;
        while (t--)
                solve();

        return 0;
}
