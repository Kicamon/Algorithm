/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<vector<int>> g(n + 1);
        vector<bool> vis(n + 1);
        vector<array<int, 2>> a(n + 1);
        for (int i = 1; i <= n; ++i) {
                cin >> a[i][0] >> a[i][1];
                g[a[i][0]].push_back(a[i][1]);
                g[a[i][1]].push_back(a[i][0]);
        }
        vector<int> ans;
        vis[1] = 1;
        ans.push_back(1);
        queue<int> q;
        q.push(1);
        while (!q.empty()) {
                int t = q.front();
                q.pop();
                for (int x : g[t]) {
                        if (!vis[x]) {
                                q.push(x);
                                vis[x] = true;
                                ans.push_back(x);
                                break;
                        }
                }
        }
        if (ans[1] != a[1][0] && ans[1] != a[1][1]) {
                reverse(all(ans));
        }
        for (int x : ans) {
                cout << x << " \n"[x == ans.back()];
        }

        return 0;
}
