#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<int> a(n);
        for (int &x : a) {
                cin >> x;
        }
        if (n == 2) {
                cout << 0 << endl;
        } else {
                sort(all(a));
                cout << min(a[n - 1] - a[1], a[n - 2] - a[0]) << endl;
        }

        return 0;
}
