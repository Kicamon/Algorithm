/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, m;
        cin >> n >> m;
        vector<ll> a(n), val(n);
        int idx = 0;
        for (int i = 0; i < n; ++i) {
                cin >> a[i];
                if (a[i] < a[idx]) {
                        idx = i;
                }
        }
        ll ans = 0;
        for (int i = 0; i < n; ++i) {
                if (i == idx) {
                        continue;
                }
                val[i] = a[idx] + a[i];
                ans += a[idx] + a[i];
        }
        for (ll i = 0, x, y, w; i < m; ++i) {
                cin >> x >> y >> w;
                x--, y--;
                if ((x == idx || y == idx) && w < a[x] + a[y]) {
                        ans -= a[x] + a[y];
                        ans += w;
                } else if (w < max(val[x], val[y])) {
                        ans -= max(val[x], val[y]);
                        ans += w;
                        if (val[x] > val[y]) {
                                val[x] += val[y];
                        } else {
                                val[y] += val[x];
                        }
                }
        }
        cout << ans << endl;

        return 0;
}
