/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        string s;
        cin >> s;
        vector<int> a(n + 1), sum(n + 1);
        for (int i = 1; i <= n; ++i) {
                if (s[i - 1] == '(') {
                        a[i] = -1;
                } else {
                        a[i] = 1;
                }
                sum[i] = sum[i - 1] + a[i];
        }
        if (sum[n] != 2 && sum[n] != -2) {
                cout << 0 << endl;
        } else {
                int ans = 0;
                if (sum[n] == 2) {
                        for (int i = 1; i <= n; ++i) {
                                if (sum[i] < 0) {
                                        ans = 0;
                                        break;
                                }
                                if (sum[i] >= 2 && a[i] > 0) {
                                        ans++;
                                }
                        }
                } else {
                        for (int i = 1; i <= n; ++i) {
                                if (sum[i] > 0) {
                                        ans = 0;
                                        break;
                                }
                                if (sum[i] <= -2 && a[i] < 0) {
                                        ans++;
                                }
                        }
                }
                cout << ans << endl;
        }

        return 0;
}
