/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, k;
        cin >> n >> k;
        if (n < k) {
                cout << "NO\n";
                return 0;
        }
        bitset<32> a = n;
        if (a.count() > k) {
                cout << "NO\n";
                return 0;
        }
        cout << "YES\n";
        if (n & 1) {
                n--, k--;
                a ^= 1;
                cout << 1 << ' ';
        }
        while (k > a.count()) {
                int x = a._Find_first();
                int t = 1 << x;
                if (k - a.count() >= t) {
                        k -= t;
                        for (int i = 0; i < t; ++i) {
                                cout << 1 << ' ';
                        }
                } else {
                        int p = k - a.count() + 1;
                        queue<int> q;
                        q.push(t);
                        while ((int)q.size() < p) {
                                q.push(q.front() / 2);
                                q.push(q.front() / 2);
                                q.pop();
                        }
                        while (!q.empty()) {
                                cout << q.front() << ' ';
                                q.pop();
                        }
                        k = a.count() - 1;
                }
                a ^= t;
        }
        for (int i = a._Find_first(); i != (int)a.size(); i = a._Find_next(i)) {
                cout << (1 << i) << ' ';
        }
        cout << endl;

        return 0;
}
