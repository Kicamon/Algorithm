/* #pragma GCC optimize(2) */
#include <bitset>
#include <iostream>
#include <algorithm>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 7001;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, q;
        cin >> n >> q;
        vector<bitset<N> > a(n + 1), pr(N), pre(N);
        bitset<N> mu;

        mu.set();
        for (int i = 2; i * i < N; ++i) {
                for (int j = 1; i * i * j < N; ++j) {
                        mu.reset(i * i * j);
                }
        }
        for (int i = 1; i < N; ++i) {
                for (int j = 1; i * j < N; ++j) {
                        pr[i * j].set(i);
                        pre[i][i * j] = mu[j];
                }
        }

        int op, x, y, v;
        while (q--) {
                cin >> op;
                if (op == 1) {
                        cin >> x >> v;
                        a[x] = pr[v];
                } else if (op == 2) {
                        cin >> x >> y >> v;
                        a[x] = a[y] ^ a[v];
                } else if (op == 3) {
                        cin >> x >> y >> v;
                        a[x] = a[y] & a[v];
                } else {
                        cin >> x >> v;
                        cout << ((a[x] & pre[v]).count() & 1);
                }
        }
        cout << endl;

        return 0;
}
