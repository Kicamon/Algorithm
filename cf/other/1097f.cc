/* #pragma GCC optimize(2) */
#include <bitset>
#include <iostream>
#include <vector>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n, q;
        cin >> n >> q;
        vector<bitset<7001> > a(n + 1);
        int op, x, y, v;
        while (q--) {
                cin >> op;
                if (op == 1) {
                        cin >> x >> v;
                        a[x] = 0;
                        a[x] |= 1 << v;
                } else if (op == 2) {
                        cin >> x >> y >> v;
                        a[x] = a[y] ^ a[v];
                } else if (op == 3) { // 如何计算gcd
                        cin >> x >> y >> v;
                } else {
                        cin >> x >> v;
                        a[x].test(v + 1);
                }
        }

        return 0;
}
