//#pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;
#define ai2 array<int,2>

void solve()
{
	int n,m;
	cin >> n >> m;
	vint a(n);
	for(int i = 0;i < n;++i)
		cin >> a[i];
	vint b = a;
	sort(all(b));
	int i;
	for(i = 0;i < n;++i)
	{
		if(b[i] > m)
			break;
		m -= b[i];
	}
	
	if(i == n)
		cout << 1 << endl;
	else if(i == 0)
		cout << n + 1 << endl;
	else 
	{
		m += b[i - 1];
		if(m >= a[i])
			cout << n - i << endl;
		else cout << n - i + 1 << endl;
	}
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
