#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
	int n;
	cin >> n;
	int a = n >> 1,b = n - a;
	auto get = [&](int x)
	{
		int res = 0;
		while(x)
		{
			res += x % 10;
			x /= 10;
		}
		return res;
	};
	if(a % 10 == 9 && b % 10 == 0)
	{
		int x = get(a),y = get(b);
		int t = x - y >> 1;
		int k = t % 9;
		t -= k;
		while(t)
		{
			k = k * 10 + 9;
			t -= 9;
		}
		a -= k,b += k;
	}
	cout << a << ' ' << b << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
