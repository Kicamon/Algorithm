#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(),a.end()
#define rall(a) a.rbegin(),a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 1;

void solve()
{
	int n;
	cin >> n;
	if(n & 1)
	{
		int t = n + 1 >> 1,k = n << 1;
		vector<array<int,2>> ans;
		for(int i = t,j = k;i;--i,--j)
			ans.pb({i,j});
		k = n + t - 1,t = n;
		for(int i = t,j = k;j > n;--i,--j)
			ans.pb({i,j});
		cout << "YES\n";
		for(auto x : ans)
			cout << x[0] << ' ' << x[1] << endl;
	}
	else cout << "NO\n";
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
	
    //clock_t start, finish;
    //start = clock();
    
	int t = 1;
	if(input)
		cin >> t;
	while(t--)
		solve();
    
    //finish = clock();
    //cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;
    
    return 0;
}
