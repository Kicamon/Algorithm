// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int n;
        cin >> n;
        int m = 1 << (n - 1);
        vector<vector<int>> f(n, vector<int>(m));
        for (int i = 0; i < n; ++i)
                for (int j = 0; j < m >> i; ++j)
                        cin >> f[i][j];
        int k;
        cin >> k;
        reverse(f.begin(), f.end());
        vector<int> res(2);
        res[0] = k, res[1] = f[0][0];
        for (int i = 1; i < n; ++i) {
                vector<int> cnt((int)res.size() << 1);
                for (int j = 0; j < 1 << i; ++j) {
                        if (res[j] > res[j + 1])
                                swap(res[j], res[j + 1]);
                        if (f[i][j] > f[i][j + 1])
                                swap(f[i][j], f[i][j + 1]);
                        if (res[j] < f[i][j] || res[j + 1] < f[i][j + 1]) {
                                cout << "No Solution\n";
                                return 0;
                        } else {
                                cnt[j << 1] = f[i][j], cnt[j << 1 | 1] = res[j];
                                ++j;
                                cnt[j << 1] = f[i][j], cnt[j << 1 | 1] = res[j];
                        }
                }
                res = cnt;
        }
        for (int i = 0; i < (int)res.size(); ++i) {
                if (i)
                        cout << ' ';
                cout << res[i];
        }
        cout << endl;

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
