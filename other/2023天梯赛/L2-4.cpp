// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl

int mx[4] = {1, 0, -1, 0}, my[4] = {0, 1, 0, -1};

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    // clock_t start, finish;
    // start = clock();

    int n, m;
    cin >> n >> m;
    vector<string> s(n);
    for (int i = 0; i < n; ++i)
        cin >> s[i];
    vector<vector<bool>> vis(n, vector<bool>(m));
    int idx = 0, num = 0;
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < m; ++j)
        {
            if (s[i][j] != '0' && !vis[i][j])
            {
                queue<pair<int, int>> q;
                q.push({i, j});
                vis[i][j] = true;
                bool f = false;
                while (!q.empty())
                {
                    auto t = q.front();
                    q.pop();
                    if (s[t.first][t.second] != '1')
                        f = true;
                    for (int k = 0; k < 4; ++k)
                    {
                        int a = t.first + mx[k], b = t.second + my[k];
                        if (a < 0 || a >= n || b < 0 || b >= m)
                            continue;
                        if (vis[a][b] || s[a][b] == '0')
                            continue;
                        vis[a][b] = true;
                        q.push({a, b});
                    }
                }
                num += f;
                ++idx;
            }
        }
    }
    cout << idx << ' ' << num << endl;

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
