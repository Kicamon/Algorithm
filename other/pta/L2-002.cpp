// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
const int N = 1e4 + 10;

bool vis[N];

signed main()
{
    // clock_t start, finish;
    // start = clock();

    string n;
    int m;
    cin >> n >> m;
    vector<array<int, 3>> v(m);
    int root;
    set<int> sv;
    for (int i = 0; i < m; ++i)
    {
        cin >> v[i][0] >> v[i][1] >> v[i][2];
        if (!sv.count(v[i][0]))
            root = v[i][0];
        sv.insert(v[i][2]);
    }
    sort(all(v));
    vector<array<int, 3>> um, hm;
    while (root != -1)
    {
        int l = 0, r = m - 1;
        while (l < r)
        {
            int mid = l + r >> 1;
            if (v[mid][0] < root)
                l = mid + 1;
            else
                r = mid;
        }
        int k = abs(v[l][1]);
        if (vis[k])
        {
            if (!hm.empty())
                hm.back()[2] = v[l][0];
            um.back()[2] = v[l][2];
            hm.push_back(v[l]);
        }
        else
            um.push_back(v[l]);
        vis[k] = true;
        root = v[l][2];
    }
    for (auto x : um)
        printf("%05d %d %05d\n", x[0], x[1], x[2]);
    for (auto x : hm)
        printf("%05d %d %05d\n", x[0], x[1], x[2]);

    // finish = clock();
    // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

    return 0;
}
