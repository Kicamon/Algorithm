// #pragma GCC optimize(2)
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define all(a) a.begin(), a.end()
#define rall(a) a.rbegin(), a.rend()
#define ll long long
#define vint vector<int>
#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        int A = 0, B = 0;
        vint a(k), b(k), c(k), d(k);
        for (int i = 0; i < k; ++i)
                cin >> a[i] >> b[i] >> c[i] >> d[i];
        for (int i = 0; i < k; ++i) {
                if (b[i] == d[i])
                        continue;
                int p = a[i] + c[i];
                A += (p == b[i]);
                B += (p == d[i]);
                if (A > n) {
                        cout << 'A' << endl;
                        cout << B << endl;
                        return;
                }
                if (B > m) {
                        cout << 'B' << endl;
                        cout << A << endl;
                        return;
                }
        }
}

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        // clock_t start, finish;
        // start = clock();

        int t = 1;
        if (input)
                cin >> t;
        while (t--)
                solve();

        // finish = clock();
        // cout <<endl<<"the time cost is:" << double(finish - start) / CLOCKS_PER_SEC<<endl;

        return 0;
}
