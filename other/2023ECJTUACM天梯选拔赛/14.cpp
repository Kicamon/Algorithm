#define pb push_back
#define Debug(x) cout << #x << ':' << x << endl
int input = 0;
#define ai2 array<int, 2>
const int inf = 0x3f3f3f3f;

const int N = 10010;
vint w[N];
bool vis[N];

int bfs(int u)
{
    memset(vis, 0, sizeof vis);
    queue<ai2> q;
    q.push({0, u});
    ai2 res = {0, inf};
    while (!q.empty())
    {
        auto t = q.front();
        q.pop();
        if (vis[t[1]])
            continue;
        vis[t[1]] = true;
        if (t[0] > res[0] || t[1] < res[1])
            res = t;

        for (int x : w[t[1]])
            q.push({t[0] + 1, x});
    }

    if (res[1] == u)
        res[1] = 0;
    return res[1];
}

