int input = 0;
const int N = 110;

int p[N], wf[N][N], w[N][N];

int findp(int x) {
        if (p[x] != x)
                p[x] = findp(p[x]);
        return p[x];
}

void solve() {
        int n, m, k;
        cin >> n >> m >> k;
        for (int i = 1; i <= n; ++i)
                p[i] = i;
        int a, b, op;
        while (m--) {
                cin >> a >> b >> op;
                w[a][b] = w[b][a] = op;
                if (op == 1) {
                        a = findp(a), b = findp(b);
                        p[a] = b;
                }
        }
        while (k--) {
                int a, b;
                cin >> a >> b;
                if (w[a][b] == 1)
                        cout << "No problem" << endl;
                else if (w[a][b] == -1) {
                        a = findp(a), b = findp(b);
                        if (a == b)
                                cout << "OK but...\n";
                        else
                                cout << "No way\n";
                } else
                        cout << "OK\n";
        }
}

