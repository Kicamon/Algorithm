#include <algorithm>
#include <cstring>
#include <iostream>
#include <queue>
#include <vector>

using namespace std;

const int N = 35;

int n;
int a[N], b[N], p[N];
int l[N], r[N];

int build(int al, int ar, int bl, int br, int d) {
        if (al > ar)
                return 0;
        int val = a[ar];
        int k = p[val];
        l[val] = build(al, al + k - 1 - bl, bl, k - 1, d + 1);
        r[val] = build(al + k - bl, ar - 1, k + 1, br, d + 1);
        return val;
}

void bfs() {
        queue<int> q;
        q.push(a[n - 1]);
        while (q.size()) {
                int t = q.front();
                q.pop();
                cout << t << ' ';
                if (l[t])
                        q.push(l[t]);
                if (r[t])
                        q.push(r[t]);
        }
}

int main() {
        cin >> n;
        for (int i = 0; i < n; i++)
                cin >> a[i];
        for (int i = 0; i < n; i++)
                cin >> b[i];
        for (int i = 0; i < n; i++)
                p[b[i]] = i;

        build(0, n - 1, 0, n - 1, 0);
        bfs();

        return 0;
}
