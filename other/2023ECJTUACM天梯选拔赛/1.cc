void solve()
{
    string n;
    cin >> n;
    int len = n.size();
    int num = 0;
    for (int i = 0; i < len; ++i)
        if (n[i] == '2')
            num++;
    double t = 1;
    if (((n[len - 1] - '0') & 1) == 0)
        t *= 2;
    if (n[0] == '-')
        t *= 1.5, len--;
    printf("%.2lf%\n", 1.0 * num / len * t * 100);
}

