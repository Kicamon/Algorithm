/* #pragma GCC optimize(2) */
#include <bitset>
#include <iostream>
#include <vector>
#include <array>
using namespace std;
#define endl '\n'
#define ll long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()
const int N = 101;

signed main() {
        ios::sync_with_stdio(false);
        cin.tie(0);

        int n;
        cin >> n;
        vector<array<int, 2>> a(n);
        for (auto &x : a) {
                cin >> x[0] >> x[1];
        }
        vector<bitset<N * N * N>> f(n + 1);
        f[0][0] = 1;
        for (int i = 1; i <= n; ++i) {
                for (int j = a[i - 1][0]; j <= a[i - 1][1]; ++j) {
                        f[i] |= f[i - 1] << (j * j);
                }
        }
        cout << f[n].count() << endl;

        return 0;
}
